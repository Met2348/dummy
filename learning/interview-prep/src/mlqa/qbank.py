def categories() -> list[str]:
    seen, out = set(), []
    for qa in BANK:
        if qa.cat not in seen:
            seen.add(qa.cat)
            out.append(qa.cat)
    return out


def quiz(cat: str | None = None, n: int | None = None) -> list[QA]:
    """确定性抽题：给 cat 则只取该类，n 截断前 n 道。"""
    pool = [qa for qa in BANK if cat is None or qa.cat == cat]
    return pool[:n] if n else pool


def grade(answer: str, qa: QA) -> float:
    """按采分关键词命中率打分(0..1)。命中即答案里出现该关键词(大小写不敏感)。"""
    low = answer.lower()
    hit = sum(1 for kw in qa.keys if kw.lower() in low)
    return hit / len(qa.keys)


def _self_test() -> None:
    assert len(BANK) >= 40, len(BANK)
    assert len(categories()) == 8, categories()
    # 每题都有非空关键词
    assert all(qa.keys for qa in BANK)
    # 完美答案(拼上所有关键词)得满分；空答案得 0
    sample = BANK[0]
    perfect = " ".join(sample.keys)
    assert grade(perfect, sample) == 1.0
    assert grade("", sample) == 0.0
    # 标准答案本身应能命中自己大部分关键词(题目自洽性检查)
    self_scores = [grade(qa.a, qa) for qa in BANK]
    avg = sum(self_scores) / len(self_scores)
    assert avg >= 0.6, f"题库自洽性低: {avg:.2f}"
    # 分类抽题
    assert all(qa.cat == "Transformer" for qa in quiz("Transformer"))
    print(f"[PASS] qbank: {len(BANK)}题/8类 + 关键词自评 + 自洽性 {avg:.0%}")


if __name__ == "__main__":
    _self_test()
