from collections import Counter


def range_bitwise_and(left: int, right: int) -> int:
    """
    【题意】给定两个整数 left、right（`left <= right`），求闭区间 `[left, right]`
    内所有整数按位与（AND）的结果。

    【思路】按位与是"只要有一个数在这一位是 0，结果这一位就是 0"，区间内数字越多、
    越容易在某一位上出现 0，所以结果只保留 left 和 right 二进制表示的**公共前缀**
    （从最高位开始、两者相同的那一段），公共前缀之后的位必然会在区间中途翻转出 0，
    最终与运算结果这些低位全部归零。原因是：只要 `left != right`，区间内一定包含
    某个"公共前缀之后全 0"和某个"公共前缀之后全 1"之间的过渡地带，会让公共前缀
    以外的每一位都在某个中间数字上取到 0。具体求公共前缀的方法很直接：把 left、
    right 同步右移（相当于同时丢弃最低位），直到两者相等——此时剩下的部分就是
    公共前缀，再把这个前缀左移回原来丢弃的位数，就是最终答案（左移出来的位置自动
    补 0，对应"公共前缀之后必然被消成 0"这个结论）。

    【复杂度】时间 O(1)（最多右移 32 次，与整数位宽有关，和 `right - left` 的区间
    大小无关，这是本题相对"暴力枚举区间内每个数逐一 AND"最大的优势）；空间 O(1)。

    【易错点】
    - 容易写成暴力遍历 `[left, right]` 逐个数 AND 起来——在 `right - left` 巨大
      （题目允许到 2^31 级别）时会直接超时，必须利用"公共前缀"这个性质避免遍历
      整个区间。
    - 移位的计数 `shift` 必须记录"一共右移了多少次"，最后要把结果**左移回同样
      的位数**（`left << shift`），忘记左移回去会得到"公共前缀本身的数值"而不是
      "公共前缀在原来位置上的数值"，两者数值上差了 `2^shift` 倍。
    - `left == right` 时循环直接不执行（shift=0），直接返回 `left` 本身——这是
      本题一个容易被忽略但天然正确处理的边界，不需要额外特判。
    """
    shift = 0
    while left < right:
        left >>= 1
        right >>= 1
        shift += 1
    return left << shift


def valid_utf8(data: list[int]) -> bool:
    """
    【题意】给一个整数数组 data，每个整数代表一个字节（只关心其最低 8 位），判断
    这一串字节是否构成合法的 UTF-8 编码序列。UTF-8 规则：1 字节字符最高位是 0；
    n 字节字符（n=2,3,4）的第一个字节前 n 位是 1、第 n+1 位是 0，后面 n-1 个"续
    字节"每个都以 `10` 开头。

    【思路】这是一道"按状态机逐字节校验格式"的位运算模拟题：维护一个"还需要读取
    几个续字节"的计数器 `remaining`。扫描到一个新字节时：
    - 如果 `remaining == 0`（说明上一个字符已经读完，这个字节应该是某个新字符的
      **起始字节**），检查它的最高若干位，决定这是 1/2/3/4 字节字符中的哪一种
      （用 `byte >> k` 和固定的位模式比较，取出最高的若干位）——1 字节字符不需要
      设置 `remaining`；2/3/4 字节字符分别把 `remaining` 设为 1/2/3（还需要读取
      的续字节数）；如果最高位既不匹配 1 字节模式也不匹配 2/3/4 字节的起始模式
      （比如以 `11111` 开头，或者单独出现一个 `10xxxxxx` 续字节却没有起始字节），
      直接判定为非法编码。
    - 如果 `remaining > 0`（正在等待续字节），检查当前字节的最高两位是否是
      `10`——不是就非法；是的话 `remaining -= 1`，继续处理下一个字节。
    整个数组扫描完之后，`remaining` 必须归零（不能停在"还欠着续字节没读完"的
    状态）才算合法。

    【复杂度】时间 O(m)（m 为 data 长度，每个字节 O(1) 处理）；空间 O(1)。

    【易错点】
    - 判断起始字节的模式时，必须**从最长的模式（4 字节 `11110`）到最短的模式
      （1 字节 `0`）按顺序比较、或者用足够精确的位移和掩码**——如果顺序或位数
      判断有误，容易把一个本该非法的字节（比如 `11111000`，5 个前导 1，不属于
      任何合法模式）误判成合法的某种字节数。
    - 数组里的整数可能大于 255（题目只关心最低 8 位），如果直接对整个整数做位
      比较而不限制在 8 位以内，理论上可能引入高位的干扰；本题给的测试数据都在
      0-255 范围内，但更严谨的实现应该先 `byte & 0xFF` 再处理。
    - 扫描结束时必须检查 `remaining == 0`——如果数组在读到一半（还有续字节没读完）
      就结束了，说明这是一个不完整的字符，必须判定为非法，容易忘记这个"扫尾检查"。
    """
    remaining = 0
    for byte in data:
        if remaining == 0:
            if byte >> 5 == 0b110:
                remaining = 1
            elif byte >> 4 == 0b1110:
                remaining = 2
            elif byte >> 3 == 0b11110:
                remaining = 3
            elif byte >> 7 != 0:
                return False
        else:
            if byte >> 6 != 0b10:
                return False
            remaining -= 1
    return remaining == 0


def find_num_of_valid_words(words: list[str], puzzles: list[str]) -> list[int]:
    """
    【题意】words 是一批小写字母单词，puzzles 是一批恰好 7 个不重复字母组成的
    "谜题"字符串。一个单词相对某个谜题"合法"，当且仅当：1) 单词包含谜题的第一个
    字母；2) 单词里的每个字母都出现在谜题里。对每个谜题，统计有多少个单词对它合法。

    【题意补充】单词和谜题都只关心"用到了哪些字母"，不关心字母出现的次数或顺序——
    这个信号提示可以把每个字符串压缩成一个 26 位的位掩码（第 k 位为 1 表示字母
    `'a'+k` 在这个字符串里出现过），字符串本身的具体内容就不再需要，只需要比较
    掩码。

    【思路】先把所有单词转换成位掩码、用 `Counter` 统计"每种掩码出现了多少个不同
    单词"（同一个掩码可能对应多个不同的单词，比如 "ab" 和 "ba" 及 "aab" 掩码相同）。
    对每个谜题，也转换成掩码，但需要把"必须包含第一个字母"这个约束和"单词字母是
    谜题字母子集"这个约束分开处理：设谜题掩码去掉第一个字母后剩下的位是 `rest_mask`
    （谜题固定 7 位，去掉首位还剩 6 位），第一个字母对应的位是 `first_bit`（必选，
    不参与枚举）。一个单词对这个谜题合法，当且仅当它的掩码等于 `first_bit | s`，
    其中 `s` 是 `rest_mask` 的**任意一个子集**（子集为空也可以，表示单词里除了
    首字母不再含谜题的其他字母）。

    枚举 `rest_mask` 的所有子集，用一个经典的位运算技巧——**子集枚举**：从
    `submask = rest_mask` 开始，每次通过 `submask = (submask - 1) & rest_mask`
    得到"比当前小的下一个子集"，直到 `submask` 变成 0（空集）为止，这样恰好不重
    不漏地枚举 `rest_mask` 的全部 `2^6=64` 个子集（因为谜题固定 7 位，去掉必选的
    首位，最多只需要枚举 64 个子集，和单词总数、谜题个数无关，是这个技巧效率的
    关键）。对每个子集查一次 `freq` 计数表、累加到这个谜题的答案里。

    【复杂度】时间 O(∑|word_i| + p · 2⁶)（p 为谜题个数，构造词频表是线性扫描所有
    单词，每个谜题只需要枚举固定 64 个子集）；空间 O(不同掩码的种类数)（最多
    2²⁶，但实际远小于这个上界）。

    【易错点】
    - 子集枚举的经典写法 `submask = (submask - 1) & rest_mask` 必须搭配一个
      "枚举完 0 之后要停止"的终止条件——如果直接用 `while submask:` 会漏掉
      `submask == 0`（空子集，代表"一个字母都不额外选"）这一种合法情况，必须用
      "先处理当前值、检查是否为 0 才 break"的写法（`do-while` 风格），不能用
      "先检查是否为 0 再处理"的写法。
    - 首字母对应的位 `first_bit` 是**必选**的，不能把它也纳入"要枚举子集"的
      `rest_mask` 里一起枚举——否则会漏算"必须包含首字母"这个约束，把一些实际
      不合法（缺少首字母）的组合也当作合法的枚举出来。
    - 单词的掩码用 `Counter` 统计"出现次数"而不是用 `set` 去重——因为不同的单词
      可能压缩出同一个掩码，必须把它们的出现次数都计入，否则会漏算重复掩码对应
      的多个单词。
    """
    def word_mask(w: str) -> int:
        mask = 0
        for ch in w:
            mask |= 1 << (ord(ch) - ord("a"))
        return mask

    freq = Counter(word_mask(w) for w in words)
    result = []
    for puzzle in puzzles:
        first_bit = 1 << (ord(puzzle[0]) - ord("a"))
        rest_mask = 0
        for ch in puzzle[1:]:
            rest_mask |= 1 << (ord(ch) - ord("a"))

        count = 0
        submask = rest_mask
        while True:
            count += freq.get(submask | first_bit, 0)
            if submask == 0:
                break
            submask = (submask - 1) & rest_mask
        result.append(count)
    return result


def minimum_one_bit_operations(n: int) -> int:
    """
    【题意】给一个整数 n，只能做两种操作：1) 把最低位（第 0 位）翻转；2) 把第 i 位
    翻转，前提是第 i-1 位是 1、且第 i-2 位到第 0 位全部是 0。求把 n 变成 0 所需的
    最少操作次数。

    【思路】这两条操作规则看起来很像是在描述"格雷码"（Gray code：相邻两个整数的
    格雷码编码恰好只有一位不同）的生成/转换规则。事实上可以证明：**把整数 n 反复
    执行题目允许的操作直到变成 0 所需的最少步数，恰好等于"n 的格雷码逆映射"**——
    也就是说，如果把 0, 1, 2, 3, ... 按顺序排成格雷码序列 `G(0)=0, G(1)=1,
    G(2)=3, G(3)=2, ...`（`G(k) = k ^ (k >> 1)`），那么"把 n 变成 0 的最少操作数"
    就是这个序列里 `G(k) = n` 的那个 k——因为题目给的两条操作规则，正是格雷码序列
    里"从 G(k) 变到 G(k-1)（或反过来）"必须翻转的那一位所遵循的规则（格雷码相邻项
    只差一位，且差在哪一位是由"当前最高的 1 在哪一位"决定的，这恰好对应操作 2 的
    触发条件）。

    于是问题转化成"已知格雷码值 n，求它在格雷码序列里的下标"，即格雷码的**逆变换**
    （格雷码正变换是 `g = k ^ (k >> 1)`，逆变换要从 g 反推出 k）。逆变换的公式是
    "从最高位开始，每一位是它自己和它左边所有已求出的位的异或前缀"，实现上等价于
    `ans = 0; while n: ans ^= n; n >>= 1`——每次把当前的 n "异或"进答案、再右移一位
    丢弃最低位，循环到 n 归零，`ans` 就是逆变换的结果。

    【复杂度】时间 O(log n)（n 每次右移一位，循环次数是 n 的二进制位数）；空间
    O(1)。

    【易错点】
    - 这个解法的正确性建立在"操作规则等价于格雷码转换规则"这一非显然的数学事实
      上，如果不知道这条性质，直接按操作规则模拟（每次决定翻转哪一位、反复递归
      直到变成 0）在 n 很大时会因为需要多层递归分类讨论而写得极其复杂，格雷码
      逆变换是这道题真正的"降维打击"解法。
    - 求逆变换的循环是 `ans ^= n` **在** `n >>= 1` **之前**（先异或当前值，再右移
      丢弃这一位），如果顺序颠倒，或者忘记每一轮都要异或（比如只在最后异或一次），
      结果都会算错。
    - 不要和"格雷码正变换"（`k ^ (k >> 1)`，这是从下标算格雷码值）搞混——本题需要
      的是反过来，从格雷码值算下标，两者的位运算公式形式上很接近但作用方向相反，
      容易记错用哪个方向。
    """
    ans = 0
    while n:
        ans ^= n
        n >>= 1
    return ans


def moves_to_chessboard(board: list[list[int]]) -> int:
    """
    【题意】给一个 n x n 的 0/1 矩阵 board，每次操作可以交换任意两行，或交换任意
    两列。目标是把它变成一个"棋盘"（任意两个 4 方向相邻的格子颜色都不同）。求达成
    目标所需的最少操作次数；如果无论如何都不可能，返回 -1。

    【思路】这是位运算和组合数学结合的经典难题，分两层判断：

    第一层——**可行性校验**：因为交换行、列不会改变"每一行/每一列具体是什么内容"，
    只会重新排列它们的相对位置，所以原始 board 本身就必须已经具备"能通过重排变成
    棋盘"的结构。一个合法棋盘只有两种可能的行内容（一种模式和它按位取反的模式），
    这意味着对任意的 `(i, j)`，`board[i][j]` 必然可以由 `board[i][0]`、
    `board[0][j]`、`board[0][0]` 三者通过异或关系唯一确定——`board[i][j] xor
    board[i][0] xor board[0][j] xor board[0][0]` 必须恒为 0（这是"矩阵可以写成
    行向量和列向量的某种秩 1 组合"这个结构性质的位运算表达）。此外，每一行、每
    一列里 1 的个数也必须接近一半（因为棋盘的每一行/每一列都是"01 交替"的），
    这转化成"第一行的 1 的个数 `row_ones`、第一列的 1 的个数 `col_ones` 都必须落
    在 `[n//2, (n+1)//2]` 这个区间内"这两个必要条件。

    第二层——**分别计算行、列各自需要的最少交换次数**：一旦校验通过，"把行变成
    正确顺序"和"把列变成正确顺序"是两个独立的子问题，可以分别求解再相加。对
    "行"这个子问题，只需要看"第一列这一竖列的 01 序列"（`board[i][0]` 对 i=0..n-1
    ——这个序列决定了每一行到底是"原始模式"还是"取反模式"，因为已经校验过整行内容
    只有这两种可能）；要把这个 01 序列通过任意位置的两两交换，变成严格交替的
    `010101...` 或 `101010...`，最少交换次数的推导是：统计"偶数位置上是 1，但目标
    要求是 0"的错位个数 `misplaced_ones`，以及"奇数位置上是 0，但目标要求是 1"的
    错位个数 `misplaced_zeros`——每次交换恰好能同时修正一个这样的"1 错位"和一个
    "0 错位"，所以最少交换次数就是 `misplaced_ones`（在总数匹配、即这个目标模式
    可行的前提下，`misplaced_ones` 必然等于 `misplaced_zeros`）。n 为偶数时两种
    目标模式（先 0 先 1）都可能可行，各自算一次错位数、取较小值；n 为奇数时，
    由于两种模式各自要求的 1 的总数不同（`floor(n/2)` 与 `ceil(n/2)`），只有和
    实际 1 的个数相符的那一种模式才可行，直接用那一种计算。"列"的子问题用同样的
    方法作用在"第一行这一横行的 01 序列"（`board[0][j]`）上。最终答案是行、列
    两部分最少交换次数之和。

    【复杂度】时间 O(n²)（校验步骤要遍历整个矩阵；行列的错位统计各是 O(n)）；
    空间 O(n)（第一行、第一列的临时序列）。

    【易错点】
    - 第一层"结构校验"（异或关系恒为 0）和"计数校验"（`row_ones`/`col_ones` 落
      在 `[n//2, (n+1)//2]`）是两个独立且都必须满足的必要条件，缺一不可——只做
      结构校验会漏掉"结构正确但 1 的比例失衡（比如全部行都以 1 开头）"这种其实
      也无法变成棋盘的情形；反过来只做计数校验也会放过"结构本身就不满足秩 1 关系"
      的非法输入。
    - 计算行/列各自的最少交换数时，n 为偶数要对两种目标模式（先 0 或先 1）都算一遍
      再取较小值——只算其中一种、想当然地认为某个模式一定更优，会得到偏大的错误
      答案。
    - 统计错位数时必须分别统计"错位的 1"和"错位的 0"两种，且这两个数值在目标模式
      可行的前提下应当相等——如果实现里只统计了其中一种、直接拿它当交换次数，
      恰好也能得到正确结果（因为两者理论上相等），但如果统计逻辑写错导致两者
      不相等却没有察觉，就说明可行性校验或错位统计本身出了 bug，需要用这一点
      交叉验证实现的正确性。
    """
    n = len(board)
    for i in range(n):
        for j in range(n):
            if board[i][j] ^ board[i][0] ^ board[0][j] ^ board[0][0]:
                return -1

    row_ones = sum(board[0])
    col_ones = sum(row[0] for row in board)
    if not (n // 2 <= row_ones <= (n + 1) // 2):
        return -1
    if not (n // 2 <= col_ones <= (n + 1) // 2):
        return -1

    def swaps_needed(vals: list[int], ones_count: int) -> int:
        m = len(vals)

        def mismatches(start_val: int) -> int:
            misplaced_ones = 0
            misplaced_zeros = 0
            for i, v in enumerate(vals):
                expected = start_val if i % 2 == 0 else 1 - start_val
                if v == 1 and expected == 0:
                    misplaced_ones += 1
                elif v == 0 and expected == 1:
                    misplaced_zeros += 1
            return max(misplaced_ones, misplaced_zeros)

        if m % 2 == 1:
            start_val = 1 if ones_count == (m + 1) // 2 else 0
            return mismatches(start_val)
        return min(mismatches(0), mismatches(1))

    col0 = [row[0] for row in board]
    row_swaps = swaps_needed(col0, col_ones)
    col_swaps = swaps_needed(board[0], row_ones)
    return row_swaps + col_swaps


def _self_test() -> None:
    assert range_bitwise_and(5, 7) == 4
    assert range_bitwise_and(0, 0) == 0
    assert range_bitwise_and(1, 2147483647) == 0

    assert valid_utf8([197, 130, 1]) is True
