"""位运算与数学 · 进阶补充（Part II）：把"异或消消乐"扩展到"出现3次/2个各1次"的变体，
并补上"用位运算模拟加减乘除"这个常被问到的子类型，共 7 道题。"""
from __future__ import annotations


def single_number_ii(nums: list[int]) -> int:
    """
    【题意】数组 nums 中除了一个数字只出现一次外，其余每个数字都恰好出现三次，
    找出那个只出现一次的数字。要求线性时间、O(1) 额外空间。

    【思路】Part I 的"只出现一次的数字"用简单异或（`a^a=0`）就能解决"其余数字出现
    两次"的情形，但这里其余数字出现的是三次——三次异或不会抵消为 0（`a^a^a = a`），
    简单异或不再适用，必须换一种统计方式。关键洞察是**按位统计**：对 32 个二进制位
    逐一考察，如果只统计"某一位上，所有数字里这一位是 1 的个数"，那么对于出现三次的
    数字，它们贡献的 1 的个数一定是 3 的倍数；只有落单的那个数字会让这一位上 1 的
    总个数"多出 1 个、不能被 3 整除"。所以对每一位分别数出现了多少次 1、对 3 取模，
    如果余数是 1，说明落单的数字在这一位上是 1，把这一位加进结果里；32 位都统计完，
    拼出来的结果就是答案。这是"异或消消乐"技巧的自然推广：从"配对抵消"升级为
    "对固定的重复次数取模抵消"。

    【复杂度】时间 O(32n)（32 个位，每位扫一遍数组），等价于 O(n)；空间 O(1)。

    【易错点】
    - 这个技巧的前提是"其余数字固定出现某个次数 c"，要对 c 取模而不是固定按 3 处理
      ——如果题目换成"其余数字出现 5 次"，取模的除数也要跟着换成 5，不能死记成
      "永远 mod 3"。
    - Python 整数没有固定位宽，如果数组里可能出现负数，按位统计完 32 位后需要把
      结果从"32 位无符号表示"转换回带符号整数（`result >= 2**31` 时减去 `2**32`），
      否则负数答案会被错误地表示成一个很大的正数。
    - 容易和"两个数各出现一次、其余出现两次"（`single_number_iii`）搞混——那道题
      两个特殊数不能通过简单异或直接分开，需要先整体异或、再分组；这道题只有一个
      特殊数，按位取模统计已经足够，不需要分组。
    """
    result = 0
    for i in range(32):
        count = sum((x >> i) & 1 for x in nums)
        if count % 3:
            result |= 1 << i
    if result >= 2 ** 31:
        result -= 2 ** 32
    return result


def single_number_iii(nums: list[int]) -> list[int]:
    """
    【题意】数组 nums 中恰好有两个数字各只出现一次，其余数字都恰好出现两次，找出
    这两个只出现一次的数字（返回顺序不限）。要求线性时间、O(1) 额外空间。

    【思路】如果直接对整个数组做异或，得到的是"两个落单数字的异或值"（其余成对的
    数字两两抵消），这个值本身不是任何一个答案，但它藏着关键信息：这两个落单的数字
    一定在某一位上不同（否则它们异或起来会是 0，矛盾于"它们是两个不同的数"），任取
    这样一个为 1 的位，就能把整个数组"划分成两组"——一组是"这一位是 1 的所有数字"，
    另一组是"这一位是 0 的所有数字"。这次划分有一个关键性质：两个落单数字必然被分到
    不同的组（因为它们在这一位上恰好相反）；而原本成对出现的数字，同一对的两个数值
    相同，在这一位上也相同，因此一对数字永远被分进同一组、不会被拆散。这样一来，
    每一组内部都变回了"只有一个数字出现一次、其余数字都出现两次"的结构——也就是
    Part I 的"只出现一次的数字"这个子问题，只需要分别对两组做一次简单异或即可各自
    独立求解。这是位运算题里"分组降维"的经典手法：**先用整体信息（全部异或）找到
    一个能区分两个特殊元素的判据（某一位不同），再用这个判据把"2 个特殊数混在一起"
    的复杂问题，降级为两个独立的、已经会解的"1 个特殊数"子问题**。

    取"任意一个为 1 的位"最简单的方式是取异或结果的最低位 1：`xor_all & (-xor_all)`
    （利用补码性质，`-x` 恰好保留 x 的最低位 1、把它左边的位取反），不需要真的写循环
    从低到高逐位试探。

    【复杂度】时间 O(n)（两趟线性扫描：一趟整体异或、一趟分组异或）；空间 O(1)。

    【易错点】
    - 划分两组之后，不需要真的把元素放进两个物理列表里再分别异或——可以在一趟扫描里
      同时维护两个累加变量 a、b，通过 `x & diff_bit` 是否为真决定这个数累加进 a 还是
      "隐式地"累进另一组（另一组的结果可以用 `xor_all ^ a` 直接算出来，不需要真的
      再扫一遍），但写成两趟扫描更直观、不容易出错，本实现采用两趟写法。
    - `diff_bit = xor_all & (-xor_all)` 这一步依赖"至少有一位不同"这个前提——如果
      xor_all 恰好是 0（比如误用在两个落单数字实际上相同的场景），会导致
      `diff_bit` 也是 0，后续分组会失效；但题目保证两个落单数字不同，这一步在本题
      前提下总是成立。
    - 分组的判据可以取 xor_all 里任意一个为 1 的位，不一定非要是最低位——只要保证
      两个特殊数在这一位上确实不同即可，取最低位只是实现上最方便的选择。
    """
    xor_all = 0
    for x in nums:
        xor_all ^= x
    diff_bit = xor_all & (-xor_all)

    a = 0
    for x in nums:
        if x & diff_bit:
            a ^= x
    b = xor_all ^ a
    return [a, b]


def reverse_bits(n: int) -> int:
    """
    【题意】输入一个 32 位无符号整数 n，将其二进制表示按位颠倒顺序（第 0 位和第 31
    位互换、第 1 位和第 30 位互换……），返回颠倒后的结果（同样按无符号 32 位整数
    解释）。

    【思路】直接逐位搬运：对 i 从 0 到 31，取出 n 的第 i 位（`(n >> i) & 1`），把它
    放到结果的第 `31 - i` 位上（`bit << (31 - i)`，再用 `|=` 累加进结果）——这是
    "颠倒"这个操作最朴素也最不容易出错的实现方式，不需要任何取巧的位技巧，只是把
    "第 i 位映射到第 31-i 位"这个规则原样翻译成代码。因为固定处理 32 位，循环次数
    固定为 32 次，不依赖 n 具体的值。

    【复杂度】时间 O(32)，即 O(1)；空间 O(1)。

    【易错点】
    - 目标位的位置是 `31 - i`（32 位，最高位下标是 31），如果位宽算错（比如按 31
      位或 64 位处理）会导致所有位都错位。
    - 结果的累加必须用 `|=`（按位或），不能用 `+=`——虽然在"每个目标位只会被写入
      一次、且初始为 0"的场景下两者结果相同，但用 `|=` 更清楚地表达了"设置某一位"
      这个语义，且更不容易在有重叠写入风险时出错。
    - Python 整数没有固定位宽，函数返回的整数会被当作"其数值等于 32 位无符号整数的
      解释结果"直接使用；因为每一位都被明确地限制在 0/1 并搬运到 `[0, 31]` 范围内的
      目标位，天然不会产生超出 32 位无符号范围的值，不需要额外掩码。
    """
    result = 0
    for i in range(32):
        bit = (n >> i) & 1
        result |= bit << (31 - i)
    return result


def get_sum(a: int, b: int) -> int:
    """
    【题意】不使用 `+`、`-` 运算符，计算两个 32 位有符号整数 a、b 的和。

    【思路】小学竖式加法的位运算版本：把"每一位相加"拆成"本位无进位的和"与"是否要
    向高位进位"两部分。异或 `a ^ b` 恰好就是"逐位相加、不考虑进位"的结果（1+0 或
    0+1 得 1、1+1 或 0+0 得 0，正好是异或表）；而 `(a & b) << 1` 恰好是"这一位的
    进位、搬到高一位去参与下一轮相加"（只有两位都是 1 才产生进位，进位要加到高一位，
    所以左移一位）。把这两部分本身再次相加（用同样的异或+进位规则），如此反复，直到
    某一轮"进位"变成 0，说明所有位都不再需要继续进位，此时的"无进位和"就是最终结果。
    因为 Python 整数是任意精度、不会自动截断成 32 位，必须手动用掩码 `0xFFFFFFFF`
    把每一步的中间结果都截断到 32 位无符号范围内，循环条件也要基于"截断后的进位是否
    非零"来判断（否则负数在 Python 里的无穷高位 1 会导致进位永远算不完、死循环）。
    最后如果这个 32 位无符号结果的最高位（符号位）是 1，说明真实值是负数，需要按照
    补码规则转换回 Python 原生的负数表示。

    【复杂度】时间 O(32)（最多 32 轮进位，每轮进位至少消除一位），即 O(1)；
    空间 O(1)。

    【易错点】
    - 每一步 `a ^ b` 和 `(a & b) << 1` 都要及时 `& mask`（`mask = 0xFFFFFFFF`）
      截断到 32 位，如果漏掉某一步的掩码，Python 的大整数会带着无穷多个高位 1
      （负数的补码在 Python 里是无限延伸的 1），导致进位计算错误或死循环。
    - 循环结束条件是"进位（截断后）为 0"，不能写成"a 或 b 为 0"之类的条件——因为
      a、b 在循环过程中会不断变化，真正该盯着看的是"还有没有需要继续进位的部分"。
    - 最后把 32 位无符号结果转换回带符号整数这一步很容易漏掉：如果结果的第 31 位
      是 1（数值上 `> 0x7FFFFFFF`），必须用 `~(result ^ mask)` 或等价方式换算成
      负数，否则会把本该是负数的答案错误地当成一个很大的正数返回。
    """
    mask = 0xFFFFFFFF
    while b & mask:
        carry = (a & b) << 1
        a = (a ^ b) & mask
        b = carry & mask
    a &= mask
    if a > 0x7FFFFFFF:
        return ~(a ^ mask)
    return a


def trailing_zeroes(n: int) -> int:
    """
    【题意】给一个非负整数 n，返回 `n!`（n 的阶乘）末尾有多少个连续的 0。

    【思路】末尾的每一个 0，都来自阶乘展开式里的一个因子 10，而 `10 = 2 × 5`——所以
    末尾 0 的个数，取决于 `n!` 的质因数分解里 2 和 5 各出现了多少次，取两者中较小的
    那个次数。在 1 到 n 的所有整数里，因子 2 出现的次数天然远多于因子 5 出现的次数
    （偶数比 5 的倍数密集得多），所以真正的瓶颈永远是"因子 5 出现了多少次"，只需要
    统计这一个数就够了，不需要真的去数因子 2。统计"1 到 n 里一共贡献了多少个因子
    5"不能只数"5 的倍数有几个"（那只统计了恰好贡献一个 5 的数，比如 10、15），还要
    考虑 25、125 这类本身含多个 5 因子的数会贡献不止一次——用 `n//5 + n//25 +
    n//125 + ...` 这个等比数列式的累加，`n//5` 数的是"至少贡献一个 5 的倍数个数"，
    `n//25` 数的是"至少贡献两个 5 的倍数个数（在第一项里已经数过一次，这里补上第
    二次）"，以此类推，直到 `power` 超过 n 为止。

    【复杂度】时间 O(log_5 n)（每次循环 power 乘 5，循环次数是 n 以 5 为底的对数）；
    空间 O(1)。

    【易错点】
    - 容易把问题想成"直接数 5 的倍数个数"（只做一次 `n // 5`）——这样会漏算 25、
      125 这类贡献不止一个因子 5 的数，必须把 `n//25`、`n//125` …… 都累加进去。
    - 循环条件是 `power <= n`（而不是 `power < n`），如果 n 本身恰好是 5 的整数
      次幂（比如 n=25），漏掉等号会少统计一层。
    - 不需要额外统计因子 2 的个数——虽然理论上答案是 `min(因子2个数, 因子5个数)`，
      但因子 5 恒是瓶颈，多余地去统计因子 2 只是浪费计算、不影响正确性，但也不必要。
    """
    count = 0
    power = 5
    while power <= n:
        count += n // power
        power *= 5
    return count


def divide(dividend: int, divisor: int) -> int:
    """
    【题意】不使用乘法、除法和取模运算符，实现两个整数相除，返回截断到 0 的商
    （比如 -2.7 截断为 -2）。结果需要截断到 32 位有符号整数范围
    `[-2^31, 2^31-1]`（溢出时截断到边界值）。

    【思路】"不能用乘除"意味着不能直接减法循环数"减了多少次"（那样对大数会超时），
    需要用位移实现"倍增式"的减法：核心技巧是"每次不只减一个除数，而是减去除数的
    2 的幂次倍，尽量一次减掉一大块"。具体做法：把被除数、除数都取绝对值（符号单独
    记录、最后处理），维护一个 `temp`（当前尝试减去的量，从除数本身开始）和
    `multiple`（`temp` 相当于除数的多少倍，从 1 开始）；不断把 `temp` 左移一位、
    `multiple` 同步左移一位（相当于两者同时乘以 2），只要 `temp` 翻倍后仍然不超过
    当前剩余的被除数，就继续翻倍；一旦翻倍会超过，就用当前这个 `temp` 去减被除数，
    把对应的 `multiple` 累加进商里，然后从"剩余的被除数"重新开始下一轮倍增（temp
    重新从除数本身开始）。这样，商的每一个二进制位实际上都是通过"位移试探"确定的，
    整个过程只用位移和减法就完成了原本需要除法的功能，效率是 O(log(商) × log(商))
    量级，远快于逐次减法的 O(商)。

    【复杂度】时间 O(log^2(dividend))（外层减法次数是 O(log(商))，每次内层倍增
    试探也是 O(log(商))）；空间 O(1)。

    【易错点】
    - 最容易漏掉的边界：`dividend = -2^31` 且 `divisor = -1` 时，数学上的商是
      `2^31`，超出 32 位有符号整数的最大值 `2^31 - 1`，必须提前特判这个组合直接
      返回 `2^31 - 1`，否则后续用绝对值处理时 `abs(-2^31)` 在别的语言里会先溢出
      （Python 整数无限精度不会溢出，但最终结果仍要按题目要求截断到范围内）。
    - 符号判断要在取绝对值之前就记录好（`(dividend < 0) != (divisor < 0)` 判断
      结果是否为负），处理完绝对值的除法逻辑之后再统一把符号乘回去，不要在中间过程
      掺杂符号导致比较大小、位移方向出错。
    - 内层"倍增试探"要注意每次重新从 `temp = 除数本身、multiple = 1` 开始（而不是
      接着上一轮结束时的 temp 继续翻倍），因为上一轮结束时的 temp 已经不适用于
      "剩余的、变小了的被除数"，必须重新试探。
    """
    INT_MAX = 2 ** 31 - 1
    INT_MIN = -2 ** 31
    if dividend == INT_MIN and divisor == -1:
        return INT_MAX

    negative = (dividend < 0) != (divisor < 0)
    a, b = abs(dividend), abs(divisor)
    result = 0
    while a >= b:
        temp, multiple = b, 1
        while a >= (temp << 1):
            temp <<= 1
            multiple <<= 1
        a -= temp
        result += multiple

    if negative:
        result = -result
    return max(INT_MIN, min(INT_MAX, result))


def is_power_of_two(n: int) -> bool:
    """
    【题意】给一个整数 n，判断它是否是 2 的幂（`2^0=1` 也算）。

    【思路】2 的幂在二进制表示下有且只有一个 1（比如 `8 = 0b1000`），这正好是
    Part I "位1的个数"那道题里用过的技巧 `n & (n-1)`——它会消去 n 最低位的那个 1；
    如果 n 本身只有一个 1，消去之后结果就是 0。所以判断"n 是不是 2 的幂"，等价于
    判断"消去最低位的 1 之后，n 是否变成了 0"，同时要求 n 本身是正数（0 和负数都
    不是 2 的幂，且负数在补码表示下 `n & (n-1)` 的结果不再具有"只有一个1"的判断
    意义，必须先排除）。

    【复杂度】时间 O(1)；空间 O(1)。

    【易错点】
    - 必须先判断 `n > 0`——`n=0` 时 `n & (n-1)` 算出的是 `0 & -1 = 0`，如果不加
      正数判断会被误判成 2 的幂；负数的补码表示也可能让 `n & (n-1) == 0` 这个
      判断失效或产生误判。
    - 不要和"位1的个数"那道题的循环版本搞混——这里不需要循环消位统计个数，只需要
      判断"消一次最低位的 1 之后是不是恰好归零"，一次位运算就能判断，不需要 while
      循环。
    """
    return n > 0 and (n & (n - 1)) == 0


def _self_test() -> None:
    assert single_number_ii([2, 2, 3, 2]) == 3
    assert single_number_ii([0, 1, 0, 1, 0, 1, 99]) == 99

    assert sorted(single_number_iii([1, 2, 1, 3, 2, 5])) == [3, 5]
    assert sorted(single_number_iii([-1, 0])) == [-1, 0]

    assert reverse_bits(0b00000010100101000001111010011100) == 964176192
    assert reverse_bits(0b11111111111111111111111111111101) == 3221225471

    assert get_sum(1, 2) == 3
    assert get_sum(2, 3) == 5

    assert trailing_zeroes(3) == 0
    assert trailing_zeroes(5) == 1
    assert trailing_zeroes(0) == 0

    assert divide(10, 3) == 3
    assert divide(7, -3) == -2

    assert is_power_of_two(1) is True
    assert is_power_of_two(16) is True
    assert is_power_of_two(3) is False
    assert is_power_of_two(4) is True
    assert is_power_of_two(5) is False

    print(
        "[PASS] p19_bit_math_ii: 7 题"
        "（只出现一次的数字II/只出现一次的数字III/颠倒二进制位/两整数之和/"
        "阶乘后的零/两数相除/2的幂）全部通过"
    )


