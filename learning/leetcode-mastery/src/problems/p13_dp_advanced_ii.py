# ── LC123 买卖股票的最佳时机 III(Hard) ───────────────────────────────────
def max_profit_iii(prices: list[int]) -> int:
    """
    【题意】最多允许完成两笔交易（一笔交易=一次买入+一次卖出，且必须先卖出手上的股票才能
    再次买入），求能获得的最大利润。
    【思路】状态机 DP：维护四个滚动状态 buy1（完成第一次买入后的最大利润，即 -price 的
    最大值）、sell1（完成第一次卖出后的最大利润）、buy2（在已完成一次完整交易的基础上，
    完成第二次买入后的最大利润）、sell2（完成第二次卖出后的最大利润，也是最终答案）。四个
    状态按顺序转移，且都用"和自己的旧值取 max"来表示"今天选择不操作"：
    buy1=max(buy1,-p)、sell1=max(sell1,buy1+p)、buy2=max(buy2,sell1-p)、
    sell2=max(sell2,buy2+p)。四个状态必须按 buy1→sell1→buy2→sell2 的顺序更新（同一天内
    先算出的新 buy1 会被同一天的 sell1 使用，这是合法的，因为"today 先买后卖"在允许无限次
    操作、只要求前后顺序合法的框架下本来就是被允许的等价变形）。初始化 buy1=buy2=-prices[0]
    （假设第一天就买入），sell1=sell2=0。
    【复杂度】时间 O(n)，空间 O(1)（只有四个滚动变量）。
    【易错点】1) 四个状态的更新顺序不能打乱——必须先更新 buy1 才能让同一天的 sell1 用上
    新值，再更新 buy2 才能让同一天的 sell2 用上新值，这个"链式依赖"的顺序错了会让状态提前
    或滞后一天；2) buy1、buy2 的初始值都要设成 -prices[0] 而不是 0 或 -inf——0 会让"还没
    买入"被误判成"买入成本是 0"（凭空产生利润），-inf 又会让第一天买入的选择永远无法被
    更新出来（-inf 加上任何有限的 p 仍是 -inf，如果后续没有更小的价格出现，buy1 会一直
    卡在 -inf）；3) 容易漏掉"最多两次交易"里"最多"两个字，把 sell2 强制等于"恰好两次",
    但因为 buy2 可以在 sell1 之后立刻发生（sell1-p 和 p 相等时利润不变），"只做一次交易"
    的方案会自然被包含在 sell2 的可能取值里，不需要额外分支处理。
    """
    if not prices:
        return 0
    buy1 = buy2 = -prices[0]
    sell1 = sell2 = 0
    for p in prices[1:]:
        buy1 = max(buy1, -p)
        sell1 = max(sell1, buy1 + p)
        buy2 = max(buy2, sell1 - p)
        sell2 = max(sell2, buy2 + p)
    return sell2


# ── LC188 买卖股票的最佳时机 IV(Hard) ────────────────────────────────────
def max_profit_iv(k: int, prices: list[int]) -> int:
    """
    【题意】把 LC123 的"最多两次交易"推广成"最多 k 次交易"，求最大利润。
    【思路】把 LC123 里手写的 buy1/sell1/buy2/sell2 四个变量推广成两个长度为 k+1 的数组
    buy[j]、sell[j]（j=1..k），buy[j] 表示"完成第 j 次买入后的最大利润"，sell[j] 表示
    "完成第 j 次卖出后的最大利润"。转移方程和 LC123 完全同构，只是从"手写四行"变成"循环
    k 次"：buy[j]=max(buy[j], sell[j-1]-p)，sell[j]=max(sell[j], buy[j]+p)，且 j 必须
    从 1 到 k **递增**遍历（sell[j-1] 依赖同一天已经算出的新值，这一点和 LC123 里
    buy1→sell1→buy2→sell2 的链式依赖顺序是同一回事，只是这里用循环下标 j 表达）。初始化
    buy[j]=-prices[0]（对所有 j，假设第一天就开始买入）、sell[0]=0（sell[0] 是"完成 0 次
    交易"的基准状态，恒为 0，代表"什么都不做"）。最终答案是 sell[k]。
    【复杂度】时间 O(n·k)，空间 O(k)。（当 k>=n//2 时，交易次数的限制形同虚设，可以退化成
    LC122 那样不限次数的贪心，从而把复杂度降到 O(n)；但题目要求写通用 dp 版本保证正确性，
    这里不做这个特判，退化成不限次数的情形会被这套 O(n·k) 的通用 dp 自动、正确地涵盖，
    不影响结果只是稍慢。）
    【易错点】1) 内层 j 循环必须从 1 递增到 k（不能倒序），因为 sell[j-1] 需要读到
    "这一天已经更新过"的新值，这是这题和 01 背包"倒序防止重复选取"的方向恰好相反的地方,
    容易搬错方向；2) sell[0] 必须固定为 0 且永不更新（代表"零次交易"的基准状态），如果
    误把 sell 数组整体初始化再参与循环更新，会把这个基准状态污染掉；3) k 为 0 或 prices
    为空时要提前返回 0，否则数组访问 prices[0] 或索引 sell[k] 可能出错或语义不对。
    """
    n = len(prices)
    if n == 0 or k == 0:
        return 0
    buy = [-prices[0]] * (k + 1)
    sell = [0] * (k + 1)
    for p in prices[1:]:
        for j in range(1, k + 1):
            buy[j] = max(buy[j], sell[j - 1] - p)
            sell[j] = max(sell[j], buy[j] + p)
    return sell[k]


# ── LC309 最佳买卖股票时机含冷冻期(Medium) ───────────────────────────────
def max_profit_cooldown(prices: list[int]) -> int:
    """
    【题意】可以不限次数买卖，但卖出股票后的第二天必须"冷冻"（不能买入），求最大利润。
    【思路】状态机 DP 需要三个状态区分"卖出当天"和"卖出之后的日子"，因为冷冻期只限制
    "紧跟着卖出的那一天"：hold（持有股票）、sold（今天刚卖出，处于冷冻期）、rest（不持有
    股票且不在冷冻期，可以自由选择买入）。转移时要注意**必须先读出三个状态"今天开始之前"
    的旧值**，再统一计算新值（否则 hold 的新值会污染 sold 的计算，制造出"当天买当天卖"
    之类不合法的穿越）：hold 新值 = max(旧 hold, 旧 rest - p)（延续持有，或者从"不在冷冻期"
    状态买入）；sold 新值 = 旧 hold + p（今天卖出，只能从"昨天已经持有"转移过来）；rest
    新值 = max(旧 rest, 旧 sold)（今天不持有也不在冷冻期，说明昨天要么已经是这个状态，要么
    昨天刚结束冷冻期）。最终答案是 max(sold, rest)（最后一天不可能以"持有股票"结束更优，
    因为还没卖出的股票不计入利润）。
    【复杂度】时间 O(n)，空间 O(1)。
    【易错点】1) 三个状态的转移必须基于"同一时刻的旧值"，如果先更新 hold 再拿新 hold 去算
    sold，等于让"今天买入"和"今天卖出"发生在同一天里连续完成，这在题目规则下是不允许的
    （必须是不同的两天）；2) 冷冻期只针对"卖出的下一天"，而不是"卖出当天"——sold 状态
    本身代表"今天卖出"，是合法状态，rest 才是"已经度过冷冻期、可以买入"的状态，这两者容易
    混淆导致转移写反；3) 最终答案漏掉和 sold 比较，只返回 rest，会在"最后一天恰好是卖出日"
    时算错（比如最后一天正好完成一次卖出，此时最优解停留在 sold 而不是 rest）。
    """
    if not prices:
        return 0
    hold = -prices[0]
    sold = 0
    rest = 0
    for p in prices[1:]:
        prev_hold, prev_sold, prev_rest = hold, sold, rest
        hold = max(prev_hold, prev_rest - p)
        sold = prev_hold + p
        rest = max(prev_rest, prev_sold)
    return max(sold, rest)


# ── LC714 买卖股票的最佳时机含手续费(Medium) ─────────────────────────────
def max_profit_fee(prices: list[int], fee: int) -> int:
    """
    【题意】可以不限次数买卖，但每完成一次交易（一次卖出）需要付固定手续费 fee，求最大利润。
    【思路】状态机 DP 只需要两个状态：hold（持有股票时的最大利润，扣掉买入成本）、cash
    （不持有股票时的最大利润）。转移：cash 新值 = max(旧 cash, 旧 hold+p-fee)（今天卖出，
    在卖出这一步统一扣除手续费）；hold 新值 = max(旧 hold, 旧 cash-p)（今天买入，或延续
    昨天的持有状态）。把手续费固定记在"卖出"这一步（而不是"买入"那一步）是最简单的实现
    方式——只要保证每完成一次买卖闭环恰好被扣一次手续费即可，记在买入或卖出效果等价，但
    记在卖出更符合直觉（"这笔交易的净利润要扣手续费"）。初始化 hold=-prices[0]（第一天
    买入），cash=0。
    【复杂度】时间 O(n)，空间 O(1)。
    【易错点】1) 手续费只应该被扣一次（每次交易扣一次），如果不小心在 hold 的转移里也扣了
    一次（买入和卖出各扣一次），会让手续费变成两倍，答案偏小；2) hold 的更新用的是"新的"
    cash 还是"旧的" cash 不影响这题的正确性（因为同一天内先卖后买或先买后卖在数值上等价：
    fee 只在卖出时扣一次，且当天价格相同），但为了和其他状态机题目风格一致、避免以后误用
    到会真正影响正确性的题目上，这里仍然按"先算 cash 再算 hold"的顺序、且不特意保存旧值；
    3) 忘记这题允许"当天卖出又当天买入"的隐式简化（因为价格相同，卖出立刻买回不会产生
    额外收益也不会有损失，等价于"不操作"），不需要专门处理这种边界。
    """
    if not prices:
        return 0
    hold = -prices[0]
    cash = 0
    for p in prices[1:]:
        cash = max(cash, hold + p - fee)
        hold = max(hold, cash - p)
    return cash


# ── LC518 零钱兑换 II(Medium) ────────────────────────────────────────────
def change(amount: int, coins: list[int]) -> int:
    """
    【题意】给定不同面值的硬币（每种硬币数量无限）和目标金额 amount，求凑出 amount 的不同
    **组合数**（不考虑顺序，[1,2] 和 [2,1] 算同一种组合）。
    【思路】同样是完全背包（硬币可重复使用），但和 LC322"求最少硬币数"的目标不同，这里求
    的是"方案数"，**遍历顺序的含义也完全不同**：这里必须**外层遍历硬币种类、内层遍历金额**
    （而 LC322 是外层金额、内层硬币，两种顺序对 LC322 那道题其实都对，因为"取 min"不关心
    重复计数）。原因：如果外层是金额、内层是硬币，对同一个金额 i，不同硬币的组合会被按
    "使用顺序"重复计数（比如凑 3 用 [1,2] 和 [2,1] 会被分别数一次，多算成"排列数"而不是
    "组合数"）；外层固定硬币种类之后，"这枚硬币用或不用、用几次"的决策在整个内层金额循环
    里只会被贯彻一次固定的相对顺序，从根本上避免了同一种组合被不同硬币使用顺序重复统计。
    dp[j] 定义为"用当前已经遍历过的硬币种类，凑出金额 j 的组合数"，dp[0]=1（凑出 0 只有
    "什么都不选"这一种方案，是递推链的地基），对每种硬币 c，dp[j]+=dp[j-c]（j 从 c 到
    amount 正序遍历，允许同一种硬币被多次累加使用，这是完全背包"正序"的标准写法）。
    【复杂度】时间 O(amount·len(coins))，空间 O(amount)。
    【易错点】1) 外层内层的遍历顺序错误是这题最典型也最隐蔽的 bug——如果写成外层金额、
    内层硬币，代码依然能跑、依然会给出一个数字，但这个数字会偏大（把排列数当成了组合数）,
    不会报错也不会崩溃，只能通过和标准答案比对才能发现；2) dp[0]=1 这个地基如果漏掉,
    后续所有依赖 dp[0] 的组合都会被漏计（比如恰好用一种硬币整除 amount 的组合）；3) 目标
    金额是 0 时（amount=0），标准语义是"恰好有一种凑法：什么都不选"，dp[0]=1 天然覆盖了
    这个边界，不需要特判。
    """
    dp = [0] * (amount + 1)
    dp[0] = 1
    for c in coins:
        for j in range(c, amount + 1):
            dp[j] += dp[j - c]
    return dp[amount]


# ── LC115 不同的子序列(Hard) ─────────────────────────────────────────────
def num_distinct(s: str, t: str) -> int:
    """
    【题意】给定字符串 s 和 t，求 s 中有多少种不同的子序列（通过删除若干字符得到，不要求
    连续）恰好等于 t。
    【思路】双序列 DP：dp[i][j] 定义为"s 的前 i 个字符里，有多少种子序列恰好等于 t 的前 j
    个字符"。对 s 的第 i 个字符 s[i-1] 只有两种处理方式：① 不用它去匹配 t 的任何字符（直接
    丢弃），这种情况下方案数等于 dp[i-1][j]（s 少一个字符，t 的匹配目标不变）；② 如果
    s[i-1]==t[j-1]，还可以选择用它去匹配 t 的最后一个字符，方案数额外加上 dp[i-1][j-1]
    （s 和 t 各退一位）。这两种选择互不冲突，可以同时贡献（这正是"不同子序列计数"和"判断
    是否存在子序列"最大的区别：后者只需要布尔或/而非加法）。若 s[i-1]!=t[j-1]，则只有方式
    ①。初始条件：dp[i][0]=1（对任意 i，"匹配空字符串 t"永远只有一种方案：什么都不删/全部
    删光，是递推链的地基），dp[0][j>0]=0（空字符串 s 不可能匹配出非空的 t）。
    【复杂度】时间 O(m·n)，空间 O(m·n)（m=len(s), n=len(t)；可以用一维滚动数组从右往左
    更新压缩到 O(n)，这里为清晰起见保留二维）。
    【易错点】1) 最容易漏掉的是"不使用 s[i-1]"这个分支（dp[i-1][j]）——很多人只在
    s[i-1]==t[j-1] 时才写转移，字符不相等时直接把 dp[i][j] 置 0，但正确做法是任何时候都要
    先继承 dp[i-1][j]，字符相等只是"多一种额外方案"而不是"唯一方案"；2) 两种方案是相加
    关系而不是取 max，这是计数类双序列 DP 和"最长公共子序列"类最优化双序列 DP 的本质区别,
    搞混会把方案数错误地退化成某种"长度"；3) dp[i][0]=1 必须对所有 i 成立（包括 i=0），
    如果只设 dp[0][0]=1 而漏了 dp[i>0][0]，后续所有依赖"匹配到空 t"的转移都会算错。
    """
    m, n = len(s), len(t)
    dp = [[0] * (n + 1) for _ in range(m + 1)]
    for i in range(m + 1):
        dp[i][0] = 1
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            dp[i][j] = dp[i - 1][j]
            if s[i - 1] == t[j - 1]:
                dp[i][j] += dp[i - 1][j - 1]
    return dp[m][n]


# ── LC97 交错字符串(Medium) ──────────────────────────────────────────────
def is_interleave(s1: str, s2: str, s3: str) -> bool:
    """
    【题意】判断 s3 是否可以由 s1 和 s2"交错"组成——即保持 s1、s2 各自内部字符的相对顺序，
    像洗牌一样交替（不要求规律地交替，只要求相对顺序不变）拼接出 s3。
    【思路】双序列 DP：dp[i][j] 定义为"s1 的前 i 个字符和 s2 的前 j 个字符，能否交错组成
    s3 的前 i+j 个字符"（s3 的前缀长度必须恰好是 i+j，这是双序列 DP 里"第三个字符串长度
    由前两者下标之和决定"的特殊之处）。dp[i][j] 为真当且仅当以下两种情况至少一种成立：
    ① s3 的第 i+j 个字符（s3[i+j-1]）来自 s1 的最后一个字符，即 dp[i-1][j] 为真且
    s1[i-1]==s3[i+j-1]；② s3 的这个字符来自 s2，即 dp[i][j-1] 为真且 s2[j-1]==s3[i+j-1]。
    初始条件 dp[0][0]=True（两个空串交错组成空串）。提前特判 len(s1)+len(s2)!=len(s3) 直接
    返回 False，避免后续下标越界（长度对不上肯定无法交错）。
    【复杂度】时间 O(m·n)，空间 O(m·n)。
    【易错点】1) 长度校验必须放在最前面，否则 s3[i+j-1] 这个下标在长度不匹配时会越界；
    2) dp[i][j] 的两个分支是"或"的关系（只要有一种拼接方式成立即可），而不是"与"——只要
    来自 s1 或来自 s2 中有一种能让前缀合法，dp[i][j] 就是 True，写成 and 会把本该成立的
    情况错误地判定为 False；3) 边界行/列（i=0 或 j=0）容易被忽略专门处理，但这里的循环
    写法（i、j 都从 0 开始遍历，用 i>0/j>0 卫语句保护）已经自然覆盖了边界，不需要额外的
    初始化循环，重复初始化反而容易和主循环的逻辑冲突。
    """
    m, n = len(s1), len(s2)
    if m + n != len(s3):
        return False
    dp = [[False] * (n + 1) for _ in range(m + 1)]
    dp[0][0] = True
    for i in range(m + 1):
        for j in range(n + 1):
            if i == 0 and j == 0:
                continue
            ok = False
            if i > 0 and dp[i - 1][j] and s1[i - 1] == s3[i + j - 1]:
                ok = True
            if j > 0 and dp[i][j - 1] and s2[j - 1] == s3[i + j - 1]:
                ok = True
            dp[i][j] = ok
    return dp[m][n]


# ── LC1035 不相交的线(Medium) ────────────────────────────────────────────
def max_uncrossed_lines(nums1: list[int], nums2: list[int]) -> int:
    """
    【题意】把 nums1 和 nums2 分别画成两排数字，若 nums1[i]==nums2[j] 可以画一条连接线，
    要求所有连线两两不相交（这等价于连线所用的下标对 (i,j) 在两个数组里都必须是同步递增
    的），求最多能画多少条线。
    【思路】"连线不相交"这个几何约束翻译成代数语言，恰好就是"下标严格递增地一一对应"——
    这正是最长公共子序列（LCS，LC1143）的定义。本题本质上就是 LCS 换了一层"画线"的包装
    （母题参见 12/13 讲前面的 `p13_dp_advanced.py::longest_common_subsequence`）：
    dp[i][j] 定义为 nums1 前 i 个数和 nums2 前 j 个数的最长公共子序列长度，相等则
    dp[i][j]=dp[i-1][j-1]+1，不相等则取 max(dp[i-1][j], dp[i][j-1])。
    【复杂度】时间 O(m·n)，空间 O(m·n)。
    【易错点】1) 容易被"画线""不相交"这些几何表述误导去设计新的状态定义，其实应该先识别
    出它和 LCS 是同一个母题，直接套用 LCS 的转移方程即可，不需要另起炉灶；2) 这里比较的是
    整数是否相等（而不是 LCS 常见例子里的字符），下标处理和字符串版完全一致，容易因为
    "数组"和"字符串"下意识地写出不同风格的代码而引入不必要的差异（比如误用字符串切片
    的写法处理列表）；3) 元素可能重复（比如两个数组都出现多个相同的值），LCS 的转移方程
    对重复值天然正确处理，不需要额外去重或特判。
    """
    m, n = len(nums1), len(nums2)
    dp = [[0] * (n + 1) for _ in range(m + 1)]
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if nums1[i - 1] == nums2[j - 1]:
                dp[i][j] = dp[i - 1][j - 1] + 1
            else:
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1])
    return dp[m][n]


# ── LC516 最长回文子序列(Medium) ─────────────────────────────────────────
def longest_palindrome_subseq(s: str) -> int:
    """
    【题意】给定字符串 s，求它的最长回文子序列（不要求连续，但要求正读反读相同）的长度。
    【思路】区间 DP：dp[i][j] 定义为"s[i:j+1]（闭区间 [i,j]）这个子串里最长回文子序列的
    长度"。如果 s[i]==s[j]（区间两端字符相同），这两个字符可以分别放在回文的最外两侧，
    dp[i][j]=dp[i+1][j-1]+2（去掉两端，中间部分的最长回文子序列长度加上这两端）；如果
    s[i]!=s[j]，两端字符不可能同时出现在同一个回文子序列的最外层，只能舍弃其中一端，取
    max(dp[i+1][j], dp[i][j-1])。base case：单个字符本身就是长度为 1 的回文，dp[i][i]=1；
    当 i+1>j-1（即 j=i+1，区间长度恰好为 2）时，dp[i+1][j-1] 这个"内部区间"是空区间，
    约定值为 0，这样"两个相邻且相同的字符"能正确算出 dp[i][j]=0+2=2。
    【复杂度】时间 O(n^2)，空间 O(n^2)。
    【易错点】1) 遍历顺序必须保证算 dp[i][j] 时 dp[i+1][j-1]、dp[i+1][j]、dp[i][j-1] 都已
    经算好——这要求 i 从大到小（从右往左）遍历、j 从小到大（从左往右）遍历，i 必须是外层
    倒序、j 是内层正序，顺序反了会读到尚未计算的默认值 0，把还没算出来的子问题误当成"空
    区间"；2) "空区间"（i+1>j-1）应该自然地默认为 0，不需要显式特判，只要保证遍历顺序
    正确、二维数组初始化为全 0，这个边界会被正确处理，不必额外写 if 分支去处理它；
    3) 长度为 0 或 1 的字符串要能正常处理：n=0 时直接返回 0，n=1 时 dp[0][0]=1 就是答案，
    循环体 range(i+1, n) 在这些边界下天然不会执行多余的代码。
    """
    n = len(s)
    if n == 0:
        return 0
    dp = [[0] * n for _ in range(n)]
    for i in range(n - 1, -1, -1):
        dp[i][i] = 1
        for j in range(i + 1, n):
            if s[i] == s[j]:
                dp[i][j] = dp[i + 1][j - 1] + 2
            else:
                dp[i][j] = max(dp[i + 1][j], dp[i][j - 1])
    return dp[0][n - 1]


# ── LC1039 多边形三角剖分的最低得分(Medium) ──────────────────────────────
def min_score_triangulation(values: list[int]) -> int:
    """
    【题意】给定一个凸多边形每个顶点上的权值 values，把这个多边形完全剖分成若干个三角形,
    每个三角形的得分是它三个顶点权值的乘积，求所有三角形得分之和的最小值。
    【思路】区间 DP：把顶点看成一个环上的序列，dp[i][j] 定义为"顶点 i 到顶点 j（沿多边形
    边界的这一段，i<j）构成的这个子多边形，最优剖分的最低得分"。枚举和"边 (i,j)"组成一个
    三角形的第三个顶点 k（i<k<j）——这条边 (i,j) 在最终剖分方案里一定恰好属于某一个三角形,
    这个三角形的第三个顶点把区间 [i,j] 分成了 [i,k] 和 [k,j] 两个独立的子多边形（两段
    互不影响，可以分别递归求解），dp[i][j]=min_k( dp[i][k]+dp[k][j]+values[i]*values[k]*
    values[j] )。base case：区间长度小于 3（即 j-i<2，最多只有两个顶点，不构成三角形）时
    dp[i][j]=0。这个递推结构和"戳气球"（LC312）的"枚举最后一步"思路同源，只是这里枚举的
    是"和固定边 (i,j) 组成三角形的第三个顶点"，戳气球枚举的是"最后戳破的气球"。
    【复杂度】时间 O(n^3)（区间长度 × 左端点 × 枚举 k），空间 O(n^2)。
    【易错点】1) 遍历顺序必须按区间长度从小到大（dp[i][j] 依赖的 dp[i][k]、dp[k][j] 都是
    更短的区间），如果直接按 i、j 的自然顺序遍历会读到尚未算出的子问题；2) k 的枚举范围是
    开区间 (i,j)（严格大于 i、严格小于 j），如果不小心让 k 取到 i 或 j 本身，会把"三角形"
    退化成"一条边"，乘积计算出的得分毫无意义；3) 容易和"戳气球"的虚拟边界技巧混淆——戳
    气球需要首尾各补一个值为 1 的虚拟节点，而这题不需要任何虚拟顶点，直接在原始顶点序列上
    做区间 DP 即可，误加虚拟顶点反而会引入错误的额外三角形。
    """
    n = len(values)
    dp = [[0] * n for _ in range(n)]
    for length in range(2, n):
        for i in range(0, n - length):
            j = i + length
            dp[i][j] = min(
                dp[i][k] + dp[k][j] + values[i] * values[k] * values[j]
                for k in range(i + 1, j)
            )
    return dp[0][n - 1]


# ── LC646 最长数对链(Medium) ─────────────────────────────────────────────
def find_longest_chain(pairs: list[list[int]]) -> int:
    """
    【题意】给定若干数对 (a, b)（a<b），如果 pair1[1] < pair2[0] 就说 pair2 可以接在 pair1
    后面组成一条链，求最长链的长度（这是一种特殊的"最长递增子序列"，可以按第 15 讲区间
    调度贪心的思路求解，不必套用通用 dp）。
    【思路】把每个数对按"结尾值"从小到大排序（贪心的关键：不看开头，只看结尾），然后从左
    到右扫描，只要当前数对的开头严格大于"已选链条的最后一个结尾"，就把它接上去、更新链尾。
    这个贪心成立的原因和活动选择问题（interval scheduling）完全一样：在能接的数对里，永远
    优先选"结尾最小"的那个，能给后面留出最大的接续空间，不会让最终结果变差。
    【复杂度】时间 O(n log n)（排序为主，扫描是 O(n)），空间 O(log n)~O(n)（排序开销）。
    【易错点】1) 排序必须按每个数对的**结尾**（pair[1]）排序，而不是开头——按开头排序会
    破坏"贪心选结尾最小者"这个核心性质，导致算出偏小的错误链长；2) 判断能否接上用的是
    严格大于（a > cur_end），题目里链的定义是 b1<a2 严格小于，等号不满足接续条件，容易
    在这里漏掉"严格"两个字导致把不能相连的数对错误地接在一起；3) 这题也可以写成 O(n^2) 的
    dp（dp[i]=以第 i 个数对结尾的最长链长度，类似 LIS 的朴素解法），两种做法殊途同归，但
    贪心版本更能体现"结尾越小、越有利于后续扩展"这一条和 LIS 优化到 O(n log n) 时"维护
    最小结尾"完全相通的核心直觉。
    """
    pairs_sorted = sorted(pairs, key=lambda pair: pair[1])
    count = 0
    cur_end = float("-inf")
    for a, b in pairs_sorted:
        if a > cur_end:
            count += 1
            cur_end = b
    return count


# ── LC10 正则表达式匹配(Hard) ────────────────────────────────────────────
def is_match_regex(s: str, p: str) -> bool:
    """
    【题意】实现支持 '.'（匹配任意单个字符）和 '*'（匹配它前面那一个字符出现零次或多次，
    不是"匹配任意字符串"）的正则表达式匹配，判断 p 能否完全匹配 s。
    【思路】双序列 DP：dp[i][j] 定义为"s 的前 i 个字符能否被 p 的前 j 个字符匹配"。分两种
    情况讨论 p[j-1]：① p[j-1] 不是 '*'（是普通字符或 '.'），要求 s[i-1] 存在且能和 p[j-1]
    匹配（相等或 p[j-1]=='.'），dp[i][j]=dp[i-1][j-1]；② p[j-1] 是 '*'，因为 '*' 的语义是
    "让它前面那个字符（p[j-2]）出现零次或多次"，有两种子选择：让这个 "字符*" 组合匹配零次
    （相当于在 p 里彻底跳过这两个字符），dp[i][j]=dp[i][j-2]；或者，如果 s[i-1] 能和
    p[j-2] 匹配，让这次 '*' 再多吃掉 s 的一个字符（重复一次），dp[i][j] 还可以是
    dp[i-1][j]（s 退一位，p 不动，因为 '*' 可以继续匹配更多次）。这两个子选择是"或"的关系。
    初始条件 dp[0][0]=True；dp[0][j] 这一整行（s 是空串）要单独处理："a*"、"a*b*" 这类整体
    可以匹配空串的模式，dp[0][j]=dp[0][j-2]（当 p[j-1]=='*' 时）。
    【复杂度】时间 O(m·n)，空间 O(m·n)。
    【易错点】1) 这题的 '*' 表示"前一个字符重复 0 次或多次"，和 LC44 通配符匹配里"匹配
    任意字符序列"的 '*' 语义完全不同——把两题的转移方程搞混是最常见也最致命的错误（详见
    本讲"深挖"部分的对照）；2) dp[0][j] 这一行必须单独初始化处理"匹配零次"的情况（比如
    p="a*b*" 应该能匹配空串 s=""），漏掉这一行会让很多"看似该成立"的空串匹配被误判为
    False；3) "匹配零次"（dp[i][j-2]）和"重复匹配一次"（dp[i-1][j]，且要求当前字符能配上
    p[j-2]）必须都考虑到、且是"或"的关系，只写其中一个分支会让某些合法匹配被漏判。
    """
    m, n = len(s), len(p)
    dp = [[False] * (n + 1) for _ in range(m + 1)]
    dp[0][0] = True
    for j in range(1, n + 1):
        if p[j - 1] == "*":
            dp[0][j] = dp[0][j - 2]
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if p[j - 1] == "*":
                dp[i][j] = dp[i][j - 2]
                if p[j - 2] == "." or p[j - 2] == s[i - 1]:
                    dp[i][j] = dp[i][j] or dp[i - 1][j]
            elif p[j - 1] == "." or p[j - 1] == s[i - 1]:
                dp[i][j] = dp[i - 1][j - 1]
            else:
                dp[i][j] = False
    return dp[m][n]


# ── LC44 通配符匹配(Hard) ────────────────────────────────────────────────
def is_match_wildcard(s: str, p: str) -> bool:
    """
    【题意】实现支持 '?'（匹配任意单个字符）和 '*'（匹配任意字符序列，**包括空串**）的
    通配符匹配，判断 p 能否完全匹配 s。**注意这里的 '*' 和 LC10 正则表达式里的 '*' 含义
    完全不同**：这里 '*' 独立代表"匹配任意长度的任意字符串"，不依赖它前面的字符。
    【思路】双序列 DP：dp[i][j] 定义为"s 的前 i 个字符能否被 p 的前 j 个字符匹配"。分两种
    情况讨论 p[j-1]：① p[j-1] 是 '?' 或者普通字符且等于 s[i-1]，dp[i][j]=dp[i-1][j-1]
    （消耗掉两边各一个字符）；② p[j-1] 是 '*'，因为它能匹配"任意长度"的字符串，有两种
    子选择：让这个 '*' 匹配空串（跳过它，看 p 剩下的部分能不能匹配 s 目前的全部），
    dp[i][j]=dp[i][j-1]；或者让这个 '*' 多吃掉 s 的一个字符（不管吃的是什么，'*' 都能
    继续匹配），dp[i][j]=dp[i-1][j]。这两个子选择同样是"或"的关系。初始条件 dp[0][0]=True；
    dp[0][j] 这一行（s 是空串）：只有当 p 的前 j 个字符全部是 '*' 时才能匹配空串,
    dp[0][j]=dp[0][j-1] 当 p[j-1]=='*'（连续的 '*' 可以链式地都匹配空串），否则为 False。
    【复杂度】时间 O(m·n)，空间 O(m·n)。
    【易错点】1) 和 LC10 最大的区别：这里的 '*' 不依赖前一个字符，dp[i][j-1]（跳过这个
    '*'，代表匹配 0 个字符）和 dp[i-1][j]（用这个 '*' 多吃一个字符，j 不退位因为 '*' 可以
    继续吃）就是全部的转移来源，不需要像 LC10 那样先看"前一个字符"能不能配上；2) dp[0][j]
    这一行不能像 LC10 那样写成 dp[0][j-2]（那是"跳过两个字符"的语义，这里 '*' 是单个字符
    且可以匹配空串，应该是 dp[0][j-1] 且要求 p[j-1] 本身就是 '*'）；3) 连续多个 '*'（比如
    "a**b"）在这套转移下会被自动正确处理（每个 '*' 各自可以选择匹配 0 个字符），不需要
    额外的"合并连续 * "预处理，虽然那样做可以优化，但不做也不影响正确性。
    """
    m, n = len(s), len(p)
    dp = [[False] * (n + 1) for _ in range(m + 1)]
    dp[0][0] = True
    for j in range(1, n + 1):
        if p[j - 1] == "*":
            dp[0][j] = dp[0][j - 1]
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if p[j - 1] == "*":
                dp[i][j] = dp[i - 1][j] or dp[i][j - 1]
            elif p[j - 1] == "?" or p[j - 1] == s[i - 1]:
                dp[i][j] = dp[i - 1][j - 1]
            else:
                dp[i][j] = False
    return dp[m][n]


