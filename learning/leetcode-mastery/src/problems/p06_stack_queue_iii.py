"""LeetCode 分类 06·栈与队列 Phase 3 竞赛级补充（Part III）：相邻字符消消乐（整理
字符串）+ 栈存"待累加分数"模拟嵌套乘 2（括号的分数）+ 贪心归约成"]...[）"残留模式
（使字符串平衡的最小交换次数）+ 计数器代替显式栈（使括号有效的最少添加）+ 栈存分组
列表做嵌套翻转（反转每对括号间的子串）+ 单变量维护深度峰值（括号的最大嵌套深度）+
用栈维护"最后一个未匹配右括号下标"作为长度基准（最长有效括号，本模块深挖对象），共
7 道范例，全部围绕"栈是处理嵌套/最近匹配结构的天然工具"这一母题的更难变体。"""
from __future__ import annotations


def make_good(s: str) -> str:
    """
    【题意】给定一个大小写字母混合的字符串 s，如果相邻两个字符是"同一个字母的大小写
    形式"（比如 'e' 和 'E'），这一对字符就是"坏对"，可以删除掉；重复这个删除操作直到
    字符串里不再存在任何坏对为止（空字符串也算好字符串），返回最终结果（题目保证
    在给定约束下结果唯一）。
    【思路】"删除一对相邻字符后，原本被它们隔开的左右两侧字符可能变成新的相邻对，
    需要继续检查"——这种"处理完当前这一对之后，前面刚确认过的字符可能需要重新参与
    判断"的模式，正是栈的典型应用场景（此消彼长后"回退"到更早的状态）。用一个栈从
    左到右扫描 s：每读到一个新字符，只看栈顶——如果栈非空且栈顶字符和当前字符不同、
    但转成同一种大小写后相同（一个是某字母的大写、另一个是同一字母的小写），说明它俩
    构成坏对，直接弹出栈顶（相当于把这一对同时消掉，新字符不入栈）；否则把当前字符
    正常入栈。扫描结束后，栈里剩下的字符从栈底到栈顶拼接起来就是最终结果——因为每次
    消除坏对之后，栈顶自动"回退"到消除前更早的字符，天然能连锁触发后续的消除判断，
    不需要额外写"消除之后要不要重新检查"的逻辑。
    【复杂度】时间 O(n)，空间 O(n)（栈最坏情况下存满 n 个字符，比如不存在任何坏对）。
    【易错点】
    1) 判断"坏对"不能只看 `stack[-1].lower() == ch.lower()`，还必须同时确认两者
    大小写不同（`stack[-1] != ch`）——如果当前字符和栈顶字符完全相同（比如连续两个
    小写 'e'），这两个字符不构成坏对，不应该被消除，题目定义的"坏对"专指"同一字母的
    大小写不同形式"；
    2) 消除坏对时只弹出栈顶、当前字符不入栈，不能写成"弹出栈顶后再把当前字符压入"——
    坏对是两个字符**一起**消失，不是"用当前字符替换掉栈顶"；
    3) 扫描完之后不需要额外再做一轮清理——每次入栈前都已经和栈顶比较过，栈里剩下的
    字符两两之间必然不再构成坏对（否则早就在扫描过程中被消掉了），可以直接拼接返回。
    """
    stack: list[str] = []
    for ch in s:
        if stack and stack[-1] != ch and stack[-1].lower() == ch.lower():
            stack.pop()
        else:
            stack.append(ch)
    return "".join(stack)


def score_of_parentheses(s: str) -> int:
    """
    【题意】给定一个平衡的括号字符串 s（只含 '(' 和 ')'），按以下规则计算它的分数：
    "()" 本身记 1 分；两个平衡括号组 A、B 并排放（"AB"）记 A 的分数加 B 的分数；
    一个平衡括号组 A 被一对括号整体包住（"(A)"）记 A 的分数的 2 倍。返回整个 s 的
    最终得分。
    【思路】"包住"对应分数翻倍、"并排"对应分数相加，这种"嵌套要乘、并列要加"的结构，
    和"挂起当前状态、进入更深一层、退出时按外层规则揉回去"的通用范式（本课开头讲的
    "括号/表达式"母题）是同一件事：用栈维护"每一层括号内部、目前已经累积出的分数"，
    栈顶永远是"最内层还没封闭的那个括号，其内部到目前为止的分数"。遇到 '(' 就在栈顶
    压入一个新的 0（开启一层新的、还没有任何内容的括号）；遇到 ')' 说明这一层内部的
    内容已经扫描完毕，弹出栈顶值 `v`（这一层内部的分数），如果 `v` 是 0（说明这一层
    直接就是最内层的 "()"，中间什么都没包），按规则记 1 分；否则说明这一层内部还
    包含着已经算出分数的子结构，按规则翻倍记 `2*v` 分——用 `max(2*v, 1)` 一次性
    覆盖这两种情况，再把这个值加到"退回去之后的、新栈顶"（也就是外层）上，表示"这一层
    的贡献已经计入外层的累计分数里"。
    【复杂度】时间 O(n)，空间 O(n)（栈深度最坏等于括号的最大嵌套深度）。
    【易错点】
    1) 弹出的这一层分数如果是 0（内部是空的，对应最简单的 "()"），要记 1 分而不是
    0 分——`max(2*v, 1)` 这个式子必须同时兼顾"内部为空记 1"和"内部非空翻倍"两种
    情况，容易漏掉"内部为空"这个特殊分支单独判断；
    2) 计算出这一层的贡献之后，必须加到弹出之后的新栈顶（外层）上，而不是直接
    覆盖或者丢弃——外层可能后面还会遇到并列的其它括号组，它们的分数需要在同一个
    位置上累加起来；
    3) 初始栈要预置一个 `[0]` 作为"整个字符串最外层"的累加基准，最终答案就是这个
    最外层累加完之后的值（`stack[0]`），不能误以为最外层不需要一个初始占位。
    """
    stack: list[int] = [0]
    for ch in s:
        if ch == "(":
            stack.append(0)
        else:
            v = stack.pop()
            stack[-1] += max(2 * v, 1)
    return stack[0]


def min_swaps_balanced(s: str) -> int:
    """
    【题意】给定一个长度为偶数、由 n/2 个 '[' 和 n/2 个 ']' 组成的字符串 s，每次操作
    可以交换字符串中任意两个位置上的括号（不要求相邻），求最少交换多少次，能让 s
    变成合法的平衡括号串。
    【思路】先用栈做一次"贪心抵消"：正常情况下 '[' 入栈；遇到 ']' 时，如果栈顶是
    '['，说明找到了配对，直接抵消（弹栈）；如果栈顶不是可配对的 '['（要么栈是空的，
    要么栈顶恰好也是一个还未被消掉的 ']'），说明这个 ']' 目前找不到配对，只能先把
    它也压进栈里，寄希望于后面出现的 '[' 通过交换"跨越"过来匹配它。可以证明，这样
    扫描一遍之后，栈里剩下的内容必然是"先一段连续的 ']'，再一段连续的 '['"这种形式
    （因为一旦栈顶变成 '[' 就会被后续的 ']' 立刻消掉，只有"']' 在前、'[' 在后"这种
    没法就地消掉的组合才会留下来）。设剩下的 '[' 有 `x` 个（等于剩下的 ']' 个数，
    因为整个字符串左右括号总数相等，被抵消掉的部分左右各占一半，剩下的部分自然也是
    左右各占一半），每一次交换可以把"一个错位的 ']' 和一个错位的 '['"同时纠正（比如
    把 "][" 变成 "[]"），也就是每次交换能同时修好 2 个错位括号，共需要 `⌊(x+1)/2⌋`
    次交换（当 x 是奇数时，最后一次交换会顺带把中间那一小段一次性理顺，不需要额外
    再交换一次）。
    【复杂度】时间 O(n)，空间 O(n)（栈最坏情况存满整个字符串；也可以只维护一个计数器
    representing 栈的"净未匹配左括号数"做到 O(1) 额外空间，这里用显式栈是为了让
    "抵消/保留"的过程更直观）。
    【易错点】
    1) 不能想当然地认为"剩下多少个未匹配括号就需要多少次交换"——每次交换能同时
    修正两个位置（一个 ']'、一个 '['），所以要除以 2 再向上取整，而不是直接返回
    剩余未匹配的数量；
    2) `x` 必须是"剩余的 '[' 或 ']' 各自的个数"（两者相等），不是"栈的总长度"——
    栈的总长度是 `2x`，如果不小心把栈的总长度直接代入换算公式，结果会翻倍；
    3) 判断"栈顶能否抵消当前的 ']'"时，条件是"栈非空且栈顶是 '['"，不能只判断栈非空
    就直接弹出——如果栈顶已经是一个未匹配的 ']'（因为它自己也曾经找不到配对被压进
    栈里），当前这个新来的 ']' 不能和它抵消，必须也压入栈中等待后续的 '[' 来匹配。
    """
    stack: list[str] = []
    for ch in s:
        if ch == "[":
            stack.append(ch)
        elif stack and stack[-1] == "[":
            stack.pop()
        else:
            stack.append(ch)
    x = len(stack) // 2
    return (x + 1) // 2


def min_add_to_make_valid(s: str) -> int:
    """
    【题意】给定只含 '(' 和 ')' 的字符串 s，每次操作可以在字符串的任意位置插入一个
    '(' 或 ')'，求最少插入多少个括号，能使结果变成合法的平衡括号串。
    【思路】不需要真的构造出插入后的结果，只需要统计"最少要补多少个"。用一个计数器
    `left_needed` 表示"当前为止，还没等到匹配右括号的左括号数量"（等价于一个只存
    '(' 的隐式栈，因为 '(' 之间彼此不需要区分身份，只需要计数）：遇到 '(' 就把
    `left_needed` 加一；遇到 ')' 时，如果 `left_needed > 0`，说明有一个之前欠下的
    '(' 正好可以和它配对，`left_needed` 减一（消耗掉一个欠账）；如果 `left_needed`
    已经是 0，说明这个 ')' 根本没有 '(' 能配它，必须靠"插入一个 '('"来补救，这种
    情况直接给答案计数器 `additions` 加一。扫描完整个字符串后，`left_needed` 里
    剩下的（如果还大于 0）是那些从头到尾都没等到匹配右括号的 '('，每一个都需要
    补一个 ')'，所以最终答案是 `additions + left_needed`。
    【复杂度】时间 O(n)，空间 O(1)（用一个整数计数器代替真正的栈，因为这里入栈的
    元素全都是同一种字符 '('，不需要记录具体是哪个，只需要记录数量）。
    【易错点】
    1) 遇到 ')' 时必须先检查 `left_needed` 是否大于 0 再决定是"消耗一个欠账"还是
    "记一次插入"，不能反过来先默认插入、再看要不要撤销——这两种情况互斥，写成先加
    再减容易在边界上重复计数；
    2) 扫描完成之后不能忘记把最终剩下的 `left_needed` 也加进答案——它们代表"从未被
    匹配过的左括号"，每一个都还需要在字符串末尾补一个右括号，只统计中途触发的
    `additions` 会漏掉这部分；
    3) 这里的计数器只需要追踪"未匹配的 '('"，不需要单独再开一个计数器追踪"未匹配的
    ')'"——因为一旦遇到 ')' 而计数器为 0，这个 ')' 已经在当场就被判定为"需要插入
    一个 '(' 来补"并计入 `additions`，不会被留到后面继续累积。
    """
    left_needed = 0
    additions = 0
    for ch in s:
        if ch == "(":
            left_needed += 1
        else:
            if left_needed > 0:
                left_needed -= 1
            else:
                additions += 1
    return additions + left_needed


def reverse_parentheses(s: str) -> str:
    """
    【题意】给定只含小写字母和圆括号的字符串 s（括号保证合法配对），从最内层的括号
    开始，依次翻转每一对括号内的子串，最终返回不包含任何括号字符的结果字符串。
    【思路】"从最内层开始翻转，再一层层把结果带到外层"，本质上和"字符串解码"（LC394）
    是同一种"挂起当前层、进入更深一层、退出时把内层结果揉回外层"的范式，只是这里
    每一层挂起/恢复的是"当前层已经拼好的字符列表"，不需要额外的数字栈。用一个栈，
    栈里的每个元素是"当前这一层已经收集到的字符列表"，初始栈里放一个空列表代表最
    外层：扫描到普通字母就追加到栈顶列表；扫描到 '(' 就压入一个新的空列表（开启更深
    一层，把外层列表挂起）；扫描到 ')' 就弹出栈顶列表（这一层已经收集完整），把它
    整体反转，再把反转后的内容整体接到新的栈顶（也就是外层）列表的末尾——这一步"接到
    外层末尾"天然实现了"内层翻转结果被揉回外层"的效果，外层还可以在这基础上继续追加
    后续字符，之后如果外层自己又被包在更外一层括号里，会在处理更外层的 ')' 时被
    再反转一次，如此层层传递，最终扫描完整个字符串时，栈底（最外层）列表拼接起来就是
    最终答案。
    【复杂度】时间 O(n^2) 最坏情况（每一层 `reverse()` 都是线性的，嵌套深度乘以每层
    长度在最坏情况下退化成平方级；存在更优的 O(n) "虫洞传送"技巧，通过预处理每对
    括号的匹配位置、用一个方向变量正反横跳来避免真正的字符串反转操作，这里为了让
    "挂起/恢复"这个通用范式更直观，采用更容易理解的显式栈+反转写法）；空间 O(n)。
    【易错点】
    1) 遇到 ')' 时，"反转"和"接到外层"这两步的顺序不能颠倒——必须先把弹出的这一层
    完整反转一遍，再整体接到外层末尾，如果反过来先接后转，会把外层已有的内容也
    一起反转进去；
    2) 每次遇到 '(' 都要压入一个全新的空列表，不能尝试"复用"或者共享内层和外层的
    列表对象，否则外层列表在内层反转时可能被意外污染（Python 里 `list.reverse()`
    是原地操作，如果内外层意外共享了同一个列表对象，外层已有内容也会被一起反转）；
    3) 最终结果取的是 `stack[0]`（最外层，也是唯一保留到扫描结束、没有被弹出过的
    那一层），因为输入保证括号合法配对，扫描结束时栈里必然只剩这一层，不需要额外
    判断栈是否还有多余的元素。
    """
    stack: list[list[str]] = [[]]
    for ch in s:
        if ch == "(":
            stack.append([])
        elif ch == ")":
            top = stack.pop()
            top.reverse()
            stack[-1].extend(top)
        else:
            stack[-1].append(ch)
    return "".join(stack[0])


def max_depth(s: str) -> int:
    """
    【题意】给定一个合法的括号表达式 s（可能夹杂数字、运算符等非括号字符），定义
    "嵌套深度"为字符串中括号最多嵌套了多少层（比如 "()()" 深度是 1，"(()())" 深度
    是 2）。返回整个表达式的嵌套深度。
    【思路】"嵌套深度"在任意时刻，就是"当前还没被关闭的左括号数量"——这个数量在扫描
    过程中会随着 '(' 增加、随着 ')' 减少，本质上和"括号是否合法"用的是同一个计数器，
    只是这里不需要判断合法性（题目保证输入合法），只需要在扫描过程中不断记录这个
    计数器出现过的**最大值**。用一个变量 `depth` 表示当前深度，另一个变量 `best`
    记录历史最大深度：遇到 '(' 就先把 `depth` 加一、再立刻用它更新 `best`（这个顺序
    很重要）；遇到 ')' 就把 `depth` 减一；其余字符（数字、运算符等）直接跳过，不影响
    这两个变量。
    【复杂度】时间 O(n)，空间 O(1)。
    【易错点】
    1) 更新 `best` 必须在 `depth` **加一之后**立即进行，不能等到遇到对应的 ')'
    再更新——如果错误地把更新时机放在 ')' 分支里（用"减一之前的值"更新），虽然数值
    上可能凑巧一样，但这混淆了"深度峰值出现在左括号入栈的那一刻"这个语义，遇到更
    复杂的变体（比如要求返回"取得最大深度的具体位置"）时容易出错；
    2) 只需要维护两个整数变量，不需要用真正的栈——因为这里只关心"嵌套了多少层"这个
    数值本身，不需要知道每一层具体对应哪个字符或者哪个位置，用栈会引入不必要的
    O(n) 额外空间；
    3) 非括号字符（数字、'+'、'*'、'/' 等）必须被完全忽略，既不增加也不减少
    `depth`——本题的"深度"只和括号嵌套结构有关，和括号内部具体是什么内容无关。
    """
    depth = 0
    best = 0
    for ch in s:
        if ch == "(":
            depth += 1
            best = max(best, depth)
        elif ch == ")":
            depth -= 1
    return best


def longest_valid_parentheses(s: str) -> int:
    """
    【题意】给定只含 '(' 和 ')' 的字符串 s，返回其中"最长的、能构成合法括号匹配"的
    连续子串的长度（子串不要求从头或到尾，只要求连续且内部本身合法平衡）。
    【思路】这题比"有效的括号"（判断整体是否合法）更进一步：不仅要判断合不合法，
    还要在"存在不合法字符"的前提下，找出被这些不合法字符分隔出来的、最长的那一段
    合法区间的**长度**。核心技巧是：栈里不直接存字符 '(' / ')'，而是存**下标**，并且
    预先在栈底放入一个哨兵下标 `-1`，用来表示"当前已知的、最后一个没有被匹配上的
    右括号的位置"（初始时还没扫描到任何右括号，就先假设"位置 -1 处有一个虚拟的、
    没被匹配上的右括号"，作为长度计算的基准点）。扫描时：遇到 '(' 就把它的下标压栈
    （表示"这个左括号正等待被匹配"）；遇到 ')' 就先弹出栈顶——如果栈顶是某个 '('
    的下标，弹出后这一对就算匹配上了，此时栈顶（弹出之后的新栈顶）就是"当前这一段
    连续合法区间往左还能追溯到的边界"，用 `当前下标 i - 新栈顶下标` 就能算出这一段
    合法区间的长度；如果弹出之后栈变空了，说明刚才弹出的那个不是真正的 '(' 配对（其实
    是哨兵，或者栈本来就已经空了），这意味着当前这个 ')' 本身就是一个"新的、没被
    匹配上的右括号"，把它的下标重新压回栈里，作为新的基准点，继续扫描。
    【复杂度】时间 O(n)，空间 O(n)（栈最坏情况存满所有下标）。
    【易错点】
    1) 栈里存的必须是**下标**而不是字符本身——最终要计算的是长度（下标之差），如果
    只存字符 '('，弹出匹配之后没有办法知道"这一段合法区间从哪个位置开始"；
    2) 初始必须往栈里放一个哨兵 `-1`，不能让栈从空开始——如果字符串一开始就是
    "()"这种最简单的合法对，扫描到 ')' 弹出 '(' 的下标 0 之后，栈会变空，如果没有
    这个哨兵兜底，取"新栈顶"就会因为栈是空的而出错；哨兵的作用就是"即使目前为止一次
    真正的 '(' 都没有被压栈过，也总有一个基准下标可以拿来算长度"；
    3) 每次弹出后**必须检查栈是否变空**，变空说明刚才弹出的其实是基准哨兵（或者
    是之前压回去的"新基准"），这种情况下当前下标本身要重新压回栈，成为**新的**
    基准点，而不能继续用"弹出之前的下标"去计算长度——这一步是本题最容易漏掉的分支，
    漏掉它会在类似 ")()())" 这种"右括号先于任何匹配出现"的输入上算错。
    """
    stack: list[int] = [-1]
    max_len = 0
    for i, ch in enumerate(s):
        if ch == "(":
            stack.append(i)
        else:
            stack.pop()
            if not stack:
                stack.append(i)
            else:
                max_len = max(max_len, i - stack[-1])
    return max_len


