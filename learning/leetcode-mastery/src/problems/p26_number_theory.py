def find_gcd_of_array(nums: list[int]) -> int:
    """
    【题意】LeetCode 1979（Easy）。给一个整数数组 nums，返回数组中最小值和最大值的
    最大公约数（GCD）。官方样例：nums=[2,5,6,9,10] → 2（最小值 2、最大值 10，
    gcd(2,10)=2）；nums=[7,5,6,8,3] → 1（最小值 3、最大值 8，gcd(3,8)=1）；
    nums=[3,3] → 3（最小值最大值都是 3，gcd(3,3)=3）。

    【思路】这题本身的"陷阱"很小——很多人会下意识地以为要求整个数组所有元素的 GCD，
    但题目只要求最小值和最大值这两个数的 GCD。抓住这一点之后，思路就是先用一次线性
    扫描找到 min 和 max，再用欧几里得算法（辗转相除法）求这两个数的 GCD：
    `gcd(a, b) = gcd(b, a % b)`，直到 b 变成 0，此时 a 就是答案。这是整个数论章节里
    最基础的应用——后面的扩展欧几里得算法、模逆元、CRT 全部建立在"求 GCD"这个操作
    之上，这道题相当于章节的热身。

    【复杂度】时间 O(n + log(min(a,b)))（n 用来找 min/max，之后欧几里得算法的迭代
    次数是对数级的）；空间 O(1)。

    【易错点】
    - 容易想复杂，去求整个数组所有元素两两的 GCD 或者所有元素的公共 GCD——题目明确
      只要求"最小值"和"最大值"这两个数的 GCD，其余元素完全不参与计算。
    - Python 自带 `math.gcd`，实际项目里可以直接调用；但这里手写一遍欧几里得算法，
      是为了给后面的扩展欧几里得算法（`ext_gcd`）做铺垫——两者结构几乎一样，只是
      扩展版还要多维护一组系数。
    """
    lo, hi = min(nums), max(nums)
    while hi != 0:
        lo, hi = hi, lo % hi
    return lo


def ext_gcd(a: int, b: int) -> tuple[int, int, int]:
    """
    【题意】自实现数论算法组件（custom，非具体 LeetCode 题）。实现扩展欧几里得算法
    `ext_gcd(a, b) -> (g, x, y)`，要求返回的三元组满足贝祖等式
    `a*x + b*y == g == gcd(a, b)`。接口约定：a、b 为非负整数（允许其中一个为 0），
    返回值 g 恒为非负的 gcd(a,b)，x、y 是满足等式的一组整数系数（不要求唯一，只要求
    满足等式）。

    【思路】普通欧几里得算法只求 `gcd(a, b)`，丢弃了"这个 gcd 是怎么由 a、b 线性
    组合出来的"这条信息。扩展欧几里得算法在递归回溯的过程中，把这条信息找回来：
    递归到 `ext_gcd(a, 0)` 时，显然 `gcd = a`，且 `a*1 + 0*0 = a`，所以基准情形是
    `(a, 1, 0)`。递归式 `ext_gcd(a, b) = ext_gcd(b, a % b)` 展开一层：设
    `ext_gcd(b, a % b)` 已经算出 `(g, x1, y1)`，满足 `b*x1 + (a % b)*y1 = g`。
    因为 `a % b = a - (a // b) * b`，代入得 `b*x1 + (a - (a//b)*b)*y1 = g`，
    整理成 `a*y1 + b*(x1 - (a//b)*y1) = g`——对比贝祖等式的形式 `a*x + b*y = g`，
    立刻得到 `x = y1`、`y = x1 - (a//b)*y1`。这样每往上回溯一层，就把子问题的系数
    "反推"成了当前层的系数，递归到顶层就得到了 a、b 的贝祖系数。

    【复杂度】时间 O(log(min(a,b)))（和普通欧几里得算法的迭代次数同阶）；空间
    O(log(min(a,b)))（递归调用栈深度）。

    【易错点】
    - 系数反推的公式 `x, y = y1, x1 - (a // b) * y1` 容易把 `x1` 和 `y1` 的角色
      搞反，或者把 `a // b` 误写成 `a % b`——一定要重新代入贝祖等式验证，而不是
      凭记忆默写。
    - 基准情形是 `b == 0` 而不是 `a == 0`：因为递归式每次把 `(a, b)` 变成
      `(b, a % b)`，b 会先降到 0（辗转相除法本身的终止条件），如果误判成 `a == 0`
      终止，会在 a、b 都不为 0 但恰好 a 先变小时提前退出，得到错误的 gcd。
    - 这个函数只是"求出满足等式的一组系数"，不保证 x、y 落在某个特定范围内（比如
      `|x| < b`）；如果下游需要"最小非负系数"，需要在这个结果基础上再做一次调整，
      本函数本身不做这个归一化。
    """
    if b == 0:
        return (a, 1, 0)
    g, x1, y1 = ext_gcd(b, a % b)
    x, y = y1, x1 - (a // b) * y1
    return (g, x, y)


def mod_inverse(a: int, m: int) -> int:
    """
    【题意】自实现数论算法组件（custom）。实现模逆元 `mod_inverse(a, m) -> int`，
    返回满足 `(a * result) % m == 1` 的 result（要求 `gcd(a, m) == 1`，否则模逆元
    不存在，本实现在这种情况下抛出 ValueError）。接口约定：m 为正整数，返回值落在
    `[0, m)` 区间内。

    【思路】在普通实数运算里，"除以 a" 等价于"乘以 a 的倒数 1/a"。但在模 m 的整数
    世界里没有"分数"这个概念，`1/a` 这个记号本身不存在——可是我们仍然希望有一个数
    `a_inv`，使得"乘以 a_inv"这个操作，效果等价于"除以 a"，也就是要求
    `a * a_inv ≡ 1 (mod m)`。这正是贝祖等式 `a*x + m*y = gcd(a,m)` 在
    `gcd(a,m)=1` 时的特例：等式变成 `a*x + m*y = 1`，两边对 m 取模，`m*y` 这一项
    直接消失（`m*y ≡ 0 mod m`），剩下 `a*x ≡ 1 (mod m)`——这里的 x 就是模逆元。
    于是直接调用 `ext_gcd(a, m)` 拿到 `(g, x, y)`，只要 `g == 1`（即 a、m 互质），
    `x % m` 就是所求的模逆元（取模是为了把 x 归一化到 `[0, m)` 范围内，因为
    `ext_gcd` 返回的 x 可能是负数或者超出这个范围）。

    【复杂度】时间 O(log m)（由 ext_gcd 决定）；空间 O(log m)（递归栈）。

    【易错点】
    - 模逆元存在的前提是 `gcd(a, m) == 1`；如果 m 不是质数，不能想当然地认为"只要
      a 不是 0 就有逆元"——比如 `mod_inverse(2, 4)` 不存在，因为 gcd(2,4)=2≠1，
      必须先检查这个前提，而不是盲目调用 ext_gcd 之后忽略 g 的值。
    - `ext_gcd` 返回的 x 可能是负数，必须做 `x % m` 而不是直接返回 x——Python 的
      `%` 对负数会自动归一化到非负区间（这一点和 C++/Java 不同），但如果照搬到
      其他语言，需要手动加一次 `+ m` 再取模来保证非负。
    - 当 m 是质数时，还有一条更简单的路径：由费马小定理，`a^(m-1) ≡ 1 (mod m)`，
      所以 `a^(m-2) mod m` 也是一个合法的模逆元，可以用 `pow_mod(a, m-2, m)` 求，
      这也是 `n_choose_r_mod` 里预处理阶乘逆元时实际采用的方法；本函数用扩展欧几
      里得算法实现是因为它不要求 m 是质数，适用范围更广。
    """
    g, x, _ = ext_gcd(a, m)
    if g != 1:
        raise ValueError(f"mod_inverse 不存在：gcd({a}, {m}) = {g} != 1")
    return x % m


def crt(remainders: list[int], moduli: list[int]) -> int:
    """
    【题意】自实现数论算法组件（custom）。实现中国剩余定理（CRT）
    `crt(remainders, moduli) -> int`，输入等长的余数列表和模数列表（模数两两互质），
    返回满足"对每个 i，result % moduli[i] == remainders[i]"的唯一解，并把结果归一化
    到 `[0, M)` 区间，其中 `M = moduli 的连乘积`（在这个模 M 的意义下解是唯一的）。

    【思路】直接的想法是从第一个方程 `x ≡ r_0 (mod m_0)` 的解集里逐个枚举，代入
    第二个方程验证，逐步收窄——这样做对，但当模数很大时枚举代价过高。CRT 的构造性
    做法一次性给出解的封闭形式：记 `M = m_0 * m_1 * ... * m_{k-1}`（所有模数的
    连乘积），对每个 i，定义 `M_i = M / m_i`（除了自己以外所有模数的乘积）。关键
    观察是：`M_i` 对模 `m_i` 一定有逆元（因为 `M_i` 只包含其余模数的因子，和
    `m_i` 互质），记这个逆元为 `inv_i = mod_inverse(M_i mod m_i, m_i)`。那么
    `M_i * inv_i` 这个数，对模 `m_i` 恒等于 1，对模 `m_j`（j≠i）恒等于 0（因为
    `M_i` 本身就包含因子 `m_j`）——也就是说，`M_i * inv_i` 恰好是"在第 i 个方程里
    贡献 1、在其余所有方程里贡献 0"的一个基向量。把所有方程的余数按这组基向量加权
    求和：`result = sum(r_i * M_i * inv_i for i) mod M`，就同时满足了所有方程——
    这本质上是线性代数里"用一组正交基表示任意向量"的思想，在同余方程组上的对应物。

    【复杂度】时间 O(k * log M)（k 个方程，每个方程需要一次模逆元计算，逆元计算是
    对数级的）；空间 O(1)（不算输入输出）。

    【易错点】
    - 前提是模数两两互质；如果模数不互质（比如 `[4, 6]`），`M_i` 和 `m_i` 就可能
      不互质，`mod_inverse` 会直接抛异常——这是本实现故意不做的"合并非互质模数"的
      扩展情形（那需要更复杂的 CRT 推广版本，判断余数是否兼容，不在本函数的接口
      约定范围内）。
    - 计算 `M_i * inv_i` 时如果不先对 `M_i mod m_i` 取模再求逆元，`M_i` 本身可能
      远大于 `m_i`，虽然数学上 `mod_inverse` 依然能算对（Python 整数无限精度不会
      溢出），但把 `M_i` 先取模能让中间结果更小、语义也更清晰。
    - 最终结果一定要对 M 取模归一化——CRT 保证的是解在模 M 意义下唯一，如果不取模，
      累加出来的 result 可能是一个远大于 M 的数（虽然它对每个 m_i 取模仍然正确，
      但不是题目约定的"落在 [0, M) 区间"的规范形式）。
    """
    M = 1
    for m in moduli:
        M *= m
    result = 0
    for r, m in zip(remainders, moduli):
        Mi = M // m
        inv = mod_inverse(Mi % m, m)
        result += r * Mi * inv
    return result % M


def reaching_points(sx: int, sy: int, tx: int, ty: int) -> bool:
    """
    【题意】LeetCode 780（Hard）。一次操作可以把点 (x, y) 变成 (x, x+y) 或者
    (x+y, y)。给定起点 (sx, sy) 和终点 (tx, ty)，判断是否存在一串操作把起点变成
    终点。官方样例：sx=1,sy=1,tx=3,ty=5 → True（(1,1)→(1,2)→(3,2)→(3,5)）；
    sx=1,sy=1,tx=2,ty=2 → False；sx=1,sy=1,tx=1,ty=1 → True。约束
    sx,sy,tx,ty ∈ [1, 10^9]。

    【思路】从起点正向搜索会在每一步分叉成两个方向，指数级爆炸，而且坐标范围到
    10^9，搜索树深度也可能很大，正向做不可行。本题的突破口是**逆向思考**：从终点
    往回推。如果 `(tx, ty)` 是某一步操作的结果，那么它一定是从
    `(tx - ty, ty)`（如果上一步是 `(x,y)→(x+y,y)`）或者 `(tx, ty - tx)`
    （如果上一步是 `(x,y)→(x,x+y)`）变来的——而且这个"上一步是谁"是**唯一确定**
    的：只要 `tx > ty`，唯一可能的上一步就是 `(tx-ty, ty)`（因为 y 分量没变，
    说明上一步操作的是 x）；反之 `ty > tx` 时唯一可能是 `(tx, ty-tx)`。这就是这题
    和"扩展欧几里得算法"思想相通的地方：辗转相除法反复用"大数对小数取模"来快速
    缩小规模，这里同理——与其一次减一个 ty（可能要减 10^9 次），不如直接用
    `tx %= ty`（一次性减掉尽可能多个 ty），这就是逆向欧几里得算法思想的直接应用。
    循环终止的判据分两种情况：当 `tx == sx` 时，只需要检查 `ty` 能否通过反复加
    `sx`（此时 `tx` 分量不再变化）回到 `sy`，即 `(ty - sy) % tx == 0`（当
    `ty >= sy` 时）；`ty == sy` 时对称处理；如果 `tx == ty` 但还没回到起点，
    说明再退一步就会产生除以 0，且这种状态下不可能是合法路径上的中间点（因为
    `x=y` 之后唯一的操作是 `(x,x)→(x,2x)` 或 `(2x,x)`，无法维持 `x=y` 又不是
    起点），直接判 False。

    【复杂度】时间 O(log(max(tx,ty)))（每次取模至少让较大的坐标减半量级，和
    欧几里得算法同阶）；空间 O(1)。

    【易错点】
    - 循环条件必须同时检查 `tx >= sx and ty >= sy`——如果只反推到某一步坐标就
      小于起点对应分量了，说明这条路径已经"越过"了起点，不可能再通过合法操作
      回到起点，需要立刻停止而不是继续减。
    - `tx == ty` 且尚未等于 `(sx, sy)` 时必须直接返回 False，否则 `tx %= ty`
      或 `ty %= tx` 会产生模 0 错误（除零异常）。
    - 终止时的收尾判断容易漏掉"必须能整除"这个条件，只检查 `ty >= sy` 是不够的：
      还要求 `(ty - sy)` 恰好是 `tx`（此时不再变化的那个分量）的整数倍，因为每次
      "退一步"都是恰好减去 tx，不能减出一个非整数倍的余量。
    """
    while tx >= sx and ty >= sy:
        if tx == sx and ty == sy:
            return True
        if tx > ty:
            if ty > sy:
                tx %= ty
            else:
                return tx > sx and (tx - sx) % ty == 0
        elif ty > tx:
            if tx > sx:
                ty %= tx
            else:
                return ty > sy and (ty - sy) % tx == 0
        else:
            return False
    return False


def count_primes(n: int) -> int:
    """
    【题意】LeetCode 204（Medium）。给定整数 n，返回严格小于 n 的素数个数。官方
    样例：n=10 → 4（2,3,5,7 共 4 个）；额外的参考值：n=100 → 25，n=1000 → 168。
    约束 0 <= n <= 5*10^6。

    【思路】最直接的想法是对每个小于 n 的数逐个做素性检测（试除到平方根），总
    时间 O(n*sqrt(n))，当 n 到 5*10^6 时会超时。埃拉托斯特尼筛法反过来利用"素数
    的倍数一定不是素数"这个方向：维护一个布尔数组 `is_prime`，初始假设 0..n-1
    都可能是素数；从最小的素数 2 开始，把 2 的所有倍数（4,6,8,...）标记为合数——
    注意标记的是"倍数"，不需要对每个倍数单独做除法验证；然后找下一个仍然标记为
    "可能是素数"的数（此时它前面所有更小的素数的倍数都已经被标记过，如果它还没
    被标记，说明它不能被任何更小的素数整除，它自己就是素数），重复这个过程。
    一个关键优化：外层循环只需要跑到 `sqrt(n)`，因为如果一个数 `k >= sqrt(n)`
    是合数，它一定能写成 `k = p * q`（p <= q），此时 `p <= sqrt(k) <= sqrt(n)`，
    也就是说 k 的某个因子一定不超过 `sqrt(n)`，已经在更早的外层循环里把 k 标记
    掉了，不需要 k 自己再作为外层循环变量去筛一遍。内层标记倍数时，还可以从
    `p*p` 开始而不是 `2*p`——因为 `2*p, 3*p, ..., (p-1)*p` 这些倍数已经在更小的
    素数 2, 3, ..., p-1 作为外层循环变量时被标记过了。

    【复杂度】时间 O(n log log n)（筛法的标准复杂度，比 O(n*sqrt(n)) 的暴力试除
    快得多）；空间 O(n)（布尔数组）。

    【易错点】
    - 题目要求"严格小于 n"，数组大小应该开 n（下标 0..n-1 对应数字 0..n-1），
      n 本身不参与计数——容易多算或少算边界，比如误开成 n+1 大小然后把 n 自己
      也计入。
    - `n <= 2` 时（n=0 或 n=1 或 n=2）答案恒为 0（0 和 1 以下没有素数，2 本身
      不小于 n=2 所以也不计入），需要单独处理，否则内层循环的 `int(n**0.5)`
      在 n 很小时可能导致数组越界或者逻辑错误。
    - 内层标记倍数的起点用 `i*i` 而不是 `2*i` 是一个重要的性能优化，但即使写成
      `2*i` 结果也是正确的（只是变慢），不是正确性问题；真正会导致错误的是
      忘记把 `is_prime[0]` 和 `is_prime[1]` 显式标记为 False（0 和 1 都不是
      素数，但初始化数组时它们和其他数字一样被默认标记为"可能是素数"）。
    """
    if n <= 2:
        return 0
    is_prime = [True] * n
    is_prime[0] = is_prime[1] = False
    for i in range(2, int(n ** 0.5) + 1):
        if is_prime[i]:
            for j in range(i * i, n, i):
                is_prime[j] = False
    return sum(is_prime)


def pow_mod(base: int, exp: int, mod: int) -> int:
    """
    【题意】自实现数论算法组件（custom）。实现快速幂取模 `pow_mod(base, exp, mod)`，
    计算 `(base ** exp) % mod`，要求 exp 为非负整数、mod 为正整数（mod=1 时约定
    返回 0，因为任何数模 1 都是 0）。

    【思路】朴素做法是循环把 base 累乘 exp 次、每次都取模，O(exp) 次乘法——当
    exp 达到 10^9 甚至更大时完全不可行。快速幂的核心洞察和 19 类"位运算与数学"
    里的 `my_pow` 完全一致：`base^exp = (base^(exp//2))^2`（exp 为偶数时），
    `base^exp = base * (base^(exp//2))^2`（exp 为奇数时）——每次把指数减半、底数
    平方，只需要 O(log exp) 次乘法。实现上把 exp 看成二进制展开，从最低位开始：
    每一位为 1 就把当前的 `base`（不断自我平方）累乘进 `result`，然后无论如何都让
    `base` 自我平方、`exp` 右移一位，直到 `exp` 变成 0。和 `my_pow` 唯一的区别是
    这里全程对 mod 取模（保证中间结果不会无限增长——虽然 Python 整数不会溢出，
    但取模能让每一步的乘法始终作用在 `[0, mod)` 范围内的数上，避免中间结果的位数
    无限膨胀拖慢乘法本身的速度）。

    【复杂度】时间 O(log exp)（每次循环指数右移一位）；空间 O(1)（迭代实现）。

    【易错点】
    - 每一步都要对 `result` 和 `base` 取模，而不是等最后再取模一次——如果只在
      最后取模，中间的 `base` 会随着不断平方而变得极其庞大（位数指数增长），
      虽然结果理论上仍然正确，但会让每次乘法的开销随位数线性增长，退化成远比
      O(log exp) 慢的实现，失去了"取模快速幂"相对朴素 `pow(base,exp)%mod` 的
      性能优势。
    - `exp` 右移和 `base` 平方的顺序不能颠倒：应该先根据当前最低位决定要不要把
      `base` 乘进 `result`，再让 `base` 平方、`exp` 右移，为下一位做准备——这一点
      和 `my_pow` 完全一样，顺序反了会用错某一位对应的底数幂次。
    - `base` 在进入循环前应该先对 mod 取模一次（`base %= mod`）；如果 base 本身
      是负数或者远大于 mod，不预处理直接进入循环虽然数学上依然收敛到正确答案
      （Python 的 `%` 会保证结果非负），但提前取模能让每一步的中间值更小。
    """
    if mod == 1:
        return 0
    result = 1
    base %= mod
    while exp > 0:
        if exp & 1:
            result = result * base % mod
        base = base * base % mod
        exp >>= 1
    return result


def super_pow(a: int, b: list[int]) -> int:
    """
    【题意】LeetCode 372（Medium）。计算 `a^b mod 1337`，其中 a 是正整数，b 是一个
    极大的指数，以数组形式给出每一位数字（比如 b=[1,2,3] 表示指数是 123）。官方
    样例：a=2, b=[3] → 8（2^3=8）；a=2, b=[1,0] → 1024（指数是 10，2^10=1024）。

    【思路】b 可能有成千上万位，不可能先把它拼成一个 Python 大整数再直接算幂
    （虽然 Python 整数不会溢出，但指数本身大到这个程度时朴素快速幂的 O(log b)
    也会退化成 O(位数 * 巨大常数)，不是这题的正确解法方向；本题真正的教学点是
    "如何在不拼出完整指数数值的情况下，逐位消化这个指数"）。关键的同余等式是：
    如果指数 b 可以写成 `b = 10*p + q`（p 是去掉最后一位的前缀，q 是最后一位
    数字），那么 `a^b = a^(10p) * a^q = (a^p)^10 * a^q`——两边同时对 1337 取模
    依然成立（模运算对乘法满足同余关系，即 `(x*y) mod m = ((x mod m)*(y mod m))
    mod m`）。这意味着可以用 Horner 法则从左到右逐位处理 b：维护一个"目前已处理
    前缀对应的幂"result，每读入一位新数字 digit，就令
    `result = (result^10 * a^digit) mod 1337`——这样每一步只需要两次 O(log)
    的模幂运算（`pow_mod(result, 10, mod)` 和 `pow_mod(a, digit, mod)`），
    而不是真的去计算一个几千位的指数。

    【复杂度】时间 O(len(b) * log(10))（对 b 的每一位做一次常数次的模幂运算，
    每次模幂运算是 O(log 10) 即 O(1)量级，因为底数不超过 10）；空间 O(1)（不算
    输入 b 本身）。

    【易错点】
    - 逐位更新的公式是 `result = pow_mod(result, 10, mod) * pow_mod(a, digit,
      mod) % mod`，容易把顺序写反成"先乘 a 的幂再整体十次方"，那样算出来的是
      `(result * a^digit)^10`，和正确的 `result^10 * a^digit` 是完全不同的
      表达式。
    - a 本身可能大于等于 1337（题目没有限制 a 的范围），累乘之前必须先对 a 取模
      （`a %= mod`），否则第一次 `pow_mod(a, digit, mod)` 虽然内部也会取模、
      结果依然正确，但如果后续代码里有直接使用未取模的 a 做其他运算，容易埋下
      不一致的隐患。
    - 1337 本身不是质数（1337 = 7 * 191），不能套用费马小定理相关的"指数循环节"
      简化技巧；这题的正解完全依赖"同余关系对乘法和乘方的分配律"，而不依赖 mod
      是否是质数，这一点是很多初学者容易搞混的地方。
    """
    mod = 1337
    a %= mod
    result = 1
    for digit in b:
        result = pow_mod(result, 10, mod) * pow_mod(a, digit, mod) % mod
    return result


def count_good_numbers(n: int) -> int:
    """
    【题意】LeetCode 1922（Medium）。一个长度为 n 的数字字符串是"好数字字符串"，
    当且仅当偶数下标（0-indexed）上的数字都是偶数（0,2,4,6,8），奇数下标上的数字
    都是质数（2,3,5,7）。返回长度为 n 的好数字字符串的总数，答案对 10^9+7 取模。
    官方样例：n=1 → 5（"0","2","4","6","8" 共 5 个）；n=4 → 400；n=50 →
    564908303。

    【思路】暴力枚举长度为 n 的所有数字字符串再逐个检查是否"好"，指数级不可行。
    这题的突破口是发现每个下标位置的选择是**互相独立**的：偶数下标（一共
    `ceil(n/2)` 个位置）每个位置各有 5 种独立选择（0,2,4,6,8），奇数下标（一共
    `floor(n/2)` 个位置）每个位置各有 4 种独立选择（2,3,5,7）。根据乘法原理，
    总方案数就是 `5^(偶数位个数) * 4^(奇数位个数)`——这是一个纯组合计数问题，
    一旦想清楚这个乘法原理，剩下的就是"如何快速算出这两个幂"：n 最大可以到
    10^15，直接循环乘不可行，必须用快速幂（`pow_mod`）把每个幂的计算压缩到
    O(log n)。这题和 `super_pow` 的共同点是"两者都依赖快速幂处理超大规模的指数"，
    区别在于 `super_pow` 的难点是"指数本身大到无法直接表示"，而这题的难点是
    "如何把一个计数问题转化成幂的乘积"这一步组合数学的洞察。

    【复杂度】时间 O(log n)（两次快速幂，每次 O(log n)）；空间 O(1)。

    【易错点】
    - 偶数位个数是 `(n+1)//2` 而不是 `n//2`——因为下标从 0 开始，长度为 n 时
      偶数下标是 0,2,4,...，个数是 `ceil(n/2) = (n+1)//2`；奇数位个数才是
      `n//2`。容易把两者算反或者搞混"向上取整"和"向下取整"分别对应哪一类下标。
    - 两次 `pow_mod` 的结果相乘之后还要再取一次模——`pow_mod` 各自的返回值已经
      在 `[0, mod)` 范围内，但两个这样的数相乘之后的乘积可能远超过 mod，必须
      再对 mod 取模才能得到最终答案。
    - 这题的 n 可以小到 0 吗？题目约束 n >= 1，`(n+1)//2` 和 `n//2` 在 n=1 时
      分别是 1 和 0，对应"长度为 1 的好数字字符串只能是一个偶数位"，和样例
      "n=1 → 5" 一致，不需要对 n=0 做特殊处理。
    """
    mod = 10 ** 9 + 7
    even_positions = (n + 1) // 2
    odd_positions = n // 2
    return pow_mod(5, even_positions, mod) * pow_mod(4, odd_positions, mod) % mod


def _mat_mult(a: list[list[int]], b: list[list[int]]) -> list[list[int]]:
    """2x2 矩阵乘法，是 `fib_matrix_pow` 的内部辅助函数，不单独作为练习题。"""
    return [
        [a[0][0] * b[0][0] + a[0][1] * b[1][0], a[0][0] * b[0][1] + a[0][1] * b[1][1]],
        [a[1][0] * b[0][0] + a[1][1] * b[1][0], a[1][0] * b[0][1] + a[1][1] * b[1][1]],
    ]


def _mat_pow(m: list[list[int]], power: int) -> list[list[int]]:
    """2x2 矩阵快速幂，是 `fib_matrix_pow` 的内部辅助函数，不单独作为练习题。"""
    result = [[1, 0], [0, 1]]  # 单位矩阵，幂为 0 时的返回值
    base = m
    while power > 0:
        if power & 1:
            result = _mat_mult(result, base)
        base = _mat_mult(base, base)
        power >>= 1
    return result


def fib_matrix_pow(n: int) -> int:
    """
    【题意】自实现数论算法组件（custom）。用矩阵快速幂求斐波那契数列第 n 项
    `fib_matrix_pow(n) -> int`（约定 F(0)=0, F(1)=1, F(2)=1, F(3)=2, ...，n 为
    非负整数），要求在 O(log n) 时间内完成（而不是朴素递推的 O(n)）。

    【思路】朴素递推 `F(n) = F(n-1) + F(n-2)` 是 O(n) 的——对于 n 很大（比如
    10^18）的场景会太慢。矩阵快速幂的核心洞察是：**线性递推可以写成矩阵乘法**。
    把状态 `(F(n+1), F(n))` 看成一个列向量，递推关系
    `F(n+1) = F(n) + F(n-1)` 和 `F(n) = F(n)` 合起来可以写成矩阵形式：
    `[F(n+1), F(n)]^T = [[1,1],[1,0]] * [F(n), F(n-1)]^T`。也就是说，"往前推
    一项"这个操作，等价于"左乘一次矩阵 `M = [[1,1],[1,0]]`"。递推 n 次就是
    连乘 n 次这个矩阵：`[F(n+1), F(n)]^T = M^n * [F(1), F(0)]^T = M^n *
    [1, 0]^T`——这意味着 `M^n` 的第一行第二列（0-indexed 的 `[0][1]`）恰好就是
    `F(n)`。而"矩阵乘法满足结合律"这条性质，恰好是快速幂能够应用在矩阵上的
    前提条件——正如整数乘法的快速幂用"指数减半、底数平方"把 O(n) 次乘法压缩到
    O(log n) 次，这里同样用"指数减半、矩阵自乘"把 O(n) 次矩阵乘法压缩到
    O(log n) 次：`M^n = (M^(n//2))^2`（n 为偶数）或者
    `M^n = M * (M^(n//2))^2`（n 为奇数）。只要一种运算满足结合律，快速幂这套
    "折半"框架就能套用在它上面，不局限于普通数字的乘法——这是这道题最重要的
    泛化启示：任何能写成"状态向量 = 常数矩阵 * 上一个状态向量"形式的线性递推
    （不只是斐波那契，任意固定阶数的线性递推都可以），都能用同样的矩阵快速幂
    技巧从 O(n) 降到 O(log n)。

    【复杂度】时间 O(log n)（矩阵快速幂的迭代次数，每次迭代做常数次 2x2 矩阵
    乘法，矩阵乘法本身是 O(1) 量级）；空间 O(1)（2x2 矩阵大小固定）。

    【易错点】
    - 矩阵快速幂返回的是 `M^n` 这整个矩阵，正确答案需要从中取出 `[0][1]` 这个
      分量（或者等价地 `[1][0]`，因为这个矩阵是对称的）——容易直接返回整个矩阵
      或者取错分量（比如误取 `[0][0]`，那实际上是 `F(n+1)` 而不是 `F(n)`）。
    - n=0 时不需要特判：`M^0` 按约定是单位矩阵 `[[1,0],[0,1]]`，其 `[0][1]`
      分量恰好是 0，正好等于 `F(0)=0`，这是"矩阵快速幂的幂为 0 返回单位矩阵"
      这个通用约定自然带来的正确性，不需要额外的边界特判代码。
    - 如果要求"斐波那契数取模"（数论题里常见的变体），矩阵乘法里的每一步累加
      都要记得同步取模，否则矩阵元素会随着 n 增大而无限增长——本函数为了保持
      和"朴素递推"直接比较用的都是无限精度整数，没有加取模，如果 n 极大导致
      结果本身有几十万位，会退化成大数乘法的开销，这是可以按需扩展但当前实现
      故意没有做的一处简化。
    """
    m = [[1, 1], [1, 0]]
    powered = _mat_pow(m, n)
    return powered[0][1]


def n_choose_r_mod(n: int, r: int, mod: int) -> int:
    """
    【题意】自实现数论算法组件（custom）。实现组合数取模
    `n_choose_r_mod(n, r, mod) -> int`，计算 `C(n, r) mod mod`（要求 mod 是一个
    质数，这是费马小定理成立的前提；n, r 为非负整数，r > n 时约定返回 0）。

    【思路】直接用 `math.comb(n, r)` 算出组合数再取模，在 n 不大时完全没问题；
    但当 n 达到 10^6 甚至更大时，`math.comb` 本身会先计算出一个天文数字般大的
    精确整数（阶乘增长极快），既慢又浪费内存，而这个巨大的精确值其实我们根本
    不需要——只需要它模 mod 之后的结果。组合数的公式是
    `C(n, r) = n! / (r! * (n-r)!)`，但模运算下不能直接做除法（分母的阶乘在模
    mod 意义下没有"除"这个操作），这正是 `mod_inverse` 存在的意义：把除以
    `r!` 转换成乘以 `r!` 的模逆元。具体做法分两步：第一步预处理 0 到 n 的阶乘
    `fact[i] = i! mod mod`（递推 `fact[i] = fact[i-1] * i % mod`）；第二步
    预处理每个阶乘的模逆元 `inv_fact[i]`——一种做法是对每个 i 单独调用
    `pow_mod(fact[i], mod-2, mod)`（费马小定理：mod 是质数时，
    `a^(mod-1) ≡ 1 (mod mod)`，两边除以 a 得到 `a^(mod-2)` 就是 a 的模逆元），
    但更高效的做法是只对 `fact[n]` 算一次逆元，再利用
    `inv_fact[i-1] = inv_fact[i] * i % mod` 从大到小递推出其余所有阶乘逆元
    （因为 `inv_fact[i] = 1/i! = (1/(i-1)!) * (1/i)`，反过来
    `inv_fact[i-1] = inv_fact[i] * i`），这样只需要一次 O(log mod) 的快速幂，
    其余都是 O(1) 递推。最终 `C(n,r) = fact[n] * inv_fact[r] * inv_fact[n-r]
    mod mod`。

    【复杂度】时间 O(n + log mod)（预处理阶乘和阶乘逆元各 O(n)，一次快速幂
    O(log mod)）；空间 O(n)（阶乘和阶乘逆元两个数组）。

    【易错点】
    - 这个实现依赖 mod 是质数（费马小定理的前提），如果传入一个合数 mod，
      `pow_mod(fact[n], mod-2, mod)` 算出来的不是真正的模逆元，结果会是错的
      且不会报错——这是本函数接口约定里"mod 必须是质数"这一条必须由调用者
      保证的隐含前提，函数内部不做质数校验。
    - `inv_fact` 的递推方向是从 n 往 0 递减（`inv_fact[i-1] = inv_fact[i] * i
      % mod`），如果写反成从 0 往 n 递增，因为只有 `inv_fact[n]` 是一开始就
      知道的，递增方向根本无法启动这个递推。
    - r 为负数或者 r > n 时组合数应该是 0（不存在这样的组合），如果不加这个
      边界检查直接进入数组访问 `inv_fact[n - r]`，当 `r > n` 时 `n - r` 是
      负数，Python 的负数下标会"悄悄地"访问到数组末尾附近的元素而不报错，
      得到一个看似正常但完全错误的结果，这是最容易被忽略的一类边界错误。
    """
    if r < 0 or r > n:
        return 0
    fact = [1] * (n + 1)
    for i in range(1, n + 1):
        fact[i] = fact[i - 1] * i % mod
    inv_fact = [1] * (n + 1)
    inv_fact[n] = pow_mod(fact[n], mod - 2, mod)
    for i in range(n, 0, -1):
        inv_fact[i - 1] = inv_fact[i] * i % mod
    return fact[n] * inv_fact[r] % mod * inv_fact[n - r] % mod


def smallest_repunit_length_div_by_k(k: int) -> int:
    """
    【题意】LeetCode 1015（Medium）。给定正整数 k，找出最小的、只由数字 1 组成的
    正整数（"重复数"，比如 1, 11, 111, ...）中能被 k 整除的那一个，返回它的位数；
    不存在则返回 -1。官方样例：k=1 → 1（n=1）；k=2 → -1（没有全 1 数能被 2
    整除）；k=3 → 3（n=111=3*37）；额外参考：k=7 → 6（n=111111=7*15873）。

    【思路】真实的重复数 n（比如 k=7 时的 111111）可能有几十万位，直接构造这个
    大整数再做除法完全不现实。这题的关键转化是：我们不需要重复数本身的数值，
    只需要它模 k 是否为 0，而"模 k 的余数"只需要 O(1) 空间就能维护——设当前
    余数是 r（表示"已经拼出来的这段全 1 数"模 k 的结果），在右边再拼上一个数字
    1，新的数相当于 `旧数 * 10 + 1`，对应的新余数就是 `(r * 10 + 1) % k`，
    完全不需要知道旧数或新数的实际数值。于是从长度 1 开始，反复用这个递推式
    更新余数，一旦余数变成 0，当前的长度就是答案。这里的"同余类"想法和 CRT、
    模逆元是同一条主线：把"一个可能无限增长的大数"投影到"模 k 的一个等价类"
    里处理，只关心那个等价类，不关心具体的数值。终止性由鸽笼原理保证：余数
    只有 `0` 到 `k-1` 共 k 种可能，如果循环了 k 步还没出现余数 0，说明进入了
    一个不含 0 的循环节，之后永远不会出现 0，可以直接判定无解——这也是为什么
    要在这之前先用"k 是偶数或者 5 的倍数就一定无解"这个数论性质做快捷剪枝：
    全 1 数永远是奇数、且永远不以 0 或 5 结尾，所以不可能被 2 或 5 整除
    （这条性质可以跳过多达 k 步的无效循环，直接返回 -1）。

    【复杂度】时间 O(k)（最坏情况下需要遍历完整的 k 个余数才能判定是否有解）；
    空间 O(1)（只维护一个余数变量和一个长度计数器）。

    【易错点】
    - 容易忘记先做"k 能被 2 或 5 整除就一定无解"这个快捷判断——不加这个判断，
      算法本身依然正确（会在循环 k 步之后自然发现进入了循环节而没找到 0，从而
      返回 -1），只是性能上更差；但很多人会误以为这个判断是"必须要有的正确性
      保障"，实际上它只是一个性能优化，去掉它答案依然正确，只是慢。
    - 循环的终止条件是"余数变成 0"而不是"长度达到某个固定值"——如果把循环写成
      固定跑 k 次再看最后一次的余数是否为 0，会漏掉"余数在中途就已经变成 0"
      这种应该提前返回的情况。
    - 初始余数应该是 `1 % k`（对应长度为 1 的重复数"1"本身）而不是 0——如果
      从 0 开始且长度计数器也从 0 开始，会在 k=1 这种边界情况上算错（k=1 时
      任何数都能被 1 整除，正确答案是长度 1，而不是 0）。
    """
    if k % 2 == 0 or k % 5 == 0:
        return -1
    remainder = 1 % k
    length = 1
    while remainder != 0:
        remainder = (remainder * 10 + 1) % k
        length += 1
    return length


def judge_square_sum(c: int) -> bool:
    """
    【题意】LeetCode 633（Medium）。给定非负整数 c，判断是否存在两个整数 a、b
    （a, b >= 0）满足 `a^2 + b^2 == c`。官方样例：c=5 → True（1^2+2^2=5）；
    c=3 → False；额外参考：c=4 → True（0^2+2^2=4），c=2 → True（1^2+1^2=2）。

    【思路】暴力枚举 a 和 b 两层循环是 O(c) 甚至 O(c^2)（如果不加剪枝）。这题的
    突破口是发现"随 a 增大、a^2 增大"和"随 b 减小、b^2 减小"这两个方向正好
    相反，天然适合双指针：从 `a=0` 出发（最小可能值）、`b=floor(sqrt(c))` 出发
    （最大可能值，因为 `b^2` 不能超过 c），计算 `s = a^2+b^2` 并和 c 比较——
    如果 `s < c`，说明当前这一对"太小"，只能通过增大 a 来增大 s（增大 b 会
    让 s 更偏离，因为 b 已经是能取的最大值附近）；如果 `s > c`，对称地减小 b。
    这个过程能够正确收敛的原因是：a 和 b 分别单调地朝着"让 s 更接近 c"的方向
    移动，且移动范围 `[0, sqrt(c)]` 每一步都在收窄，最多移动 `O(sqrt(c))` 步，
    双指针相遇（`a > b`）时如果还没找到就说明无解。这是"双指针+数学"这一类
    技巧的典型应用：先用数学（`b` 的上界是 `sqrt(c)`）确定搜索空间的边界，
    再用双指针的单调性把搜索空间从 O(c) 压缩到 O(sqrt(c))。

    【复杂度】时间 O(sqrt(c))（双指针最多各移动 O(sqrt(c)) 步）；空间 O(1)。

    【易错点】
    - `b` 的初始值必须用整数平方根（`math.isqrt`，向下取整），不能用浮点数
      `sqrt` 直接截断——浮点数在 c 很大时可能有精度误差，导致算出的初始 b
      偏大 1（比如某个刚好是完全平方数附近的 c），从而漏掉本该在边界处成立
      的解或者引入错误的越界访问。
    - 双指针的移动方向容易搞反：`s < c` 时应该增大 a（让和变大），`s > c`
      时应该减小 b（让和变小）——如果搞反移动方向，指针会朝着让 s 更偏离 c
      的方向走，退化成永远找不到解或者提前越界退出。
    - 循环条件是 `a <= b` 而不是 `a < b`——`a == b` 时仍然是一个合法的候选
      （比如 c=2 时 a=b=1 是解），如果用严格小于会漏掉这种"两个数相等"的解。
    """
    a = 0
    b = math.isqrt(c)
    while a <= b:
        s = a * a + b * b
        if s == c:
            return True
        if s < c:
            a += 1
        else:
            b -= 1
    return False


def consecutive_numbers_sum(n: int) -> int:
    """
    【题意】LeetCode 829（Hard，"面试. 连续整数求和"）。给定正整数 n，返回能把 n
    写成若干个连续正整数之和的方案数（至少 1 个数，n 自己也算一种方案）。官方
    样例：n=5 → 2（5=5，5=2+3）；n=9 → 3（9=9，9=4+5，9=2+3+4）；n=15 → 4
    （15=15，15=8+7，15=4+5+6，15=1+2+3+4+5）。约束 1 <= n <= 10^9。

    【思路】枚举"从哪个数开始、一共多少项"的连续和，暴力做法是双层循环，O(n^2)
    级别太慢。设这段连续和从正整数 a 开始、一共有 i 项（i >= 1），那么这段和是
    一个等差数列求和：`n = a + (a+1) + ... + (a+i-1) = i*a + i*(i-1)/2`，
    整理得 `a = (n - i*(i-1)/2) / i`。给定 i，a 有没有合法解（即 a 是不是正
    整数）就完全由这个式子决定——只需要检查 `(n - i*(i-1)/2)` 能不能被 i
    整除、且商是正数。于是问题转化成纯粹的"因子分解/整除枚举"：从 i=1 开始
    递增，对每个 i 检查上式是否给出合法的正整数 a，直到 `i*(i-1)/2 >= n`
    为止（此时 a 必然不再是正数，因为 `i*a` 这一项都不够抵消 `i*(i-1)/2`，
    更别说加上它还要等于正的 n）。这个终止条件天然把枚举范围限制在
    `O(sqrt(n))` 量级（因为 `i*(i-1)/2 < n` 意味着 `i` 是 `O(sqrt(n))` 级别），
    不需要额外的因子分解技巧就能达到 `O(sqrt(n))` 的复杂度——这是这道题作为
    本章节压轴题的原因：它把"求和公式的代数变形"和"整除性枚举"这两个数论
    子技能结合在一起，枚举量的上界又恰好由代数不等式自然给出。

    【复杂度】时间 O(sqrt(n))（i 的枚举范围由 `i*(i-1)/2 < n` 限制，是
    O(sqrt(n)) 级别）；空间 O(1)。

    【易错点】
    - 判断"a 是正整数"需要两个条件都满足：`(n - i*(i-1)//2)` 必须能被 i
      整除**并且**商必须是正数——只检查整除性、不检查商是否为正，会把"i 太大
      导致 a 变成 0 或负数"这种非法情况也误计入方案数。
    - 循环的终止条件 `i*(i-1)//2 < n` 容易写成 `i*(i-1)//2 <= n` 或者干脆
      写成一个固定的 `sqrt(n)` 上界然后不做这个精确判断——用不等式本身作为
      循环条件更稳妥，因为它直接对应"a 还有可能是正数"这个数学要求，而不是
      一个近似的范围估计。
    - i=1 对应的方案永远合法（"n 自己单独作为一项"），容易在特殊情况处理上
      画蛇添足地把它排除掉——按照统一的公式枚举，i=1 时
      `(n - 0) % 1 == 0` 恒成立，会自然而然地把这个方案计入，不需要特殊处理。
    """
    count = 0
    i = 1
    while i * (i - 1) // 2 < n:
        remaining = n - i * (i - 1) // 2
        if remaining % i == 0:
            count += 1
        i += 1
    return count


