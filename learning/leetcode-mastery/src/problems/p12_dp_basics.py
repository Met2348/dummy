"""DP 基础/序列专题：爬楼梯 / 打家劫舍 / 最大子数组和 / 乘积最大子数组 /
最长递增子序列 / 零钱兑换 / 单词拆分。

通用思考顺序（比死记模板更重要，因为每题的 dp[i] 定义都不一样）：
① 定义 dp[i] 表示什么（一句话说清楚，含糊的定义会让递推关系写不对）
② 想清楚 dp[i] 和更小子问题（dp[i-1]、dp[i-2]……）之间的递推关系
③ 想清楚初始条件（dp[0] 或 dp[1] 该是多少，往往是递推链的"地基"）
④ 想清楚遍历顺序（从小到大？需要内外两层循环？完全背包和 01 背包的顺序不同）
"""
from __future__ import annotations

from bisect import bisect_left


# ── LC70 爬楼梯 ──────────────────────────────────────────────────────────
def climb_stairs(n: int) -> int:
    """
    【题意】爬 n 级楼梯，每次只能爬 1 级或 2 级，问一共有多少种不同的爬法。
    【思路】dp[i] 定义为"爬到第 i 级的方法数"。想清楚递推关系：爬到第 i 级的最后一步，
    要么是从第 i-1 级再爬 1 级上来，要么是从第 i-2 级再爬 2 级上来，这两种情况互不
    重叠、又覆盖了所有可能，所以 dp[i]=dp[i-1]+dp[i-2]（本质就是斐波那契数列的递推）。
    初始条件 dp[1]=1（只有一种走法：直接爬 1 级），dp[2]=2（爬两次 1 级，或一次
    2 级）。用两个滚动变量代替整个 dp 数组，因为每一步只依赖前两个状态。
    【复杂度】时间 O(n)；空间 O(1)（滚动变量）。
    【易错点】1) dp[0] 的含义容易和 dp[1]/dp[2] 的初始值搞混，边界定义不一致会导致
    整条递推链错位；2) n=1 或 n=2 这两个最小边界如果忘记提前特判，直接进入需要
    访问"dp[i-2]"的循环会越界或算错。
    """
    if n <= 2:
        return n
    prev2, prev1 = 1, 2  # prev2=dp[1], prev1=dp[2]
    for _ in range(3, n + 1):
        prev2, prev1 = prev1, prev1 + prev2
    return prev1


# ── LC198 打家劫舍 ───────────────────────────────────────────────────────
def rob(nums: list[int]) -> int:
    """
    【题意】一排房子，nums[i] 是第 i 间房子里的钱；相邻两间房子不能在同一晚都偷（会
    触发报警），求能偷到的最大金额。
    【思路】dp[i] 定义为"只考虑前 i 间房子时，能偷到的最大金额"。对第 i 间房子只有
    两种选择：不偷它，金额沿用 dp[i-1]；或者偷它，金额等于 dp[i-2]+nums[i]（偷了第
    i 间就绝不能偷相邻的第 i-1 间）。取两者中较大的一个就是递推关系。用两个滚动变量
    prev（对应 dp[i-1]）和 prev2（对应 dp[i-2]）代替整个数组，因为只依赖前两个状态。
    【复杂度】时间 O(n)；空间 O(1)。
    【易错点】1) 直觉性地写"隔一个偷一个"的贪心策略，在金额分布不均匀时会算错
    （比如 [2,1,1,2]，贪心隔一个偷会漏掉更优的组合），必须老实按 dp 递推；2) 空数组
    这种边界（这里的滚动写法天然返回 0，兼容良好，但如果改写成数组版 dp 就要记得
    特判 len(nums)==0）。
    """
    prev, prev2 = 0, 0
    for x in nums:
        prev, prev2 = max(prev, prev2 + x), prev
    return prev


# ── LC53 最大子数组和 ────────────────────────────────────────────────────
def max_sub_array(nums: list[int]) -> int:
    """
    【题意】给一个可能含负数的整数数组，找出和最大的连续子数组，返回这个最大和。
    【思路】Kadane 算法：dp[i] 定义为"以 nums[i] 结尾的连续子数组的最大和"。递推
    关系 dp[i]=max(nums[i], dp[i-1]+nums[i])——如果前面累积的和 dp[i-1] 是负数，
    那还不如从 nums[i] 自己重新开始（拖着负数包袱只会拖累后面的和）；如果 dp[i-1]
    是正数，接上它显然更优。最终答案是所有 dp[i] 里的最大值，而不是 dp[-1]（因为
    最优子数组不一定以数组末尾元素结尾）。
    【复杂度】时间 O(n)；空间 O(1)（用一个滚动变量 cur 代替 dp 数组）。
    【易错点】1) 只返回 dp 数组的最后一个值，漏掉最优子数组不在末尾的情况；
    2) 数组全是负数时（比如 [-3,-1,-2]）如果把 best 初始化成 0 会算错——子数组不能
    为空，必须至少包含一个元素，所以初始值应该是 nums[0] 而不是 0。
    """
    best = cur = nums[0]
    for x in nums[1:]:
        cur = max(x, cur + x)
        best = max(best, cur)
    return best


# ── LC152 乘积最大子数组 ─────────────────────────────────────────────────
def max_product(nums: list[int]) -> int:
    """
    【题意】给一个整数数组，找出乘积最大的连续子数组，返回这个最大乘积。
    【思路】和最大子数组和很像，但多一个陷阱：负数乘负数会变成正数，所以"以 i 结尾
    的最大乘积"不能只维护一个最大值，还要同时维护"以 i 结尾的最小乘积"（可能是一个
    绝对值很大的负数）——因为下一步如果乘上一个负数，当前的最小值反而可能翻转成新的
    最大值。所以要同时维护 cur_max 和 cur_min 两条 dp 链，每一步都从
    (x, cur_max*x, cur_min*x) 这三个候选值里重新算出新的 cur_max 和 cur_min。
    【复杂度】时间 O(n)；空间 O(1)。
    【易错点】1) 只维护最大值、不维护最小值，遇到"负负得正"的情况会漏掉真正的最大
    乘积；2) 更新时必须用同一步的三个候选值同时算出新的 cur_max 和 cur_min，不能先
    更新 cur_max 再拿"新的" cur_max 去计算 cur_min（那样用的就不是上一步的状态了）；
    3) 数组里出现 0 时容易担心"乘积链被打断"要不要特殊处理——其实这里的写法天然
    正确：乘 0 会让 cur_max/cur_min 都变成 0，相当于自动重新开始一条新链，不需要
    额外的 if 分支。
    """
    best = cur_max = cur_min = nums[0]
    for x in nums[1:]:
        candidates = (x, cur_max * x, cur_min * x)
        cur_max, cur_min = max(candidates), min(candidates)
        best = max(best, cur_max)
    return best


# ── LC300 最长递增子序列 ─────────────────────────────────────────────────
def length_of_lis(nums: list[int]) -> int:
    """
    【题意】给一个整数数组，找出其中最长严格递增子序列（不要求下标连续，但要求下标
    递增且对应值严格递增）的长度。
    【思路】O(n^2) 的朴素解法是 dp[i]="以 nums[i] 结尾的最长递增子序列长度"，枚举 i
    之前所有满足 nums[j]<nums[i] 的 j，取 dp[j]+1 的最大值——"每个位置都要回头扫一遍
    之前所有更小的数"正是可以被优化掉的重复劳动。优化到 O(n log n) 的关键 insight：
    维护一个数组 tails，tails[k] 表示"长度为 k+1 的递增子序列中，结尾数值最小的那
    个"。为什么只需要记录最小结尾：如果两个长度相同的递增子序列，结尾数值更小的那个
    未来能接上的候选数字显然更多（更容易被继续扩展），所以同一长度只有"最小结尾"
    值得留下。对每个新数 x：如果 x 比 tails 中所有值都大，说明它能接在目前最长的
    序列后面，直接 append，整体长度 +1；否则用 bisect_left 找到 tails 中第一个 >=x
    的位置，把那个位置替换成 x（因为 x 能让"长度为该位置+1 的子序列"的结尾变得更小，
    对未来的扩展更有利）。**必须强调：替换发生之后，tails 本身已经不是任何一个真实
    存在过的子序列**（它可能拼接了来自不同"候选链"的值），但 len(tails) 在任意时刻
    都精确等于"处理到当前位置为止"的最长递增子序列长度——这是这个技巧最反直觉、也
    最容易被误解的地方。
    【复杂度】时间 O(n log n)（n 个数，每个数二分查找耗时 O(log n)）；空间 O(n)
    （tails 数组，最坏情况长度等于原数组长度）。
    【易错点】1) 想当然地把 tails 数组当成"真实存在的最长递增子序列"去还原具体内容，
    这是错的——tails 只保证长度正确，不保证是一个货真价实的子序列；2) 用
    bisect_right 代替 bisect_left，会把"严格递增"错误地放宽成"非严格递增"（允许
    相等的数被当成可以接续），[7,7,7,7] 这个样例正是用来检验这一点：正确答案是 1，
    因为不允许两个相等的数组成"严格"递增序列。
    """
    tails: list[int] = []
    for x in nums:
        i = bisect_left(tails, x)
        if i == len(tails):
            tails.append(x)
        else:
            tails[i] = x
    return len(tails)


# ── LC322 零钱兑换 ───────────────────────────────────────────────────────
def coin_change(coins: list[int], amount: int) -> int:
    """
    【题意】给不同面值的硬币数组 coins（每种硬币视为有无限枚）和目标金额 amount，求
    凑出 amount 所需的最少硬币数；如果任何组合都凑不出 amount，返回 -1。
    【思路】dp[i] 定义为"凑出金额 i 所需的最少硬币数"。递推关系：对每个金额 i，枚举
    每种硬币面值 c（要求 c<=i），dp[i]=min(dp[i], dp[i-c]+1)——含义是"用了一枚面值
    c 的硬币，剩下的 i-c 金额最优要用多少枚硬币凑出，再加上这一枚"。这里是**完全
    背包**（每种硬币可以重复使用无限次），所以只需要外层按金额从小到大遍历、内层
    遍历硬币种类即可，完全不需要像 01 背包（每种物品只能用一次，例如后续"分割等和
    子集"那道题）那样刻意把金额维度倒序遍历——正因为硬币可以重复使用，dp[i-c] 本身
    就允许"复用同一种硬币算出来的状态"，顺序遍历金额天然支持这种复用。初始条件
    dp[0]=0（凑出金额 0 用 0 枚硬币），其余先设成一个"不可能"的哨兵值（比如
    amount+1，因为最优解不可能用超过 amount 枚面值为 1 的硬币）。
    【复杂度】时间 O(amount*len(coins))；空间 O(amount)。
    【易错点】1) 把"完全背包"误写成"01 背包"的遍历顺序（金额维度倒序遍历），会
    导致某些必须重复使用同一种硬币才能凑出的金额被漏掉，答案偏大甚至被误判为无解；
    2) 用 -1 或 0 当"不可达"的哨兵值容易和"合法答案"混淆（某个状态本来就该是接近
    0 或 -1 的值），应该用一个明确大于任何可能答案的数（如 amount+1）作哨兵，最后
    再统一转换成 -1。
    """
    unreachable = amount + 1
    dp = [0] + [unreachable] * amount
    for i in range(1, amount + 1):
        for c in coins:
            if c <= i:
                dp[i] = min(dp[i], dp[i - c] + 1)
    return dp[amount] if dp[amount] != unreachable else -1


# ── LC139 单词拆分 ───────────────────────────────────────────────────────
def word_break(s: str, word_dict: list[str]) -> bool:
    """
    【题意】给字符串 s 和一个单词词典 word_dict，判断 s 是否可以被拆分成一个或多个
    词典中单词的拼接（同一个单词允许被重复使用）。
    【思路】dp[i] 定义为"s 的前 i 个字符（即 s[:i]）能否被词典单词完全拆分"。递推
    关系：枚举一个分割点 j（0<=j<i），如果 dp[j] 为真（前 j 个字符已经能被完整
    拆分）且 s[j:i] 本身就是词典里的一个单词，那么 dp[i] 也为真。初始条件
    dp[0]=True（空字符串天然"已经被拆分完"，是整条递推链的地基）。为了让
    "s[j:i] in word_dict" 这个子串查找是 O(1) 而不是对列表做 O(len(word_dict))
    的线性扫描，需要先把 word_dict 转成 set。
    【复杂度】时间 O(n^2)（n=len(s)，双重循环枚举 i、j；子串切片和哈希查找各自摊销
    O(n)/O(1)）；空间 O(n)（dp 数组 + word set）。
    【易错点】1) 忘记把 word_dict 转成 set，每次子串查找退化成对列表的线性扫描，
    字符串较长或词典较大时会明显变慢；2) dp[0] 的初始值如果写反成 False，整条递推
    链从一开始就断掉，后面无论怎么拆分都会算成 False；3) 内层循环找到一个合法分割
    点之后应尽快跳出（这里用 break），否则虽然不会导致逻辑错误（dp[i] 已经是
    True 不会再被后续不满足条件的 j 覆盖回 False），但会做多余的无用比较。
    """
    words = set(word_dict)
    n = len(s)
    dp = [False] * (n + 1)
    dp[0] = True
    for i in range(1, n + 1):
        for j in range(i):
            if dp[j] and s[j:i] in words:
                dp[i] = True
                break
    return dp[n]


