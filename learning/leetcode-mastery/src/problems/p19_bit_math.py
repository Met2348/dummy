"""分类 19：位运算与数学 —— 只出现一次的数字 / 位1的个数 / 多数元素 / Pow(x, n) / 整数反转。

位运算题的通用套路是几个固定技巧：异或消消乘（利用"成对出现的数字异或为 0"）、
`n & (n-1)` 消去最低位的 1（常用来数二进制里 1 的个数或判断 2 的幂）、位掩码代替
集合（用一个整数的每一位表示一个布尔状态）。数学题的通用套路是想清楚"能不能把
O(n) 的暴力循环换成 O(log n) 的倍增或者一条封闭公式"——比如快速幂用"指数减半、
底数平方"把 O(n) 次乘法降到 O(log n) 次，思路上和二分查找的"每次砍掉一半搜索空间"
是同一类母题的不同应用。
"""
from __future__ import annotations


def single_number(nums: list[int]) -> int:
    """
    【题意】数组 nums 中除了一个数字只出现一次外，其余每个数字都恰好出现两次，
    找出那个只出现一次的数字。要求线性时间、且不使用额外空间（不能开哈希表计数）。

    【思路】如果用哈希表计数每个数字出现的次数，再挑出次数为 1 的那个，能做对但
    需要 O(n) 额外空间，不满足"不使用额外空间"的要求。异或运算恰好有三条性质：
    `a ^ a = 0`（任何数和自己异或得 0）、`a ^ 0 = a`（任何数和 0 异或还是自己）、
    异或满足交换律和结合律（异或的顺序不影响最终结果）。把这三条组合起来：把数组
    里所有数字依次异或到一起，成对出现的数字会因为 `a ^ a = 0` 而两两抵消为 0，
    抵消顺序不影响结果（结合律、交换律保证可以任意重排），最后剩下的就是"只出现
    一次的那个数字异或上若干个 0"，根据 `a ^ 0 = a` 结果就是它本身。全程只需要
    一个累加变量，天然是 O(1) 额外空间。

    【复杂度】时间 O(n)（一趟扫描）；空间 O(1)。

    【易错点】
    - 初始值必须是 0（而不是 nums[0] 再从下标 1 开始异或，虽然那样写也对，但更
      容易在边界上出错），因为 `0 ^ x = x`，从 0 开始异或不会引入多余的偏差。
    - 容易想复杂去构造哈希表计数，忽视了题目"不使用额外空间"的约束正是在提示
      异或这个技巧。
    - 这个技巧要求"恰好出现两次"这个前提；如果题目变成"其余数字出现三次"，
      异或就不再适用，需要换成按位统计每一位上 1 的个数对 3 取模的技巧。
    """
    result = 0
    for x in nums:
        result ^= x
    return result


def hamming_weight(n: int) -> int:
    """
    【题意】输入一个非负整数（视为无符号 32 位整数），返回它二进制表示中数字 1 的
    个数（俗称"汉明重量"）。

    【思路】最直接的想法是把 n 右移 32 次，每次看最低位是不是 1（`n & 1`），累计
    计数——这样也是对的，但每一位都要检查一遍，包括前导的一大串 0。更快的技巧是
    `n & (n - 1)`：`n - 1` 会把 n 的最低位 1 变成 0，同时把它右边原本的 0 全部
    变成 1；`n` 和 `n - 1` 做与运算，恰好会把 n 最低位的那个 1"消掉"，其余位不变。
    于是每执行一次 `n = n & (n - 1)`，n 里的 1 的个数就恰好减少一个，循环到 n
    变成 0 为止，循环执行的次数就是 1 的个数——循环次数只跟"1 的个数"有关，
    跳过了所有本来就是 0 的位，比逐位检查 32 次更快（当 1 很稀疏时优势明显）。

    【复杂度】时间 O(k)（k 是 n 中 1 的个数，最坏 O(32) 即 O(1)）；空间 O(1)。

    【易错点】
    - 这题在很多语言里需要注意"无符号"处理（因为负数的补码表示在有符号语言里
      可能导致死循环或错误的高位判断），Python 的整数是任意精度且题目保证输入
      非负，直接对 n 做位运算不会有符号位的问题，不需要额外掩码。
    - 循环条件要用 `while n:`（n 非零就继续），如果误写成固定循环 32 次也能做对，
      但没有利用"1 的个数决定循环次数"这一点，退化成了逐位检查。
    - 每次更新是 `n &= n - 1`，不要误写成 `n &= n >> 1`（那是完全不同的操作，
      混淆了"消去最低位的 1"和"右移一位"两种技巧）。
    """
    count = 0
    while n:
        n &= n - 1
        count += 1
    return count


def majority_element(nums: list[int]) -> int:
    """
    【题意】给一个长度为 n 的数组 nums，其中有一个元素出现的次数严格超过
    `n // 2`（题目保证这样的"多数元素"一定存在），找出它。

    【思路】如果用哈希表统计每个元素出现的次数再取最大值，能做对但要 O(n) 额外
    空间。Boyer-Moore 投票算法的核心洞察是：如果把"多数元素"记成 +1 票、把其余
    所有元素记成 -1 票，那么整个数组的票数总和一定是正的（因为多数元素出现次数
    超过一半，其余元素再怎么反对也抵消不完）。据此维护一个"候选人" candidate 和
    一个计数器 count：count 为 0 时，把当前元素设为新的候选人（count 重置为 1）；
    否则如果当前元素等于候选人，count 加 1，否则 count 减 1（表示"一票反对"，
    抵消候选人的一票支持）。因为真正的多数元素票数够多，即使每次都被其他不同的
    元素"抵消"一票，也不可能被完全抵消到最后——扫描结束时留下的候选人就是答案。

    【复杂度】时间 O(n)（一趟扫描）；空间 O(1)（只有 candidate 和 count 两个变量）。

    【易错点】
    - 这个算法本身不验证"候选人是否真的超过一半"——它依赖题目保证"多数元素一定
      存在"这个前提。如果题目没有这个保证，扫描结束后还需要再扫一遍数组统计
      candidate 的真实出现次数来验证。
    - `count == 0` 时才更换候选人，容易漏写这个判断而错误地"每次都更新候选人"，
      那样就退化成了记录最后一个元素，起不到抵消作用。
    - 初始 candidate 可以是任意值（反正第一个元素处理时 count 一定是 0，会被
      立刻覆盖），不需要特殊初始化逻辑。
    """
    candidate = None
    count = 0
    for x in nums:
        if count == 0:
            candidate = x
        count += 1 if x == candidate else -1
    return candidate


def my_pow(x: float, n: int) -> float:
    """
    【题意】实现 `pow(x, n)`，即计算 x 的 n 次幂（n 可以是负数，负数次幂等于
    正数次幂的倒数；不需要处理 x=0 且 n 为负这类未定义场景之外的输入）。

    【思路】最直接的想法是循环把 x 累乘 n 次，O(n) 次乘法——对于很大的 n（比如
    2^31）会超时。快速幂的核心洞察是：`x^n = (x^(n//2))^2`（n 为偶数时），
    `x^n = x * (x^(n//2))^2`（n 为奇数时，多出来的一次单独乘）。也就是说每次可以
    把指数减半、底数平方，只需要 O(log n) 次乘法就能算完——这和二分查找"每次
    砍掉一半搜索空间"是同一种"倍增/折半"思想的不同应用：二分查找是折半地缩小
    "答案可能存在的范围"，快速幂是折半地缩小"还需要乘多少次"。实现时用迭代版
    更省栈空间：把 n 看成二进制展开，每一位为 1 就把当前的 base（不断自我平方）
    累乘进结果里，然后 base 自我平方、n 右移一位，直到 n 变成 0。n 为负数时，
    先令 `x = 1/x`、`n = -n`，转化成正数次幂的情形复用同一套逻辑。

    【复杂度】时间 O(log n)（指数每次减半/右移一位）；空间 O(1)（迭代版本；
    如果写成递归版本则是 O(log n) 的调用栈空间）。

    【易错点】
    - n 为负数时容易忘记转换，或者转换后忘记同时把 x 取倒数——只把 n 取相反数
      而不处理 x，会算出 `x^(-n)` 而不是 `1/x^(-n)`。
    - 迭代版本里"指数右移、底数平方"的顺序不能搞反：应该先判断当前最低位是否
      为 1（决定要不要把 base 乘进 result），再让 base 平方、n 右移，供下一位
      使用——顺序反了会导致某一位用错了底数的幂次。
    - 初始 `result` 必须是 1.0（乘法单位元），如果误初始化成 x 或 0.0 会导致
      结果整体错误或恒为 0。
    """
    if n < 0:
        x = 1 / x
        n = -n
    result = 1.0
    base = x
    while n > 0:
        if n & 1:
            result *= base
        base *= base
        n >>= 1
    return result


def reverse_integer(x: int) -> int:
    """
    【题意】给一个 32 位有符号整数 x，将其各位数字反转（比如 123 → 321，
    -123 → -321，120 → 21，末尾的 0 反转后自然消失）。如果反转后的结果超出
    32 位有符号整数的范围 `[-2^31, 2^31-1]`，返回 0。

    【思路】反转数字最自然的做法是不断地对 10 取模拿到最低位数字、再对 10 整除
    去掉这一位，把拿到的数字依次拼到结果的末尾（`rev = rev * 10 + digit`）——
    这本质上是"每次剥离一位、累加到反转结果里"的倍增/累积过程,类似于前缀和
    "边扫边累加"的思路,只是这里是十进制位而不是数组元素。先记录符号、对 x 取
    绝对值处理数字部分，反转完成后再把符号乘回去。最后必须检查结果是否超出
    32 位有符号整数的范围，超出就返回 0（这是本题区别于"单纯反转数字"的关键
    额外要求）。

    【复杂度】时间 O(log x)（x 有多少位就循环多少次）；空间 O(1)。

    【易错点】
    - 溢出检查必须在反转完成之后做，且要用 Python 的大整数放心地算出真实的
      反转结果再比较范围（不能像 Java/C++ 那样在过程中提前用固定 32 位类型
      判断溢出，Python 整数不会自动截断,必须显式检查）。
    - 负数取绝对值处理、结束时要把符号乘回去，容易忘记这一步导致负数输入
      被错误地当成正数处理。
    - 范围边界是闭区间 `[-2**31, 2**31-1]`（即 `[-2147483648, 2147483647]`），
      等于边界值本身是合法的，只有严格超出这个区间才返回 0，容易把 `<=`/`>=`
      的方向写反。
    """
    sign = -1 if x < 0 else 1
    x = abs(x)
    rev = 0
    while x != 0:
        digit = x % 10
        x //= 10
        rev = rev * 10 + digit
    rev *= sign
    if rev < -(2 ** 31) or rev > 2 ** 31 - 1:
        return 0
    return rev


def _self_test() -> None:
    assert single_number([2, 2, 1]) == 1
    assert single_number([4, 1, 2, 1, 2]) == 4

    assert hamming_weight(0b1011) == 3
    assert hamming_weight(0b10000000) == 1
    assert hamming_weight(4294967293) == 31

    assert majority_element([3, 2, 3]) == 3
    assert majority_element([2, 2, 1, 1, 1, 2, 2]) == 2

    assert abs(my_pow(2.0, 10) - 1024.0) < 1e-6
    assert abs(my_pow(2.1, 3) - 9.261) < 1e-6
    assert abs(my_pow(2.0, -2) - 0.25) < 1e-6

    assert reverse_integer(123) == 321
    assert reverse_integer(-123) == -321
    assert reverse_integer(120) == 21
    assert reverse_integer(1534236469) == 0

    print("[PASS] p19_bit_math: 只出现一次的数字 / 位1的个数 / 多数元素 / Pow(x,n) / 整数反转 全部正确")


if __name__ == "__main__":
    _self_test()
