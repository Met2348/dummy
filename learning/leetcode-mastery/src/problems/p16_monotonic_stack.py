"""分类 16：单调栈 / 单调队列 —— 下一个更大元素 I / 每日温度 / 柱状图中最大的矩形 / 滑动窗口最大值。

四道题共享同一个核心动作：维护一个"单调"的栈或双端队列，让每个元素最多入栈/入队、
出栈/出队各一次，从而把"对每个位置都要往左/右扫一遍找目标元素"的 O(n^2) 暴力，
压缩成整体 O(n)。差别只在于：找的是"更大"还是"更小"、方向是"从左找右"还是
"两边都要看"、以及要不要支持"窗口滑动时移除过期元素"（这一点决定了用栈还是双端队列）。
"""
from __future__ import annotations

from collections import deque


def next_greater_element(nums1: list[int], nums2: list[int]) -> list[int]:
    """
    【题意】nums1 是 nums2 的子集（元素各不相同）。对 nums1 里的每个数 x，
    在 nums2 中找到 x 出现的位置，返回 x 右边第一个比 x 大的数；不存在则记 -1。
    最终按 nums1 的顺序返回这些答案组成的列表。

    【思路】如果对 nums1 里每个数都去 nums2 里从它的位置往右线性扫一遍找"下一个更大"，
    就是 O(n*m) 的暴力，且同一个 nums2 会被扫很多遍——重复劳动。更好的做法是先只对
    nums2 做一遍"每个数的下一个更大元素是谁"，一次性算出一张查找表，nums1 直接查表。
    算这张表的标准手法是单调栈：从左到右扫 nums2，栈里始终保持"从栈底到栈顶递减"的
    数值；每来一个新数 x，只要它比栈顶大，说明栈顶那个数"下一个更大的元素"就是 x
    （因为 x 是从栈顶数出发、往右走遇到的第一个更大的数），于是弹栈并把这对映射记下来，
    直到栈顶不再小于 x 或栈空，再把 x 压入栈。栈里始终不递减保证了：每个数只会在
    "遇到比自己大的数"时被弹出恰好一次，不会被重复处理。
    最后没能被弹出的栈内元素，说明它们右边不存在更大的数，答案是 -1。

    【复杂度】时间 O(n + m)（n=len(nums2) 单调栈一遍扫描，m=len(nums1) 查表）；
    空间 O(n)（哈希表 + 栈）。

    【易错点】
    - 弹栈判断条件写反：应该是"栈顶元素 < 当前元素"才弹（找的是"更大"），
      写成 `<=` 或 `>` 都会得到错误的映射。
    - 忘记这题的结论只对 nums2 生效，nums1 只是"查表"，不需要重新跑单调栈。
    - 栈里存的应该是数值本身（因为 nums2 元素各不相同，值可以唯一定位），
      如果换成有重复元素的场景就必须存下标，这里为了直观直接存值。
    """
    next_greater: dict[int, int] = {}
    stack: list[int] = []  # 栈内数值从底到顶递减
    for x in nums2:
        while stack and stack[-1] < x:
            smaller = stack.pop()
            next_greater[smaller] = x
        stack.append(x)
    # 栈里剩下的元素右边没有更大的数
    for remaining in stack:
        next_greater[remaining] = -1
    return [next_greater[x] for x in nums1]


def daily_temperatures(temperatures: list[int]) -> list[int]:
    """
    【题意】给一个每日气温列表，对每一天，求"还要等多少天才会出现比今天更暖和的气温"，
    等不到则记 0。返回与输入等长的天数列表。

    【思路】暴力解是对每一天都往右扫，找到第一个更高的温度为止——最坏情况
    （温度递减数列）是 O(n^2)。注意到低温之间会互相"抢答案"：一旦出现一个更高的
    温度，它会同时解决"它左边所有还没找到答案、且比它低的日子"的疑问。这提示我们
    应该"延迟结算"——把还没找到答案的日子先存起来，等右边出现更高温度时一次性
    把能结算的都结算掉。用栈存"下标"（下标能同时换算出"等了几天"和回填 res 数组），
    维护栈内下标对应的温度从底到顶递减：新一天温度比栈顶高，就弹栈、用
    `当前下标 - 栈顶下标` 算出等待天数填入结果，重复直到栈顶温度不低于当前温度，
    再把当前下标压栈。因为每个下标入栈一次、最多出栈一次，整体是 O(n)。

    【复杂度】时间 O(n)，空间 O(n)（最坏情况栈内存下所有下标，如输入是严格递减序列）。

    【易错点】
    - 栈里要存下标而不是温度值本身——因为答案要的是"等待天数"（下标之差），
      光有温度值算不出天数。
    - `while` 循环里一次可能连续弹出多个栈顶（比如温度骤升，一次性解决好几天的疑问），
      不能只判断一次就 `break`，必须循环弹到栈顶不再小于当前温度为止。
    - 初始化结果数组要用 0 填充（表示"等不到"的默认值），而不是 -1 或空。
    """
    res = [0] * len(temperatures)
    stack: list[int] = []  # 存下标，对应温度从底到顶递减
    for i, t in enumerate(temperatures):
        while stack and temperatures[stack[-1]] < t:
            j = stack.pop()
            res[j] = i - j
        stack.append(i)
    return res


def largest_rectangle_area(heights: list[int]) -> int:
    """
    【题意】给一排柱子的高度数组，柱子宽度都是 1，求"能在这些柱子里画出的最大矩形面积"
    （矩形的高必须等于某根柱子的高度，宽度是它能向左右延伸到的范围）。

    【思路】如果对每根柱子都暴力地向左、向右扩展直到遇到比它矮的柱子为止，是
    O(n^2)（每根柱子都要扫一遍两边）。关键观察：以柱子 i 为高度的矩形，它能扩展到的
    左边界是"左边第一个比它矮的柱子"，右边界是"右边第一个比它矮的柱子"——这正是
    单调栈的经典模式（找左右两边"第一个更小的元素"）。维护一个下标栈，栈内对应的
    高度从底到顶递增：新柱子如果比栈顶矮，说明栈顶柱子的"右边界"就在这里被确定了
    （右边第一个更矮的就是当前柱子），于是弹出栈顶，此时栈顶的新顶就是"左边第一个
    比刚才弹出的柱子矮的下标"，宽度 = 当前下标 - 新栈顶下标 - 1，用这个宽度乘被弹出的
    高度更新答案。为了让最后栈里剩下的（一直递增到结尾的）柱子也能被结算，在数组末尾
    补一个高度 0 的哨兵柱子，保证所有柱子最终都会被弹出结算一次。

    【复杂度】时间 O(n)（每根柱子入栈出栈各一次，末尾哨兵也算 O(1) 次额外操作）；
    空间 O(n)。

    【易错点】
    - 宽度公式最容易算错：弹出栈顶 top 后，若栈为空，说明 top 左边没有更矮的柱子，
      宽度就是"从 0 到当前下标-1"共 `i` 个；若栈非空，宽度是
      `i - stack[-1] - 1`（新栈顶和当前柱子都不算在矩形内，只是边界）。
    - 忘记在末尾补哨兵 0，会导致"高度一路递增到结尾"的柱子永远不会被弹栈结算，
      漏算这部分面积。
    - 栈内应维护高度"递增"（找的是更小的边界），和每日温度的"递减栈"方向相反，
      两者容易搞混。
    """
    stack: list[int] = []  # 存下标，对应高度从底到顶递增
    best = 0
    for i, h in enumerate(heights + [0]):  # 末尾补哨兵，保证栈最终清空结算
        while stack and heights[stack[-1]] >= h:
            top = stack.pop()
            height = heights[top]
            width = i if not stack else i - stack[-1] - 1
            best = max(best, height * width)
        stack.append(i)
    return best


def max_sliding_window(nums: list[int], k: int) -> list[int]:
    """
    【题意】给数组 nums 和窗口大小 k，窗口从左到右每次滑动一格，求每个窗口位置里的
    最大值组成的列表。

    【思路】暴力解是每滑一格就把窗口内 k 个数重新扫一遍求最大值，O(n*k)。注意到
    窗口滑动时只增加了一个新元素、丢弃了一个旧元素——真正有用的信息是"当前窗口内，
    从大到小排开、且各自都还'有可能成为未来某个窗口最大值'的候选序列"。这里和单调栈
    的关键区别是：栈只能从一端进出（LIFO），但这题需要"窗口滑出去的旧下标"从队首移除，
    同时"新元素"从队尾进来、还要挤掉队尾比它小的候选——这是两端都要操作，所以必须用
    双端队列（`collections.deque`），维护队列中下标对应的数值从队首到队尾递减：
    每来一个新下标 i，先把队尾所有比 nums[i] 小的候选弹掉（它们不可能再成为最大值，
    因为 nums[i] 比它们大且比它们活得久），再把 i 追加到队尾；然后检查队首下标是否
    已经滑出窗口（`队首下标 <= i - k`），滑出则弹出队首；当 i >= k-1（窗口第一次填满）
    之后，队首就是当前窗口的最大值下标。

    【复杂度】时间 O(n)（每个下标入队出队各一次）；空间 O(k)（队列里最多留 k 个候选）。

    【易错点】
    - 判断队首"过期"的条件是 `dq[0] <= i - k`（下标已经不在窗口 `[i-k+1, i]` 内），
      写成 `< i - k` 或者忘记检查都会导致窗口最大值算错。
    - 队列里要存下标而不是数值——既要能判断"是否过期"（需要下标），又要能取值。
    - 弹队尾的 `while` 条件要用 `<=`还是 `<` 取决于是否要保留相等值：这里用
      `nums[dq[-1]] < nums[i]` 即可（相等的旧下标仍可能在新下标过期前继续有效，
      但由于新下标更靠右、值又不比它小，其实留哪个不影响最大值结果，保留更靠右的
      写法更简单不容易错）。
    - 只有当 `i >= k - 1` 时才开始记录答案，窗口还没填满之前不能提前输出。
    """
    dq: deque[int] = deque()  # 存下标，对应数值从队首到队尾递减
    res: list[int] = []
    for i, x in enumerate(nums):
        while dq and nums[dq[-1]] < x:
            dq.pop()
        dq.append(i)
        if dq[0] <= i - k:  # 队首已经滑出窗口
            dq.popleft()
        if i >= k - 1:
            res.append(nums[dq[0]])
    return res


def _self_test() -> None:
    assert next_greater_element([4, 1, 2], [1, 3, 4, 2]) == [-1, 3, -1]
    assert next_greater_element([2, 4], [1, 2, 3, 4]) == [3, -1]

    assert daily_temperatures([73, 74, 75, 71, 69, 72, 76, 73]) == [1, 1, 4, 2, 1, 1, 0, 0]

    assert largest_rectangle_area([2, 1, 5, 6, 2, 3]) == 10
    assert largest_rectangle_area([2, 4]) == 4

    assert max_sliding_window([1, 3, -1, -3, 5, 3, 6, 7], 3) == [3, 3, 5, 5, 6, 7]
    assert max_sliding_window([1], 1) == [1]

    print("[PASS] p16_monotonic_stack: 下一个更大元素I / 每日温度 / 柱状图最大矩形 / 滑动窗口最大值 全部正确")


if __name__ == "__main__":
    _self_test()
