# ── LC174 地下城游戏(Hard) ───────────────────────────────────────────────
def calculate_minimum_hp(dungeon: list[list[int]]) -> int:
    """
    【题意】骑士从左上角出发，只能向右或向下走，最终要走到右下角救出公主。每个格子的值可能
    是负数（恶魔，扣血）、正数（魔法球，加血）或 0。骑士的生命值任何时候降到 0 或以下就会
    立刻死亡。求骑士出发时最少需要多少初始生命值，才能保证全程存活走到终点。
    【思路】这题"正向 DP 会失败"：如果从左上角正向定义 dp[i][j]="走到 (i,j) 时剩余的最大
    生命值"，会发现"当前剩余生命值最大"并不等价于"最优选择"——因为后续路径可能刚好需要
    在某个特定的生命值水位上才能兼顾"不死"和"最省初始血量"，正向最大化当前生命值不能保证
    全局最优（这是"局部贪心最大化"在这道题里失效的经典反例）。正确做法是**反向 DP**：从
    终点往起点推，dp[i][j] 定义为"从 (i,j) 出发走到终点，至少需要多少初始生命值才能全程
    存活"（这是一个"至少需要多少"的下界要求，而不是"剩余多少"，视角彻底反过来）。转移：
    dp[i][j] = max(1, min(dp[i+1][j], dp[i][j+1]) - dungeon[i][j])——"至少需要的血量"等于
    "去下一步中较省血的方向所需的血量"减去"当前格子能加的血/需要扣的血"，但任何时候这个
    值不能低于 1（哪怕当前格子是超大的正数魔法球，把血量需求"倒扣成负数或 0"也没有意义，
    骑士的生命值必须始终 >=1 才算存活，dp 值同样必须 >=1）。base case：终点右下角格子
    dp[m][n-1]=dp[m-1][n]=1（超出边界的两个"虚拟"格子，代表"从终点出发不需要再走，最低
    血量要求是 1"），其余格子先设为一个足够大的哨兵值。
    【复杂度】时间 O(m·n)，空间 O(m·n)（可以用一维滚动数组压缩到 O(n)，这里为清晰起见
    保留二维）。
    【易错点】1) 最容易犯的错误是尝试正向 DP（从起点往终点推、维护"当前最大剩余血量"），
    这个思路在直觉上很自然但是错的——不同路径在"剩余血量最大"和"全局所需初始血量最小"
    之间并不等价，必须反向 DP 才能把"未来至少需要多少"这个约束正确地从终点传播回起点；
    2) "max(need, 1)" 这个下限每一步都要做，而不是只在最后对 dp[0][0] 做一次——如果中间
    某一步的血量需求被算成 0 或负数而不加下限保护，会污染后续更靠近起点的格子的计算；
    3) 边界初始化 dp[m][n-1]=dp[m-1][n]=1 分别对应"终点右侧的虚拟格子"和"终点下方的
    虚拟格子"，这是让终点自身可以统一套用同一条转移公式的技巧，如果直接对终点格子单独
    特判（不设置这两个虚拟哨兵）容易漏掉终点格子本身的 dungeon[m-1][n-1] 扣血/加血。
    """
    m, n = len(dungeon), len(dungeon[0])
    INF = float("inf")
    dp = [[INF] * (n + 1) for _ in range(m + 1)]
    dp[m][n - 1] = dp[m - 1][n] = 1
    for i in range(m - 1, -1, -1):
        for j in range(n - 1, -1, -1):
            need = min(dp[i + 1][j], dp[i][j + 1]) - dungeon[i][j]
            dp[i][j] = max(need, 1)
    return dp[0][0]


# ── LC1691 堆叠长方体的最大高度(Hard) ─────────────────────────────────────
def max_height(cuboids: list[list[int]]) -> int:
    """
    【题意】给定若干长方体 cuboids[i]=[width,length,height]，每个长方体可以任意旋转 90 度
    （即三条边可以互换哪条当宽、长、高），要求 i 能叠在 j 上面必须满足三条边两两"不超过"
    （宽<=宽、长<=长、高<=高），求选一部分长方体叠放能达到的最大总高度。
    【思路】允许旋转是这题的关键预处理：**对每个长方体自身的三条边先排序**（不妨设排序后
    满足 a<=b<=c），可以证明如果一种堆叠方案合法，把每个长方体都换成"排序后的姿态摆放
    （最短边、次短边、最长边分别对应到同一个坐标轴上）"依然合法，且这样统一姿态之后，
    "i 能叠在 j 上"这个三维偏序关系简化成了"排序后的三元组逐分量都不超过"——这就把问题
    转化成了三维版本的最长递增子序列（LIS）的"链"问题：先把所有排序后的三元组按字典序
    整体排序，然后 dp[i]="以第 i 个长方体（排序后）为最顶层能达到的最大总高度"，枚举
    j<i 且 cuboids[j] 三个维度都 <= cuboids[i] 时，dp[i]=max(dp[i], dp[j]+height[i])。
    最终答案是所有 dp[i] 的最大值。
    【复杂度】时间 O(n^2)（对每个长方体枚举之前所有的长方体，是朴素 LIS 的写法；因为这里
    是三维偏序，不能像一维 LIS 那样用二分优化到 O(n log n)），空间 O(n)。
    【易错点】1) "每个长方体先把自己的三条边排序"这一步是本题最容易被忽略的预处理——
    如果不做这一步，直接用原始的 [width,length,height] 顺序比较，会漏掉大量"旋转后其实
    能叠放"的合法组合，导致答案偏小；2) 排序之后，"整体排序长方体列表"用的是对三元组做
    字典序排序（Python 里 sorted() 对元组默认就是字典序），这一步只是为了让"更小的长方体
    排在前面"从而使 dp[j] (j<i) 都是"可能的候选底座"，本身不是充分条件，仍然需要在内层
    循环里显式校验三个维度都 <= 才能转移；3) 容易混淆"这道题禁止用二分优化"和"LC300
    普通 LIS 可以用二分优化到 O(n log n)"的边界——本题的偏序关系是三维的，不满足普通
    LIS 二分优化所依赖的"严格线性序"结构，只能退回到 O(n^2) 的朴素写法。
    """
    boxes = sorted(tuple(sorted(c)) for c in cuboids)
    n = len(boxes)
    dp = [box[2] for box in boxes]
    for i in range(n):
        for j in range(i):
            if boxes[j][0] <= boxes[i][0] and boxes[j][1] <= boxes[i][1] and boxes[j][2] <= boxes[i][2]:
                dp[i] = max(dp[i], dp[j] + boxes[i][2])
    return max(dp)


# ── LC1770 执行乘法运算的最大分数(Hard) ──────────────────────────────────
def maximum_score(nums: list[int], multipliers: list[int]) -> int:
    """
    【题意】给定数组 nums（长度 n）和 multipliers（长度 m，m<=n），依次执行 m 次操作：第 i
    次操作从 nums 的**头部或尾部**取一个数 x，得分增加 multipliers[i]*x，并把 x 从 nums 中
    移除。求 m 次操作后能获得的最大总分。
    【思路】关键观察：进行到第 i 次操作时，如果已经从左边取了 left 个数，那么从右边取的
    个数一定是 i-left（两者之和恰好是已经完成的操作数），这意味着"剩余 nums 的右边界"可以
    由 (i, left) 唯一确定，不需要单独作为状态维度——状态可以压缩成 dp[i][left]，只有两个
    自由维度而不是三个。dp[i][left]="已经完成 i 次操作、其中从左边取了 left 个，能获得的
    最大得分"。转移：第 i+1 次操作要么继续从左取（用 nums[left]，转移到
    dp[i+1][left+1] = multipliers[i]*nums[left] + dp[i][left]），要么从右取（用
    nums[n-1-(i-left)]，转移到 dp[i+1][left] = multipliers[i]*nums[right] + dp[i][left]）。
    实现上可以按"操作序号 i 从大到小逆推"或"从小到大正推"，本实现采用逆推（从最后一次
    操作往前递推，dp[i][left] 表示"已经执行了 i 次操作、其中 left 次取左"这个状态下，
    **接下来还能从剩余的 m-i 次操作里获得的最大额外得分**），这样最终答案就是 dp[0][0]。
    【复杂度】时间 O(m^2)（i 和 left 都在 [0,m] 范围内，是二维状态；虽然 nums 长度可以
    远大于 m，但只有 m 次操作，真正需要决策的维度只有 O(m^2) 个），空间 O(m)（一维滚动
    数组，每次只保留"当前操作次数"这一层）。
    【易错点】1) 容易忽略"从右边取的位置可以由 i 和 left 推出来"这个关键性质，误以为需要
    三维状态 (left, right, i)，从而写出 O(n^2) 甚至更差的状态空间，在 nums 很长时会超时或
    超内存——本质上"进行了多少次操作"和"其中多少次取左"就足以唯一确定右边界；2) 右边界
    公式是 n-1-(i-left)，其中 i 是"已经执行的操作总数"，如果把 i 和 left 的角色搞反（比如
    写成 n-1-(left-i)）会导致下标越界或访问到错误的元素；3) 这道题如果直接写记忆化搜索
    （dfs(i, left)）思路上更直观，但要注意函数式递归在 Python 里默认递归深度对 m 较大的
    情况可能触及递归上限，本实现改用自底向上的迭代式滚动数组更稳妥。
    """
    n, m = len(nums), len(multipliers)
    dp = [0] * (m + 1)
    for i in range(m - 1, -1, -1):
        new_dp = [0] * (m + 1)
        for left in range(i, -1, -1):
            right = n - 1 - (i - left)
            take_left = multipliers[i] * nums[left] + dp[left + 1]
            take_right = multipliers[i] * nums[right] + dp[left]
            new_dp[left] = max(take_left, take_right)
        dp = new_dp
    return dp[0]


# ── LC730 统计不同回文子序列(Hard) ────────────────────────────────────────
def count_palindromic_subsequences(s: str) -> int:
    """
    【题意】给定字符串 s（只含 'a'/'b'/'c'/'d' 四种字符），求 s 中**不同**的非空回文子序列
    个数（子序列不要求连续；两个子序列只要字符序列本身相同就算重复，不看具体来自哪些下标），
    结果对 1e9+7 取模。和「最长回文子序列」（LC516，只求长度）不同，这里既要计数又要精确
    去重，是区间 DP 里公认最棘手的变体之一。
    【思路】dp[i][j] 定义为"s[i:j+1] 中不同回文子序列的个数"。分两种情况：① s[i]!=s[j]，
    两端字符不同不可能同时出现在同一个回文的最外层，此时用容斥原理 dp[i][j] = dp[i+1][j]
    + dp[i][j-1] - dp[i+1][j-1]（"以左端开头能构成的" 加 "以右端结尾能构成的"，减去被
    重复计算了两次的"两端都不用"的部分）；② s[i]==s[j]（设这个公共字符为 c），此时能构成
    的新回文分三类：单独的 "c"、"cc"（两端字符各用一次拼在一起），以及"c + 中间的回文
    子序列 + c"（中间任取内部区间的一个回文子序列，两端套上 c）。这里的难点在于"中间区间
    的回文子序列"如果直接用 dp[i+1][j-1] 会产生**重复计数**：如果内部区间的最左端和最右端
    也恰好是字符 c，那么"c+内部回文+c"这个新回文串，可能可以由内部区间不同的"c 的位置
    选择"构造出完全相同的字符序列，造成重复。standard 做法是在内部区间 [i+1,j-1] 里找到
    "第一个等于 c 的位置 lo"和"最后一个等于 c 的位置 hi"：如果内部完全没有字符 c
    （lo>hi），说明不存在重复风险，dp[i][j]=2*dp[i+1][j-1]+2（c、cc 各算一次，中间套两层
    c 也不会有重复，所以是 2 倍再加 2）；如果 lo==hi（内部恰好只有一个 c），
    dp[i][j]=2*dp[i+1][j-1]+1（少加一个 1，因为"cc"这个双字符回文和"c+这唯一一个内部
    c+c"这个三字符回文在下面这种记法下会被隐式合并，需要通过减少 1 来修正）；如果
    lo<hi（内部至少有两个 c），dp[i][j]=2*dp[i+1][j-1]-dp[lo+1][hi-1]（内部以 c 开头和
    结尾的这部分回文子序列，在"整体乘 2"时被重复统计了一次，需要减去这部分的重复量
    dp[lo+1][hi-1]）。
    【复杂度】时间 O(n^2)（区间 DP 的状态数是 O(n^2)，每个状态转移里定位 lo/hi 是
    O(n)，可以预处理每个位置每个字符的"下一个/上一个出现位置"降到 O(1)，本实现为清晰
    起见保留 O(n) 的线性扫描定位），空间 O(n^2)。
    【易错点】1) s[i]==s[j] 时的三种子情况（lo>hi / lo==hi / lo<hi）对应的公式系数
    （+2 / +1 / -dp[lo+1][hi-1]）极易记混或漏写，这是这道题被公认为"回文计数类 DP 里
    最容易出错"的核心原因，必须对着小例子（比如 "bccb"）手动验算确认；2) 取模运算里
    "2*dp[i+1][j-1]-dp[lo+1][hi-1]" 这一步在 Python 里天然不会出现负数问题（Python 的
    % 对正模数总是返回非负结果），但如果照搬到其他语言（如 C++/Java），减法之后必须手动
    加一次模数再取模，否则会得到负数结果；3) 遍历顺序必须按区间长度从小到大（这是所有
    区间 DP 的通用要求），如果直接按 i、j 自然顺序遍历，dp[i+1][j-1] 这类"更短区间"的
    依赖会读到尚未计算的默认值 0。
    """
    MOD = 10**9 + 7
    n = len(s)
    dp = [[0] * n for _ in range(n)]
    for i in range(n):
        dp[i][i] = 1
    for length in range(2, n + 1):
        for i in range(n - length + 1):
            j = i + length - 1
            if s[i] == s[j]:
                lo, hi = i + 1, j - 1
                while lo <= hi and s[lo] != s[i]:
                    lo += 1
                while lo <= hi and s[hi] != s[i]:
                    hi -= 1
                if lo > hi:
                    dp[i][j] = 2 * dp[i + 1][j - 1] + 2
                elif lo == hi:
                    dp[i][j] = 2 * dp[i + 1][j - 1] + 1
                else:
                    dp[i][j] = 2 * dp[i + 1][j - 1] - dp[lo + 1][hi - 1]
            else:
                dp[i][j] = dp[i + 1][j] + dp[i][j - 1] - dp[i + 1][j - 1]
            dp[i][j] %= MOD
    return dp[0][n - 1] % MOD


# ── LC546 移除盒子(Hard) ─────────────────────────────────────────────────
def remove_boxes(boxes: list[int]) -> int:
    """
    【题意】给定一排有颜色的盒子，每次可以选择一段连续且颜色相同的盒子（长度 k>=1）整体
    移除，获得 k*k 分。可以按任意顺序反复移除（移除后原本不相邻的盒子会连接起来，如果颜色
    相同就可能在未来构成新的连续段），求能获得的最大总分。这是**公认最难的 DP 之一**：
    朴素的区间 DP 状态 dp[l][r] 完全无法表达"未来可能因为中间部分被先移除，而让原本不相邻
    的两段同色盒子拼接在一起"这种效应。
    【思路】这题的核心突破口是**在区间 DP 的状态里额外加一维"附加计数"**：dp(l, r, k) 表示
    "考虑区间 [l,r] 的盒子，并且在 boxes[r] 右侧还有额外 k 个和 boxes[r] 颜色相同、将来可以
    合并一起移除的盒子"，返回这种状态下能获得的最大分数。为什么必须加这一维：如果只用
    dp[l][r] 表示"区间 [l,r] 的最大得分"，一旦 boxes[r] 这个颜色在 [l,r] 之外还有"待定的
    同色搭子"（比如上一层递归里已经决定"先把中间一段异色盒子清空，让两侧同色盒子拼接"），
    这个"待定"信息就无处安放——加上第三维 k，就是把"还没有真正物理相邻、但注定将来会拼在
    一起的同色盒子数量"显式地记录进状态里。转移分两步：① 预处理：如果 boxes[l] 到
    boxes[l+t] 是连续同色（一开始就物理相邻的同色段），可以直接把 l 右移、k 增加，合并
    成一个"逻辑上的起点"（这是纯粹的常数优化，不影响状态定义本身）；② 核心决策——枚举
    "把 boxes[l]（连同它右侧那 k 个待定同色盒子）留到最后，和区间内某个位置 m 处颜色相同的
    boxes[m] 拼在一起"：先把 [l+1, m-1] 这一段（颜色和 boxes[l] 不同或至少不参与这次拼接）
    单独处理消耗掉（dp(l+1, m-1, 0)，这里 k 重置为 0，因为这一段是独立被消耗的，不携带任何
    "延后合并"的信息），再把 boxes[l..l] 的这 k+1 个同色盒子"过继"给 boxes[m]，让
    dp(m, r, k+1) 处理"boxes[m] 到 boxes[r] 这一段，且 boxes[m] 左边还挂着 k+1 个同色盒子"
    这个新状态；两部分相加，在所有满足 boxes[m]==boxes[l] 的 m 之间取最大值。此外还有一种
    "干脆现在就把 boxes[l] 连同它的 k 个同色搭子一起清空"的选项：得分 (k+1)^2 加上
    dp(l+1, r, 0)。
    【复杂度】时间 O(n^4)（状态数是 O(n^3)——l、r、k 各一维，其中 k 的取值范围是 O(n)；
    转移里还要枚举 m，是额外的 O(n)），空间 O(n^3)（记忆化表；用字典存储比预分配三维数组
    更省空间，因为很多 (l,r,k) 组合根本不会被访问到）。
    【易错点】1) **这道题最容易犯的错误就是不给状态加"k"这一维**，只用 dp[l][r] 会让"中间
    先清空、两侧同色拼接"这种关键操作完全无法表达，算出的答案会系统性偏低；2) 决策②里
    "把 [l+1,m-1] 消耗掉"时，这一段的 k 必须重置为 0（它是一段独立被处理掉的区间，不继承
    外层的"待合并计数"），如果错误地把外层的 k 传进去，会让这一段的计分逻辑产生不该有的
    额外加成；3) "预处理合并连续同色前缀"这一步（while l<r and boxes[l+1]==boxes[l]:
    l+=1; k+=1）只是性能优化，即使不做这一步，纯粹依赖决策②本身也能算出正确答案，只是
    状态数会成倍增加、更容易超时——这一步很容易被误解成"影响正确性的必要步骤"，实际上它
    只是等价状态的合并去重，不加也不会算错，只会变慢。
    """
    n = len(boxes)

    @lru_cache(maxsize=None)
    def dp(l: int, r: int, k: int) -> int:
        if l > r:
            return 0
        ll, kk = l, k
        while ll < r and boxes[ll + 1] == boxes[ll]:
            ll += 1
            kk += 1
        best = (kk + 1) * (kk + 1) + dp(ll + 1, r, 0)
        for m in range(ll + 1, r + 1):
            if boxes[m] == boxes[ll]:
                best = max(best, dp(ll + 1, m - 1, 0) + dp(m, r, kk + 1))
        return best

    result = dp(0, n - 1, 0)
    dp.cache_clear()
    return result


# ── LC902 最大为 N 的数字组合(Hard) ───────────────────────────────────────
def at_most_n_given_digit_set(digits: list[str], n: int) -> int:
    """
    【题意】给定一个从小到大排好序的数字字符集合 digits（比如 ["1","3","5","7"]），每个数字
    可以重复使用任意次，求能拼出多少个满足"值 <= n"的正整数。这是"数位 DP"的入门标杆题：
    逐位构造、按"是否贴合上界 n 的前缀"分类讨论。
    【思路】把 n 转成字符串 S，长度 L；D 表示 digits 的种类数。dp[i] 定义为"用 digits 拼出
    一个和 S 长度相同、且从第 i 位到最后一位**恰好贴合 S 的前 i 位前缀、继续往后拼**能得到
    的、值 <= S 的方案数"（严格来说是"后缀部分"的计数，从右往左递推）。从最后一位往前推：
    dp[L]=1（哨兵，代表"已经完整贴合了 S 的所有位"，这一种"恰好等于 n"的方案要被计入）；
    dp[i] 的计算：枚举 digits 里的每个候选数字 d，如果 d < S[i]（这一位选的数字比 n 对应
    位小，那么后面 L-i-1 位可以随便选，每一位都有 D 种选择），贡献 D^(L-i-1) 种方案；如果
    d == S[i]（这一位恰好贴合，继续递归贴合下一位），贡献 dp[i+1] 种方案；如果 d > S[i]，
    这一位就已经超过 n 对应位，不合法，不贡献。最终"长度恰好等于 L"的合法方案数是 dp[0]；
    此外还要加上所有"长度严格小于 L"的方案（这些方案只要位数比 n 少，值必然小于 n，天然
    合法），数目是 sum(D^i for i in range(1, L))（i 位长的方案数是 D^i，i 从 1 到 L-1）。
    【复杂度】时间 O(L·D)（L 是 n 的位数，通常在 10 以内；D 是 digits 集合大小，最多 9），
    空间 O(L)。
    【易错点】1) 这题的"数位 DP"和常见"计数满足某数字属性（如各位数字和、不含某数字）的
    数字个数"那种数位 DP 略有不同——这里每一位的候选集合是固定的 digits 集合而不是 0~9，
    如果不注意，容易在统计"比当前位小的候选个数"这一步直接套用"0~S[i]-1 共 S[i] 个"这种
    常规公式，但实际上应该数的是"digits 集合里比 S[i] 小的元素个数乘以 D^(剩余位数)"；
    2) "长度小于 L 的数字天然合法"这个加法项容易被漏掉——只算长度恰好等于 L 的方案会
    漏掉大量真正合法的、位数更少的候选数；3) dp[i+1] 这个"贴合"分支要求 digits 里恰好存在
    等于 S[i] 的数字才会被触发（如果 digits 里没有这个数字，"==" 分支根本不会命中，
    自然不会加上 dp[i+1]，不需要额外的存在性检查代码，只要用简单的 if d==S[i] 判断即可,
    但容易误以为需要提前检查"S[i] 是否在 digits 里"这类多余的分支）。
    """
    s = str(n)
    length = len(s)
    d_count = len(digits)
    dp = [0] * length + [1]
    for i in range(length - 1, -1, -1):
        for d in digits:
            if d < s[i]:
                dp[i] += d_count ** (length - i - 1)
            elif d == s[i]:
                dp[i] += dp[i + 1]
    return dp[0] + sum(d_count**i for i in range(1, length))


# ── LC1074 元素和为目标值的子矩阵数量(Medium) ─────────────────────────────
def num_submatrix_sum_target(matrix: list[list[int]], target: int) -> int:
    """
    【题意】给定矩阵 matrix 和整数 target，求有多少个（可以不同大小、不同位置的）子矩阵，
    其内部所有元素之和恰好等于 target。这是把「和为 K 的子数组」（LC560，一维前缀和+哈希）
    推广到二维的经典练习：先用二维前缀和把"任意子矩阵"压缩降维成"一维子数组"，再复用一维
    的哈希计数技巧。
    【思路】固定子矩阵的"上边界 top"和"下边界 bottom"两行，把这两行之间（含首尾）的每一列
    元素累加成一个一维数组 col_sum（col_sum[c] = 这一列在 [top,bottom] 行范围内的和）。
    此时，"top 到 bottom 之间、任意左右边界的子矩阵"就一一对应"col_sum 数组里任意一段连续
    子数组"，问题退化成 LC560：用前缀和+哈希表统计 col_sum 中有多少段连续子数组的和恰好
    等于 target（对每个前缀和 cur，查哈希表里 cur-target 出现过多少次，累加计数，再把 cur
    本身计入哈希表）。外层双重循环枚举所有 (top, bottom) 组合（bottom 从 top 开始递增，
    这样可以增量更新 col_sum 而不用每次重新算），内层对每个 (top,bottom) 组合都做一次
    O(n) 的一维子数组计数，整体三层嵌套。
    【复杂度】时间 O(m^2·n)（m 是行数，n 是列数；外两层枚举 top、bottom 共 O(m^2)，内层
    一维哈希计数是 O(n)），空间 O(n)（col_sum 数组和哈希表）。
    【易错点】1) col_sum 必须在固定 top 之后、bottom 递增的过程中**增量更新**（每次只加上
    新的一行 bottom 的值），而不是每次都从 top 到 bottom 重新求和，否则复杂度会多一个
    O(m) 因子，退化成 O(m^3·n)；2) 一维哈希计数里，"前缀和为 0"要预置 {0:1} 这个初始状态
    （代表"空前缀"），这是 LC560 同款的易错点，直接影响"子数组从最左边开始"这类边界情况
    能否被正确计数；3) 每一对 (top, bottom) 都需要一个**全新的**哈希表（对应"这一批
    子矩阵是独立统计的"），如果不小心在外层循环之间复用了同一个哈希表而不清空，会把
    不同 (top,bottom) 对应的子矩阵错误地混在一起计数。
    """
    m, n = len(matrix), len(matrix[0])
    count = 0
    for top in range(m):
        col_sum = [0] * n
        for bottom in range(top, m):
            for c in range(n):
                col_sum[c] += matrix[bottom][c]
            prefix_count = {0: 1}
            cur = 0
            for v in col_sum:
                cur += v
                count += prefix_count.get(cur - target, 0)
                prefix_count[cur] = prefix_count.get(cur, 0) + 1
    return count


# ── LC828 统计子串中的唯一字符(Hard) ──────────────────────────────────────
def unique_letter_string(s: str) -> int:
    """
    【题意】定义 countUniqueChars(t) 为字符串 t 中"只出现恰好一次"的字符个数（比如
    "LEETCODE" 里 L、T、C、O、D 各只出现一次，countUniqueChars=5）。给定字符串 s，求
    s 的**所有子串**（可以重复取相同的子串，重复也要累加）的 countUniqueChars 之和。
    直接枚举所有 O(n^2) 个子串再对每个子串统计唯一字符是 O(n^3) 起步，本题需要"逐字符
    贡献法"才能降到线性。
    【思路】不去想"每个子串贡献多少"，反过来想"每个字符在哪些子串里贡献了 1 分"：对字符串
    中位置 i 上的字符 ch，它在子串 t 中"恰好出现一次"，当且仅当 t 完整包含位置 i、且不包含
    ch 在 i 左边最近的上一次出现（记作 prev）、也不包含 ch 在 i 右边最近的下一次出现（记作
    next）——也就是说，子串的左端点可以自由取在 (prev, i] 这个开区间里（共 i-prev 种取法），
    右端点可以自由取在 [i, next) 这个区间里（共 next-i 种取法），两者是独立的自由选择，
    所以位置 i 上的字符一共在 (i-prev)*(next-i) 个不同子串中贡献了 1 分。对字符串每个位置
    都求出这个贡献值再求和，就是最终答案。prev 和 next 分别用一次从左到右、一次从右到左
    的扫描预处理出来（某字符不存在"上一次出现"时记为 -1，不存在"下一次出现"时记为 n）。
    【复杂度】时间 O(n)（两次线性扫描预处理 prev_occ/next_occ 数组，一次线性扫描求和），
    空间 O(n)（预处理数组；也可以只用 26 个字母的哈希表滚动维护，进一步压到 O(26)）。
    【易错点】1) 这道题最大的思维跨越是"从枚举子串转为枚举每个字符的贡献"，如果没有想到
    这一层转换，很容易陷入对每个子串暴力统计唯一字符的 O(n^3) 陷阱；2) prev_occ[i] 表示
    "同一字符在 i 左侧最近一次出现的下标"，不存在时必须用 -1（而不是 0），因为 (i-prev)
    这个乘数在"从未出现过"时应该等于 i-(-1)=i+1，代表"左端点可以取到最左边（下标 0）"这个
    完整范围，如果误用 0 会少算一种左端点的选法；3) 同理 next_occ[i] 不存在时要用字符串
    长度 n（而不是 n-1），(next-i) 这个乘数才能正确覆盖"右端点可以取到最右边（下标 n-1）"
    这个完整范围。
    """
    n = len(s)
    prev_occ = [-1] * n
    next_occ = [n] * n
    last: dict[str, int] = {}
    for i, ch in enumerate(s):
        if ch in last:
            prev_occ[i] = last[ch]
        last[ch] = i
    last = {}
    for i in range(n - 1, -1, -1):
        ch = s[i]
        if ch in last:
            next_occ[i] = last[ch]
        last[ch] = i
    return sum((i - prev_occ[i]) * (next_occ[i] - i) for i in range(n))


# ── LC1000 合并石头的最低成本(Hard) ───────────────────────────────────────
def merge_stones(stones: list[int], k: int) -> int:
    """
    【题意】一排 n 堆石子，每次操作必须选**恰好连续 k 堆**合并成一堆，代价是这 k 堆的石子
    总数，求把所有石子合并成 1 堆的最小总代价；如果不可能合并成 1 堆，返回 -1。这是"戳气球"
    「多边形三角剖分」一类区间 DP 的推广版：从"每次合并恰好 2 份"（二叉合并）推广到"每次
    恰好合并 k 份"。
    【思路】**可行性判断先行**：每次合并把 k 堆变成 1 堆，堆数净减少 k-1；从 n 堆减到 1 堆
    需要减少 n-1 堆，因此必须有 (n-1) 能被 (k-1) 整除，否则无论怎么合并都无法收尾到恰好
    1 堆，直接返回 -1。可行时，dp[i][j] 定义为"把区间 [i,j] 合并成**尽可能少的堆数**所需
    的最小代价"（这里故意不强制"合并成 1 堆"，因为合并成 1 堆只在特定长度下才可能，中间过程
    需要允许"合并到最少能停留的堆数"这一中间态）。转移：枚举第一段的分割点 mid（区间
    [i,mid] 和 [mid+1,j]），且 mid 的步进要跳着走（mid 从 i 开始，每次跳 k-1 步：
    for mid in range(i, j, k-1)）——这个跳步的含义是，要让 [i,mid] 这一段最终恰好能合并
    成"1 堆"（作为后续合并的一个基本单元），[i,mid] 的长度必须满足 (mid-i) % (k-1)==0，
    这个条件天然由跳步 k-1 保证；dp[i][j]=min(dp[i][mid]+dp[mid+1][j])。**关键的额外一步**：
    如果这一次「(j-i)%(k-1)==0」（也就是整个区间 [i,j] 的长度恰好允许被合并成 1 堆），
    就再执行一次"把这一整个区间的所有已经堆积的部分一次性合并成 1 堆"，代价是这个区间的
    元素总和，dp[i][j] += prefix[j+1]-prefix[i]（因为最后这一次合并动作，不管中间怎么分段,
    最终都要把停留在这个区间里的所有"半成品"堆再合并一次，代价固定是区间总和，累加一次
    即可，不需要在枚举 mid 时重复计入）。
    【复杂度】时间 O(n^2·n/k)=O(n^3/k)（区间数 O(n^2)，每个区间内枚举 mid 的步数是
    O(n/k)），空间 O(n^2)。
    【易错点】1) 枚举 mid 时步进必须是 k-1 而不是 1——如果按普通区间 DP 那样逐个枚举 mid,
    会产生大量"长度不满足 (mid-i)%(k-1)==0"的无效或错误的中间状态，虽然结果不一定错，
    但会让 dp[i][mid] 这个子问题在语义上不对应"能进一步参与合并"的中间形态；2) 判断
    "能否把整个区间合并成 1 堆"的条件是 (j-i)%(k-1)==0，如果漏掉这个条件、对所有区间都
    无脑执行"加一次区间和"，会给那些天然无法收尾成 1 堆的区间错误地叠加多余的代价；3) 最
    外层的可行性检查 (n-1)%(k-1)!=0 极易被忽略——很多人会直接跑 DP，对无法合并成 1 堆的
    输入，dp[0][n-1] 最终会残留成一个"不完整合并"的代价而不是 -1，必须在函数最开始就用
    这个数论条件提前拦截。
    """
    n = len(stones)
    if (n - 1) % (k - 1) != 0:
        return -1
    prefix = [0] * (n + 1)
    for i in range(n):
        prefix[i + 1] = prefix[i] + stones[i]
    dp = [[0] * n for _ in range(n)]
    for length in range(k, n + 1):
        for i in range(n - length + 1):
            j = i + length - 1
            dp[i][j] = float("inf")
            for mid in range(i, j, k - 1):
                dp[i][j] = min(dp[i][j], dp[i][mid] + dp[mid + 1][j])
            if (length - 1) % (k - 1) == 0:
                dp[i][j] += prefix[j + 1] - prefix[i]
    return dp[0][n - 1]


# ── LC1547 切棍子的最小成本(Hard) ─────────────────────────────────────────
def min_cost(n: int, cuts: list[int]) -> int:
    """
    【题意】一根长度为 n 的木棍，标记从 0 到 n。给定若干切割位置 cuts，切割顺序可以任意
    调整，每一刀的代价是"被切这一刀的那根小棍当前的长度"，求完成所有切割的最小总代价。
    【思路】切割顺序不同，代价不同的原因是"越早切的那一刀，往往切的是一根更长的棍子（代价
    更大）"，所以本质是要安排一个切割顺序使"长棍子被切的次数尽量少"。这可以转化成经典的
    区间 DP：先把 0、n 和所有 cuts 位置一起排序去重，得到 points 数组（这是"棍子上所有会
    成为某个最终小段端点的坐标"，起到和"戳气球"里"首尾补 1"、"多边形三角剖分"里"顶点序列"
    类似的**虚拟端点**作用：0 和 n 本身并不是真正的切割动作，但作为区间的左右边界参与 DP
    十分自然）。dp[i][j] 定义为"把 points[i] 到 points[j] 这一段（对应原始棍子上的一段
    连续区间）内部所有需要的切割都做完，所需的最小代价"。枚举这一段里**最后一个**（或者
    等价地说，视角上"第一个"）被切的位置 mid（i<mid<j，points[mid] 是这一段内部的某个
    候选切割点），代价是"这一刀发生时，这一整段棍子的长度" points[j]-points[i]（因为不管
    这一段内部之前已经切了多少刀，只要 mid 这一刀切的是"points[i] 到 points[j] 这一整段",
    它的代价就固定是这一整段的长度——这是"倒着想：把所有切割看成是在一段还完整的区间上
    切第一刀"这一区间 DP 的经典视角，和戳气球"把最后戳破的气球看成是这次决策的对象"是
    同一种"逆向定义决策顺序"的思路）。dp[i][j] = min over mid( dp[i][mid]+dp[mid][j] )
    + (points[j]-points[i])。base case：j-i<=1（区间内没有任何需要切割的候选点）时
    dp[i][j]=0（这一段不需要再切）。
    【复杂度】时间 O(m^3)（m 是 points 数组长度，即 cuts 个数+2；区间数 O(m^2)，每个区间
    枚举 mid 是 O(m)），空间 O(m^2)。
    【易错点】1) "0 和 n 要加入 points 参与排序"这一步很容易被漏掉——如果只对 cuts 本身
    做区间 DP，会丢失"最左/最右两端的棍子长度"这个信息，代价计算的边界会出错；2) 转移里
    的代价 points[j]-points[i] 对应的是"这一整段棍子当前的长度"，是**固定值**（不随 mid
    的选择变化），容易被误理解成需要根据 mid 动态计算，其实所有 mid 的取值共享同一个
    "本段总长度"作为这一刀的代价，只是枚举 mid 决定"先切哪一个内部点，把问题拆成两个
    子区间"；3) mid 的枚举范围是开区间 (i,j)（严格大于 i、严格小于 j），如果不小心让
    mid 取到 i 或 j 本身，会让区间退化成空区间或者产生自己切自己的无意义决策。
    """
    points = sorted(cuts + [0, n])
    m = len(points)
    dp = [[0] * m for _ in range(m)]
    for length in range(2, m):
        for i in range(m - length):
            j = i + length
            dp[i][j] = min(
                dp[i][mid] + dp[mid][j] + (points[j] - points[i]) for mid in range(i + 1, j)
            )
    return dp[0][m - 1]


